using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Enums
{
//-------------------------------------------------------------------------------------------------------
	public enum EColors
	{
//-------------------------------------------------------------------------------------------------------
		E_C_RED=1,
		E_C_GREEN=2,
		E_C_BLUE=3,
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	public static class CColorsExtensions
	{
//-------------------------------------------------------------------------------------------------------
		public static string NiceString(this EColors Value)
		{
			return(Value.ToString().Substring(4));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
