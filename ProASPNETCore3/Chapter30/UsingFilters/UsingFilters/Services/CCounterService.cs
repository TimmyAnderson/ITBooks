using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFilters.Services
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CCounterService : ICounterService
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly object									MObject;
//----------------------------------------------------------------------------------------------------------------------
		private int												MCounter;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCounterService()
		{
			MObject=new object();
			MCounter=0;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												Counter
		{
			get
			{
				lock(MObject)
				{
					return(MCounter);
				}
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void IncrementCounter()
		{
			lock(MObject)
			{
				MCounter++;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------