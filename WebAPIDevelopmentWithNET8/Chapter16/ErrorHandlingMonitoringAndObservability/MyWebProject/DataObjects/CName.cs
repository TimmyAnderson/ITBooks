using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Net;
using System.ComponentModel.DataAnnotations;
//--------------------------------------------------------------------------------------------------------------------------------
namespace MyWebProject
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CName
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CName()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CName(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		[Required]
		[StringLength(15,ErrorMessage="FIRST NAME is LONGER than 15 CHARACTERS.")]
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
			set
			{
				MFirstName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[Required]
		[StringLength(20,ErrorMessage="LAST NAME is LONGER than 20 CHARACTERS.")]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			set
			{
				MLastName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[Required]
		[Range(0,100,ErrorMessage="AGE is OUT of RANGE <0,100>.")]
		public int												Age
		{
			get
			{
				return(MAge);
			}
			set
			{
				MAge=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------------------------------------------------------