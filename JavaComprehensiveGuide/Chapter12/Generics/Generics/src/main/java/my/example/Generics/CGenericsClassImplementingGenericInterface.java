package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CGenericsClassImplementingGenericInterface<TParameter1,TParameter2> implements IGenericsInterface<TParameter1,TParameter2>
{
//----------------------------------------------------------------------------------------------------------------------
	private final TParameter1									value1;
	private final TParameter2									value2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsClassImplementingGenericInterface(TParameter1 value1, TParameter2 value2)
	{
		this.value1=value1;
		this.value2=value2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public TParameter1 getValue1()
	{
		return(value1);
	}
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public TParameter2 getValue2()
	{
		return(value2);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------