#include <iostream>
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
class CCommaOperators final
{
//-----------------------------------------------------------------------------
	private:
		int														MValue;

	public:
		int operator,(const CCommaOperators& Value)
		{
			std::wcout << "MEMBER OPERATOR, CALLED !" << std::endl;

			return(MValue + Value.MValue);
		}

	public:
		int GetValue(void) const noexcept
		{
			return(MValue);
		}

	public:
		CCommaOperators(int Value)
			: MValue(Value)
		{
		}
//-----------------------------------------------------------------------------
};
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
inline int operator,(int Left, const CCommaOperators& Right)
{
	std::wcout << "NON-MEMBER OPERATOR, CALLED !" << std::endl;

	return(Left + Right.GetValue());
}
