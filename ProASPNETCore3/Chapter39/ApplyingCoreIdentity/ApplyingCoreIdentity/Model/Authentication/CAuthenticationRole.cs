using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Identity;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CAuthenticationRole
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MRoleID;
		private string											MRoleName;
		private string											MRoleDescription;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CAuthenticationRole()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CAuthenticationRole(string RoleID, string RoleName, string RoleDescription)
		{
			MRoleID=RoleID;
			MRoleName=RoleName;
			MRoleDescription=RoleDescription;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											RoleID
		{
			get
			{
				return(MRoleID);
			}
			set
			{
				MRoleID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											RoleName
		{
			get
			{
				return(MRoleName);
			}
			set
			{
				MRoleName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											RoleDescription
		{
			get
			{
				return(MRoleDescription);
			}
			set
			{
				MRoleDescription=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------