using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace Trees
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	public class CTreeNodeData : IEquatable<CTreeNodeData>
	{
//-------------------------------------------------------------------------------------------------------
		private CTreeNodeID										MTreeNodeID;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeData(CTreeNodeID TreeNodeID)
		{
			MTreeNodeID=TreeNodeID;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public CTreeNodeID										TreeNodeID
		{
			get
			{
				return(MTreeNodeID);
			}
			private set
			{
				MTreeNodeID=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public bool Equals(CTreeNodeData Other)
		{
			if (Other==null)
				return(false);

			if (MTreeNodeID!=Other.MTreeNodeID)
				return(false);

			return(true);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			StringBuilder		SB=new StringBuilder();

			SB.Append(this.GetType().Name);
			SB.AppendFormat(", TreeObjectID: [{0}]",MTreeNodeID);

			return(SB.ToString());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------