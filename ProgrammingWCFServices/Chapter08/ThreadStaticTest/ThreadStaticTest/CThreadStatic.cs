using System;
using System.Collections.Generic;
using System.Text;
using System.Threading;
//-------------------------------------------------------------------------------------------------------
namespace ThreadStaticTest
{
//-------------------------------------------------------------------------------------------------------
	class CThreadStatic
	{
//-------------------------------------------------------------------------------------------------------
		[ThreadStatic]
		private static int										MValue=0;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		static public void ThreadFunction(object Param)
		{
			int			Value=(int) Param;

			Console.WriteLine(string.Format("CThreadStatic.ThreadFunction(): {0} START !",Value));

			MValue=Value;
			Thread.Sleep(3000);

			Console.WriteLine(string.Format("CThreadStatic.ThreadFunction(): {0} MVALUE: {1} !",Value,MValue));

			Console.WriteLine(string.Format("CThreadStatic.ThreadFunction(): {0} END !",Value));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------