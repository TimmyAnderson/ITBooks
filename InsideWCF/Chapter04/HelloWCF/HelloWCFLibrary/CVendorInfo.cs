using System;
using System.Collections.Generic;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace HelloWCFLibrary
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	public class CVendorInfo
	{
//-------------------------------------------------------------------------------------------------------
        private int												MVendorNumber;
        private string											MVendorName;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CVendorInfo(int VendorNumber, string VendorName)
		{
			MVendorNumber=VendorNumber;
			MVendorName=VendorName;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
        public int					VendorNumber
		{
			get
			{
				return(MVendorNumber);
			}
			set
			{
				MVendorNumber=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
        public string				VendorName
		{
			get
			{
				return(MVendorName);
			}
			set
			{
				MVendorName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("VendorNumber: {0}, VendorName: {1}",MVendorNumber,MVendorName));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------