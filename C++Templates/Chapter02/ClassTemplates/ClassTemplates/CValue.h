//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
class CValue
{
//----------------------------------------------------------------------------------------------------------------------
	private:
		TType													MValue;

	public:
		const TType& Get(void) const noexcept
		{
			return(MValue);
		}

	public:
		void Add(const TType& Value)
		{
			MValue=(MValue+Value);
		}

		void Subtract(const TType& Value)
		{
			MValue=(MValue-Value);
		}

	public:
		CValue(const TType& Value)
			: MValue(Value)
		{
		}
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------