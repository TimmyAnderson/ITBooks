using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace CSharpEssentialFeatures
{
//----------------------------------------------------------------------------------------------------------------------
	public interface IPersonSource
	{
//----------------------------------------------------------------------------------------------------------------------
		CPerson[] GetPersons();
//----------------------------------------------------------------------------------------------------------------------
		// !!! DEFAULT METHOD IMPLEMENTATION.
		CPerson[] GetMales()
		{
			CPerson[]											Persons=GetPersons();
			CPerson[]											Males=Persons.Where(P => P.Sex==ESex.Male).ToArray();

			return(Males);
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! DEFAULT METHOD IMPLEMENTATION.
		CPerson[] GetFemales()
		{
			CPerson[]											Persons=GetPersons();
			CPerson[]											Females=Persons.Where(P => P.Sex==ESex.Female).ToArray();

			return(Females);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
