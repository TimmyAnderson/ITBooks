using System;
//--------------------------------------------------------------------------------------------------------------------------------
namespace WebAPICommonPracticesWeb
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CPaginatedArray<TType> where TType : class
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly TType[]								MItems;
		private readonly int									MPageIndex;
		private readonly int									MPageSize;
		private readonly int									MTotalPages;
		private readonly bool									MHasPreviousPage;
		private readonly bool									MHasNextPage;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CPaginatedArray(TType[] Items, int PageIndex, int PageSize, int TotalItems)
		{
			MItems=Items;
			MPageIndex=PageIndex;
			MPageSize=PageSize;
			MTotalPages=(int)Math.Ceiling(TotalItems/(double)PageSize);
			MHasPreviousPage=(MPageIndex>1);
			MHasNextPage=(MPageIndex<MTotalPages);
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public TType[]											Items
		{
			get
			{
				return(MItems);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												PageIndex
		{
			get
			{
				return(MPageIndex);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												PageSize
		{
			get
			{
				return(MPageSize);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												TotalPages
		{
			get
			{
				return(MTotalPages);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public bool												HasPreviousPage
		{
			get
			{
				return(MHasPreviousPage);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public bool												HasNextPage
		{
			get
			{
				return(MHasNextPage);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------------------------------------------------------