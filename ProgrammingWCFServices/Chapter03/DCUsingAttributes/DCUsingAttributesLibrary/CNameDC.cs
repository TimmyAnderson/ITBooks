using System;
using System.Collections.Generic;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace DCUsingAttributesLibrary
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	public class CNameDC
	{
//-------------------------------------------------------------------------------------------------------
		// Oznacim MFirstName, ale pri MLastName pouzijem property.
		[DataMember]
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CNameDC()
		{
			MFirstName="NOT SERIALIZED";
			MLastName="NOT SERIALIZED";
			MAge=0;
		}
//-------------------------------------------------------------------------------------------------------
		public CNameDC(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			internal set
			{
				MLastName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public int												Age
		{
			get
			{
				return(MAge+3);
			}
			set
			{
				MAge=value+3;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("CNameDC - FirstName: {0}, LastName: {1}, Age: {2} !",MFirstName, MLastName, MAge));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------