CTypeWithMethods::CTypeWithMethods(MY_TYPE Value)
	: MValue(Value)
{
}
//----------------------------------------------------------------------------------------------------------------------
CTypeWithMethods::~CTypeWithMethods(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CTypeWithMethods::MY_TYPE CTypeWithMethods::GetValue(void) const noexcept
{
	return(MValue);
}
//----------------------------------------------------------------------------------------------------------------------
void CTypeWithMethods::SetValue(double Value) noexcept
{
	MValue=Value;
}
//----------------------------------------------------------------------------------------------------------------------
void CTypeWithMethods::SetValue(double Value1, double Value2) noexcept
{
	MValue=(Value1+Value2);
}
