using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFilters.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CResultMessageWithTime
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MMessage;
		private readonly string									MTime;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CResultMessageWithTime(string Message, string Time)
		{
			MMessage=Message;
			MTime=Time;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Message
		{
			get
			{
				return(MMessage);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											Time
		{
			get
			{
				return(MTime);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------