using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//-------------------------------------------------------------------------------------------------------
namespace DynamicTypes
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CWrappedInt
	{
//-------------------------------------------------------------------------------------------------------
		private int												MValue;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CWrappedInt(int Value)
		{
			MValue=Value;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int												Value
		{
			get
			{
				return(MValue);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public static CWrappedInt operator+(CWrappedInt Value1, CWrappedInt Value2)
		{
			Console.WriteLine("OVERLOADED OPERATOR+ CALLED !");

			CWrappedInt											NewInt=new CWrappedInt(Value1.MValue+Value2.Value);

			return(NewInt);
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("MValue: [{0}]",MValue));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
