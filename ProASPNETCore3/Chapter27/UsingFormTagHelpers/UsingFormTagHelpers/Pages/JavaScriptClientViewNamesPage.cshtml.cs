using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingFormTagHelpers.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFormTagHelpers.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class JavaScriptClientViewNamesPage : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly IPageNameProvider						MPageNameProvider;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public JavaScriptClientViewNamesPage(IPageNameProvider PageNameProvider)
		{
			MPageNameProvider=PageNameProvider;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CPageName[]										PageNames
		{
			get
			{
				return(MPageNameProvider.GetNames());
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnGet()
        {
			// !!! Zobrazia sa data ulozene v STORAGE.
			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnPost(CPageName PageName)
        {
			// !!! NAME sa zapise do STORAGE.
			MPageNameProvider.AddName(PageName);

			// !!! Spravi sa REDIRECT na SAM SEBA, aby sa v GET vratila UPDATED verzia dat zo STORAGE.
			IActionResult										Result=RedirectToPage();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------