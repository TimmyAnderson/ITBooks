//--------------------------------------------------------------------------------------------------------------------------------
namespace PublishSubscribePublisher
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CMessage
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private int												MValue1;
		private int												MValue2;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CMessage()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CMessage(int Value1, int Value2)
		{
			MValue1=Value1;
			MValue2=Value2;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public int												Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
