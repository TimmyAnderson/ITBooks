#include <chrono>
#include <thread>
#include "MyDebug.h"
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
class CTaskFunctor
{
//-----------------------------------------------------------------------------
	private:
		int														MIndex;

	public:
		void operator()(int Index)
		{
			wprintf_s(L"TASK OPERATOR() [%d,%d] STARTED !\n",MIndex,Index);

			// Caka sa 5 sekund.
			std::this_thread::sleep_for(std::chrono::duration<int,std::ratio<1,1>>(5));

			wprintf_s(L"TASK OPERATOR() [%d,%d] FINISHED !\n",MIndex,Index);
		}

	public:
		CTaskFunctor(int Index)
			: MIndex(Index)
		{
		}
//-----------------------------------------------------------------------------
};
//-----------------------------------------------------------------------------