using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingRazorPages.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingRazorPages.Pages
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS implementuje RAZOR PAGE MODEL.
    public class ShowNameUsingParameters : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private CName											MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName											Name
		{
			get
			{
				return(MName);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! METHOD sa vola ako reakcia na HTTP GET VERB.
		// !!! METHOD ma PARAMETER, ktory je mapovany na PARAMETER QUERY STRING.
        public void OnGet(int ID)
        {
			if (ID==1)
			{
				MName=new CName(ID,"Timmy","Anderson",12);
			}
			else if (ID==2)
			{
				MName=new CName(ID,"Jenny","Thompson",13);
			}
			else
			{
				throw(new InvalidOperationException("UNSUPPORTED ID !"));
			}
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
