package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CNonGenericsClassImplementingGenericInterface implements IGenericsInterface<String,Long>
{
//----------------------------------------------------------------------------------------------------------------------
	private final String										value1;
	private final Long											value2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CNonGenericsClassImplementingGenericInterface(String value1, Long value2)
	{
		this.value1=value1;
		this.value2=value2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public String getValue1()
	{
		return(value1);
	}
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public Long getValue2()
	{
		return(value2);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------