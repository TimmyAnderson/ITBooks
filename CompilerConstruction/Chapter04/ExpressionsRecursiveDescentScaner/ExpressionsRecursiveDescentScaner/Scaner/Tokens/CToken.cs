using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ExpressionsRecursiveDescentScaner
{
//-------------------------------------------------------------------------------------------------------
	public class CToken
	{
//-------------------------------------------------------------------------------------------------------
		private string											MTokenID;
		private StringBuilder									MLexema;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CToken()
		{
			MTokenID=TokenID;
			MLexema=new StringBuilder();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											TokenID
		{
			get
			{
				return(MTokenID);
			}
			set
			{
				MTokenID=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											Lexema
		{
			get
			{
				return(MLexema.ToString());
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void AddChar(char NewChar)
		{
			MLexema.Append(NewChar);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(Lexema);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
