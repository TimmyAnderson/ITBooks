package Chapter13.UsingTransactions.Transactions;
//----------------------------------------------------------------------------------------------------------------------
// !!! CLASS obsahuje RECORD z DATABASE TABLE 'Accounts'.
public class CAccount
{
//----------------------------------------------------------------------------------------------------------------------
	private int													MID;
	private String												MName;
	private int													MAmount;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CAccount()
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	public CAccount(int ID, String Name, int Amount)
	{
		MID=ID;
		MName=Name;
		MAmount=Amount;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public int getID()
	{
		return(MID);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setID(int ID)
	{
		MID=ID;
	}
//----------------------------------------------------------------------------------------------------------------------
	public String getName()
	{
		return(MName);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setName(String Name)
	{
		MName=Name;
	}
//----------------------------------------------------------------------------------------------------------------------
	public int getAmount()
	{
		return(MAmount);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setAmount(int Amount)
	{
		MAmount=Amount;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------