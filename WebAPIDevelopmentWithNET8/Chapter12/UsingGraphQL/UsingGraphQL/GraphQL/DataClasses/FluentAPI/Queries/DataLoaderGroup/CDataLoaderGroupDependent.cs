using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CDataLoaderGroupDependent
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly int									MDependentID;
		private readonly string									MDependentValue1;
		private readonly string									MDependentValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CDataLoaderGroupDependent(int DependentID, string DependentValue1, string DependentValue2)
		{
			MDependentID=DependentID;
			MDependentValue1=DependentValue1;
			MDependentValue2=DependentValue2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												DependentID
		{
			get
			{
				return(MDependentID);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											DependentValue1
		{
			get
			{
				return(MDependentValue1);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											DependentValue2
		{
			get
			{
				return(MDependentValue2);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
