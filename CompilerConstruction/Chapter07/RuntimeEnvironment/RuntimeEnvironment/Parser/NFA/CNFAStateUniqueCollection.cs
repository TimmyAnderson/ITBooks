using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace RuntimeEnvironment
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CNFAStateUniqueCollection
	{
//-------------------------------------------------------------------------------------------------------
		private readonly List<CNFAState>						MNFAStates;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CNFAStateUniqueCollection()
		{
			MNFAStates=new List<CNFAState>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CNFAState[]										NFAStates
		{
			get
			{
				return(MNFAStates.ToArray());
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public bool Add(CNFAState NewNFAState)
		{
			bool			StateExists=false;

			foreach(CNFAState ExistingNFAState in MNFAStates)
			{
				if (ExistingNFAState.ContainsLR1Item(NewNFAState.Item)==true)
				{
					StateExists=true;
					break;
				}
			}

			if (StateExists==false)
			{
				MNFAStates.Add(NewNFAState);
				return(true);
			}
			else
			{
				return(false);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------