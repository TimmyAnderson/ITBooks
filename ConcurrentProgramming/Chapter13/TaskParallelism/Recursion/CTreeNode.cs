using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//------------------------------------------------------------------------------
namespace Recursion
{
//------------------------------------------------------------------------------
	public class CTreeNode
	{
//------------------------------------------------------------------------------
		private string							MValue;
		private CTreeNode						MLeft;
		private CTreeNode						MRight;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
		public CTreeNode(string Value, CTreeNode Left, CTreeNode Right)
		{
			MValue=Value;
			MLeft=Left;
			MRight=Right;
		}
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
		public string							Value
		{
			get
			{
				return(MValue);
			}
			set
			{
				MValue=Value;
			}
		}
//------------------------------------------------------------------------------
		public CTreeNode						Left
		{
			get
			{
				return(MLeft);
			}
			set
			{
				MLeft=value;
			}
		}
//------------------------------------------------------------------------------
		public CTreeNode						Right
		{
			get
			{
				return(MRight);
			}
			set
			{
				MRight=value;
			}
		}
//------------------------------------------------------------------------------
	}
//------------------------------------------------------------------------------
}
