using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using RealApplication2.Infrastructure;
using RealApplication2.Models;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication2.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class CartModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly IStoreRepository						MStoreRepository;
//----------------------------------------------------------------------------------------------------------------------
		private CCart											MCart;
		private string											MReturnUrl;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CartModel(IStoreRepository StoreRepository)
		{
			MStoreRepository=StoreRepository;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCart											Cart
		{
			get
			{
				return(MCart);
			}
			set
			{
				MCart=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ReturnUrl
		{
			get
			{
				return(MReturnUrl);
			}
			set
			{
				MReturnUrl=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void OnGet(string ReturnUrl)
		{
			this.ReturnUrl=ReturnUrl ?? "/";

			Cart=HttpContext.Session.GetJson<CCart>("Cart") ?? new CCart();
		}
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult OnPost(long ProductID, string ReturnUrl)
		{
			CProduct											Product=MStoreRepository.Products.FirstOrDefault(P => P.ProductID==ProductID);

			Cart=HttpContext.Session.GetJson<CCart>("Cart") ?? new CCart();

			Cart.AddItem(Product,1);

			HttpContext.Session.SetJson("Cart",Cart);

			IActionResult										Result=RedirectToPage(new {ReturnUrl = ReturnUrl});

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------