using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Optimizations
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CSourceCodeLine
	{
//-------------------------------------------------------------------------------------------------------
		private readonly int									MSourceCodeIndex;
		private readonly string									MSouceCodeLine;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSourceCodeLine(int SourceCodeIndex, string SouceCodeLine)
		{
			MSourceCodeIndex=SourceCodeIndex;
			MSouceCodeLine=SouceCodeLine;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int												SourceCodeIndex
		{
			get
			{
				return(MSourceCodeIndex);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											SouceCodeLine
		{
			get
			{
				return(MSouceCodeLine);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string GetSouceCodeLineWithoutNewLineCharacter()
		{
			string												SourceCodeLine=MSouceCodeLine.TrimEnd('\r','\n');

			return(SourceCodeLine);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(GetSouceCodeLineWithoutNewLineCharacter());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
