using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Scaner
{
//-------------------------------------------------------------------------------------------------------
	public class CInputChar_AnyNumber : CInputChar
	{
//-------------------------------------------------------------------------------------------------------
		public CInputChar_AnyNumber(string Name)
			: base(Name)
		{

		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override bool IsValidChar(char CharToCheck)
		{
			if (CharToCheck>='0' && CharToCheck<='9')
				return(true);
			else
				return(false);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
