package Chapter10.ImplementingRESTServices.ExceptionHandling;
//----------------------------------------------------------------------------------------------------------------------
// !!! CLASS reprezentuje HTTP RESPONSE BODY ak WEB SERVICE OPERATION skonci uspesne.
public class CResponseOK
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue1;
	private String												MValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CResponseOK()
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	public CResponseOK(String Value1, String Value2)
	{
		MValue1=Value1;
		MValue2=Value2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue1()
	{
		return(MValue1);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue1(String Value1)
	{
		MValue1=Value1;
	}
//----------------------------------------------------------------------------------------------------------------------
	public String getValue2()
	{
		return(MValue2);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue2(String Value2)
	{
		MValue2=Value2;
	}
//----------------------------------------------------------------------------------------------------------------------
}
