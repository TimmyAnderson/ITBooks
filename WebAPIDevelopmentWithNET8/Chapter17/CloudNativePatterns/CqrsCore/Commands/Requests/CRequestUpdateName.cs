using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CRequestUpdateName : IRequest<CDtoFullName>
	{
//----------------------------------------------------------------------------------------------------------------------
		private CDtoNameToUpdate								MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CRequestUpdateName()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CRequestUpdateName(CDtoNameToUpdate Name)
		{
			MName=Name;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CDtoNameToUpdate									Name
		{
			get
			{
				return(MName);
			}
			set
			{
				MName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------