using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Events
{
//-------------------------------------------------------------------------------------------------------
	public class CTimerEventArgs : EventArgs
	{
//-------------------------------------------------------------------------------------------------------
		private readonly string									MID;
		private readonly DateTime								MActualTime;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTimerEventArgs(string ID, DateTime ActualTime)
		{
			MID=ID;
			MActualTime=ActualTime;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											ID
		{
			get
			{
				return(MID);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public DateTime											ActualTime
		{
			get
			{
				return(MActualTime);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------