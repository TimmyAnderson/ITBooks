using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Identity;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CLogin
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MUserName;
		private string											MPassword;
		private string											MReturnURL;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CLogin()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CLogin(string UserName, string Password, string ReturnURL)
		{
			MUserName=UserName;
			MPassword=Password;
			MReturnURL=ReturnURL;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
			set
			{
				MUserName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Password
		{
			get
			{
				return(MPassword);
			}
			set
			{
				MPassword=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											ReturnURL
		{
			get
			{
				return(MReturnURL);
			}
			set
			{
				MReturnURL=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------