//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE FUNCTION ma EXTERNAL LINKAGE.
template<typename TType>
void TemplateLinkageExternalLinage(TType Value)
{
	std::wcout << L"CTemplateLinkage1 - EXTERNAL LINKAGE - VALUE [" << Value << L"]." << std::endl;
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
void TestExternalLinage1(void)
{
	TemplateLinkageExternalLinage(100);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE FUNCTION ma INTERNAL LINKAGE, pretoze je pouzity KEYWORD [static].
template<typename TType>
static void TemplateLinkageInternalLinage(TType Value)
{
	std::wcout << L"CTemplateLinkage1 - INTERNAL LINKAGE - VALUE [" << Value << L"]." << std::endl;
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
void TestInternalLinage1(void)
{
	TemplateLinkageInternalLinage(100);
}
