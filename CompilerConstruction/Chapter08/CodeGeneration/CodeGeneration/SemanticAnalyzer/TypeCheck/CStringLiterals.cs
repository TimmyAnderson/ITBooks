using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CodeGeneration
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CStringLiterals
	{
//-------------------------------------------------------------------------------------------------------
		private Dictionary<string,string>						MLiterals;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CStringLiterals()
		{
			MLiterals=new Dictionary<string,string>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void AddLiteral(string Literal)
		{
			if (MLiterals.ContainsKey(Literal)==false)
			{
				int												NumberOfLiterals=MLiterals.Count;

				NumberOfLiterals++;

				string											ID=string.Format("_STRING_{0}",NumberOfLiterals);

				MLiterals.Add(Literal,ID);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string[] GetAllLiterals()
		{
			string[]											Literals=MLiterals.Keys.ToArray();

			return(Literals);
		}
//-------------------------------------------------------------------------------------------------------
		public string GetIDForLiteral(string Literal)
		{
			string												ID=MLiterals[Literal];

			return(ID);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
