using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFilters.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CResultMessageWithExecutionTime
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MMessage;
		private readonly string									MExecutionTime;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CResultMessageWithExecutionTime(string Message, string ExecutionTime)
		{
			MMessage=Message;
			MExecutionTime=ExecutionTime;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Message
		{
			get
			{
				return(MMessage);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ExecutionTime
		{
			get
			{
				return(MExecutionTime);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------