using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using UsingDependencyInjection.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Controllers
{
//----------------------------------------------------------------------------------------------------------------------
	public class FactoryFunctionController : Controller
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly IFactoryFunctionService				MFactoryFunctionService;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public FactoryFunctionController(IFactoryFunctionService FactoryFunctionService)
		{
			MFactoryFunctionService=FactoryFunctionService;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult MyActionMethod()
		{
			CName[]												Model=MFactoryFunctionService.GetNames();
			ViewResult											Result=View("Names",Model);

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
