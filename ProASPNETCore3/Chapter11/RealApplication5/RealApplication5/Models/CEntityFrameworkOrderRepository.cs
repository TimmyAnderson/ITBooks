using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.EntityFrameworkCore;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication5.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CEntityFrameworkOrderRepository : IOrderRepository
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly CApplicationDbContext					MContext;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityFrameworkOrderRepository(CApplicationDbContext Context)
		{
			MContext=Context;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IQueryable<COrder>								Orders
		{
			get
			{
				IQueryable<COrder>								Result=MContext.Orders.Include(P => P.Lines).ThenInclude(P => P.Product);

				return(Result);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void SaveOrder(COrder Order)
		{
			MContext.AttachRange(Order.Lines.Select(P => P.Product));

			if (Order.OrderID==0)
			{
				MContext.Orders.Add(Order);
			}

			MContext.SaveChanges();
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
