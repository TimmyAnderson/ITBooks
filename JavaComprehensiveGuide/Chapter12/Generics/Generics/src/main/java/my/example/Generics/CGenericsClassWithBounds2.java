package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
// !!! BOUND obsahuje viacero CONDITIONS.
public final class CGenericsClassWithBounds2<TParameter extends CBoundsClassBase1<String> & IBoundsInterface1<Integer> & IBoundsInterface2<Long>>
{
//----------------------------------------------------------------------------------------------------------------------
	private final TParameter									value;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsClassWithBounds2(TParameter value)
	{
		this.value=value;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public void Print()
	{
		// !!! Pouzitie BOUND umoznuje pouzitie METHODS BOUND TYPE.
		String													innerValue1=value.getValueClass();
		
		// !!! Pouzitie BOUND umoznuje pouzitie METHODS BOUND TYPE.
		Integer													innerValue2=value.getValueInterface1();
		
		// !!! Pouzitie BOUND umoznuje pouzitie METHODS BOUND TYPE.
		Long													innerValue3=value.getValueInterface2();
		
		System.out.printf("BOUNDS - VALUE 1 [%s] VALUE 2 [%s] VALUE 3 [%s].\n",innerValue1,innerValue2,innerValue3);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public TParameter getValue()
	{
		return(value);
	}
//----------------------------------------------------------------------------------------------------------------------
}
