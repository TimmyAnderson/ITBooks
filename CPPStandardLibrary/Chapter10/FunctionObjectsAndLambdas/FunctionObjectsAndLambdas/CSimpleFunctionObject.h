class CSimpleFunctionObject final
{
//-----------------------------------------------------------------------------
	private:
		bool													MIsFirst=true;

	public:
		void operator()(const std::wstring& Value)
		{
			if (MIsFirst==false)
			{
				std:: wcout << L" ";
			}
			else
			{
				MIsFirst=false;
			}

			std::wcout << Value;
		}
//-----------------------------------------------------------------------------
};
