using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace Trees
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CTreeNode : IEquatable<CTreeNode>
	{
//-------------------------------------------------------------------------------------------------------
		private readonly CTreeNodeData							MTreeNodeData;
//-------------------------------------------------------------------------------------------------------
		private int												MTreeOccurrences;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNode(CTreeNodeData TreeNodeData)
		{
			MTreeNodeData=TreeNodeData;
			MTreeOccurrences=0;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		internal int											TreeOccurrences
		{
			get
			{
				return(MTreeOccurrences);
			}
			set
			{
				MTreeOccurrences=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeID										TreeNodeID
		{
			get
			{
				return(MTreeNodeData.TreeNodeID);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeData									TreeNodeData
		{
			get
			{
				return(MTreeNodeData);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public bool Equals(CTreeNode Other)
		{
			if (Other==null)
				return(false);

			bool		Ret=MTreeNodeData.Equals(Other.MTreeNodeData);

			return(Ret);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			StringBuilder		SB=new StringBuilder();

			SB.Append(this.GetType().Name);
			SB.AppendFormat(", TreeObjectID: [{0}]",TreeNodeID);

			return(SB.ToString());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
