namespace RealApplication5.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
	public partial class Products : OwningComponentBase<IStoreRepository>
	{
//----------------------------------------------------------------------------------------------------------------------
		private IEnumerable<CProduct>							MProductData;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IStoreRepository									Repository
		{
			get
			{
				return(Service);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public IEnumerable<CProduct>							ProductData
		{
			get
			{
				return(MProductData);
			}
			set
			{
				MProductData=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		protected async override Task OnInitializedAsync()
		{
			await UpdateData();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public async Task UpdateData()
		{
			ProductData=await Repository.Products.ToListAsync();
		}
//----------------------------------------------------------------------------------------------------------------------
		public async Task DeleteProduct(CProduct Product)
		{
			Repository.DeleteProduct(Product);

			await UpdateData();
		}
//----------------------------------------------------------------------------------------------------------------------
		public string GetDetailsUrl(long ID)
		{
			string												URL=$"/Admin/Products/Details/{ID}";

			return(URL);
		}
//----------------------------------------------------------------------------------------------------------------------
		public string GetEditUrl(long ID)
		{
			string												URL=$"/Admin/Products/Edit/{ID}";

			return(URL);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
