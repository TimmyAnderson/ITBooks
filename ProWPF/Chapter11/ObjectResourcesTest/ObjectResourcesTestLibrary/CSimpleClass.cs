using System;
using System.Collections.Generic;
using System.Text;
//------------------------------------------------------------------------
namespace ObjectResourcesTestLibrary
{
//------------------------------------------------------------------------
	public class CSimpleClass
	{
//------------------------------------------------------------------------
		private static int						MIncrement=1;
//------------------------------------------------------------------------
		private string							MSomeValue;
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		// Musi mat empty konstruktor kvoli XAML.
		public CSimpleClass()
		{
			MSomeValue=string.Format("EMPTY {0}",MIncrement);
			MIncrement++;
		}
//------------------------------------------------------------------------
		public CSimpleClass(string SomeValue)
		{
			MSomeValue=SomeValue;
			MSomeValue=string.Format("{0} {1}",SomeValue,MIncrement);
			MIncrement++;
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public string							SomeValue
		{
			get
			{
				return(MSomeValue);
			}
			set
			{
				MSomeValue=value;
			}
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("SV: {0}",MSomeValue));
		}
//------------------------------------------------------------------------
	}
//------------------------------------------------------------------------
}
