#pragma once
//----------------------------------------------------------------------------------------------------------------------
template<typename TLock>
class CLocker final
{
//----------------------------------------------------------------------------------------------------------------------
	private:
		TLock&													MLock;

	public:
		explicit CLocker(TLock& Lock)
			: MLock(Lock)
		{
			MLock.Lock();
		}

		~CLocker(void)
		{
			MLock.Unlock();
		}
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------/----------------------------------------------------------------------------------------------------------------------