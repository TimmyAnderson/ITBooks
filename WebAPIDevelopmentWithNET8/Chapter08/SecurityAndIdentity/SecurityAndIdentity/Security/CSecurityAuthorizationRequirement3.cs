using Microsoft.AspNetCore.Authorization;
//----------------------------------------------------------------------------------------------------------------------
namespace SecurityAndIdentity
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CSecurityAuthorizationRequirement3 : IAuthorizationRequirement
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MClaimsStartsWith;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CSecurityAuthorizationRequirement3(string ClaimsStartsWith)
		{
			MClaimsStartsWith=ClaimsStartsWith;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											ClaimsStartsWith
		{
			get
			{
				return(MClaimsStartsWith);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
