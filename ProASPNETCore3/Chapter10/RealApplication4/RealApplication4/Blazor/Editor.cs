using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Components;
using Microsoft.EntityFrameworkCore;
using RealApplication4.Models;
//----------------------------------------------------------------------------------------------------------------------
// !!! CODE BEHIND CLASS MUSI byt definovana v ROVNAKOM NAMESPACE ako [Editor.razor].
namespace RealApplication4.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
	public partial class Editor : OwningComponentBase<IStoreRepository>
	{
//----------------------------------------------------------------------------------------------------------------------
		private NavigationManager								MNavManager;
		private long											MID=0;
		private CProduct										MProduct=new CProduct();
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IStoreRepository									Repository
		{
			get
			{
				return(Service);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		protected override void OnParametersSet()
		{
			if (MID!=0)
			{
				MProduct=Repository.Products.FirstOrDefault(P => P.ProductID==MID);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Inject]
		public NavigationManager								NavManager
		{
			get
			{
				return(MNavManager);
			}
			set
			{
				MNavManager=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Parameter]
		public long												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CProduct											Product
		{
			get
			{
				return(MProduct);
			}
			set
			{
				MProduct=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ThemeColor
		{
			get
			{
				if (MID==0)
				{
					return("primary");
				}
				else
				{
					return("warning");
				}
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											TitleText
		{
			get
			{
				if (MID==0)
				{
					return("Create");
				}
				else
				{
					return("Edit");
				}
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void SaveProduct()
		{
			if (MID==0)
			{
				Repository.CreateProduct(MProduct);
			}
			else
			{
				Repository.SaveProduct(MProduct);
			}

			MNavManager.NavigateTo("/Admin/Products");
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
