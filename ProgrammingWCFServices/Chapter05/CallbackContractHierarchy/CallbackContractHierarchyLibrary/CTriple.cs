using System;
using System.Collections.Generic;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CallbackContractHierarchyLibrary
{
//-------------------------------------------------------------------------------------------------------
	class CTriple<T1, T2, T3>
	{
//-------------------------------------------------------------------------------------------------------
		private	T1												MFirst;
		private	T2												MSecond;
		private	T3												MThird;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTriple(T1 First, T2 Second, T3 Third)
		{
			MFirst=First;
			MSecond=Second;
			MThird=Third;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public	T1												First
		{
			get
			{
				return(MFirst);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public	T2												Second
		{
			get
			{
				return(MSecond);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public	T3												Third
		{
			get
			{
				return(MThird);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------