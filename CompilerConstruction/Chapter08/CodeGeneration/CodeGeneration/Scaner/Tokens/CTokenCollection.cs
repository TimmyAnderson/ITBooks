using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CodeGeneration
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CTokenCollection
	{
//-------------------------------------------------------------------------------------------------------
		private readonly List<CToken>							MTokens;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTokenCollection()
		{
			MTokens=new List<CToken>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CToken[]											Tokens
		{
			get
			{
				return(MTokens.ToArray());
			}
		}
//-------------------------------------------------------------------------------------------------------
		public CToken[]											TokensForParsing
		{
			get
			{
				CToken[]										Tokens=MTokens.Where(P => (P.TokenID!="WHITE CHARS" && P.TokenID!="COMMENT")).ToArray();

				return(Tokens);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void AddToken(CToken Token)
		{
			MTokens.Add(Token);
		}
//-------------------------------------------------------------------------------------------------------
		public void Clear()
		{
			MTokens.Clear();
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------