using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.IO;
//-------------------------------------------------------------------------------------------------------
namespace IOBoundAsynchronousOperations
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CFileStreamCallbackParams
	{
//-------------------------------------------------------------------------------------------------------
		private readonly FileStream								MFileStream;
		private readonly byte[]									MBuffer;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CFileStreamCallbackParams(FileStream FS)
		{
			MFileStream=FS;
			MBuffer=new byte[FS.Length];
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public FileStream										FileStream
		{
			get
			{
				return(MFileStream);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public byte[]											Buffer
		{
			get
			{
				return(MBuffer);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------