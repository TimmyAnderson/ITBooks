using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UnderstandingCorePlatform.Configurations
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CConfigurationClassWithConfiguration
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MValue1;
		private string											MValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CConfigurationClassWithConfiguration()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CConfigurationClassWithConfiguration(string Value1, string Value2)
		{
			MValue1=Value1;
			MValue2=Value2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------