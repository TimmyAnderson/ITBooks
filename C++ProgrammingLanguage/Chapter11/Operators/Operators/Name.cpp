//-------------------------------------------------------------------------------------------------------
CName::CName(void)
{
	MFirstName=L"";
	MLastName=L"";
	MAge=0;
}
//-------------------------------------------------------------------------------------------------------
CName::CName(wstring FirstName, wstring LastName, int Age)
{
	MFirstName=FirstName;
	MLastName=LastName;
	MAge=Age;
}
//-------------------------------------------------------------------------------------------------------
CName::~CName(void)
{
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
wstring& CName::GetFirstName(void)
{
	return(MFirstName);
}
//-------------------------------------------------------------------------------------------------------
wstring& CName::GetLastName(void)
{
	return(MLastName);
}
//-------------------------------------------------------------------------------------------------------
int& CName::GetAge(void)
{
	return(MAge);
}
//-------------------------------------------------------------------------------------------------------
void CName::PrintFullName(void)
{
	wcout << L"Name: [" << MFirstName << L" " << MLastName << L"], Age: [" << MAge << "] !\n";
}
//-------------------------------------------------------------------------------------------------------
std::wstring CName::GetFullName(void)
{
	wstring														Result=L"";
	wchar_t														Buffer[100];

	swprintf_s(Buffer,L"%d",MAge);

	Result+=L"Name: [";
	Result+=MFirstName;
	Result+=L" ";
	Result+=MLastName;
	Result+=L"], Age: [";
	Result+=Buffer;
	Result+=L"]";

	return(Result);
}
