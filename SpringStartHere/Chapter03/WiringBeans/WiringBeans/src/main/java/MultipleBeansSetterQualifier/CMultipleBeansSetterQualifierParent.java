//----------------------------------------------------------------------------------------------------------------------
package MultipleBeansSetterQualifier;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.stereotype.Component;
//----------------------------------------------------------------------------------------------------------------------
@Component
public class CMultipleBeansSetterQualifierParent
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue;
	private CMultipleBeansSetterQualifierChild					MChild;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CMultipleBeansSetterQualifierParent()
	{
		MValue="PARENT BEAN !!!";
	} 	
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue()
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue(String Value)
	{
		MValue=Value;
	}
//----------------------------------------------------------------------------------------------------------------------
	public CMultipleBeansSetterQualifierChild getChild()
	{
		return(MChild);
	}
//----------------------------------------------------------------------------------------------------------------------
	// !!! ANNOTATION [@Autowired] sa aplikuje na SETTER, aby SETTER bol automaticky zavolany SPRING FRAMEWORK.
	// !!!!! Nazov v ANNOTATION [@Qualifier] sa MUSI zhodovat s nazvom BEAN.
	@Autowired
	@Qualifier("MyChild")
	public void setChild(CMultipleBeansSetterQualifierChild ChildParameter)
	{
		MChild=ChildParameter;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------