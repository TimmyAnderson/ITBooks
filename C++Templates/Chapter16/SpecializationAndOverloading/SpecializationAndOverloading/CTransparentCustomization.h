#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <iostream>
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
class CTransparentCustomization final
{
//----------------------------------------------------------------------------------------------------------------------
	template<typename TLocalType>
	friend std::wostream& operator<<(std::wostream& Stream, const CTransparentCustomization<TLocalType>& Value);

	private:
		TType													MValue;

	public:
		const TType& GetValue(void) const noexcept;

	public:
		CTransparentCustomization(const TType& Value);
		virtual ~CTransparentCustomization(void) noexcept;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
CTransparentCustomization<TType>::CTransparentCustomization(const TType& Value)
	: MValue(Value)
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
CTransparentCustomization<TType>::~CTransparentCustomization(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
const TType& CTransparentCustomization<TType>::GetValue(void) const noexcept
{
	return(MValue);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType>
std::wostream& operator<<(std::wostream& Stream, const CTransparentCustomization<TType>& Value)
{
	std::wcout << L"OPERATOR << CALLED !" << std::endl;

	Stream << Value.MField;

	return(Stream);
}
