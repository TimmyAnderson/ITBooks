using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Identity;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication5.Pages.Admin
{
//----------------------------------------------------------------------------------------------------------------------
	[Authorize]
	public class IdentityUsersModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly UserManager<IdentityUser>				MUserManager;
//----------------------------------------------------------------------------------------------------------------------
        private IdentityUser									MAdminUser;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IdentityUsersModel(UserManager<IdentityUser> UserManager)
		{
			MUserManager=UserManager;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IdentityUser										AdminUser
		{
			get
			{
				return(MAdminUser);
			}
			set
			{
				MAdminUser=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public async Task OnGetAsync()
		{
			MAdminUser=await MUserManager.FindByNameAsync("Admin");
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------