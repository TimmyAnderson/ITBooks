using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CEntitySortingManually
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private string											MValueString;
		private int												MValueInt;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntitySortingManually()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntitySortingManually(int ID, string ValueString, int ValueInt)
		{
			MID=ID;
			MValueString=ValueString;
			MValueInt=ValueInt;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ValueString
		{
			get
			{
				return(MValueString);
			}
			set
			{
				MValueString=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												ValueInt
		{
			get
			{
				return(MValueInt);
			}
			set
			{
				MValueInt=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------