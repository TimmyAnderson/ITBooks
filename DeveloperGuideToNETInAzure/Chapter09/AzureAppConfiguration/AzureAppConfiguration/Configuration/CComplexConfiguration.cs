using Newtonsoft.Json;
//----------------------------------------------------------------------------------------------------------------------
namespace AzureAppConfiguration
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CComplexConfiguration
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MValue1;
		private int												MValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CComplexConfiguration()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CComplexConfiguration(string Value1, int Value2)
		{
			MValue1=Value1;
			MValue2=Value2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("value1")]
		public string											Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("value2")]
		public int												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------