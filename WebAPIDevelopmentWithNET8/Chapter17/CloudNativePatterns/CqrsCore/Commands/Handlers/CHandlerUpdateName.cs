using System.Threading;
using System.Threading.Tasks;
using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CHandlerUpdateName : IRequestHandler<CRequestUpdateName,CDtoFullName>
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly INameRepository						MNameRepository;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CHandlerUpdateName(INameRepository NameRepository)
		{
			MNameRepository=NameRepository;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public async Task<CDtoFullName> Handle(CRequestUpdateName Request, CancellationToken CancellationToken)
		{
			CDtoFullName										FullName=await MNameRepository.UpdateName(Request.Name);

			return(FullName);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------