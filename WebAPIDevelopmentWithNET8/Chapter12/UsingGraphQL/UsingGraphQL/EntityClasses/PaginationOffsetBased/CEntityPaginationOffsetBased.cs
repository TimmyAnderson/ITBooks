using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CEntityPaginationOffsetBased
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private int												MValue1;
		private int												MValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityPaginationOffsetBased()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityPaginationOffsetBased(int ID, int Value1, int Value2)
		{
			MID=ID;
			MValue1=Value1;
			MValue2=Value2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
