namespace EntraExternalIDWebPages
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CErrorViewModel
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly string									MRequestID;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CErrorViewModel(string RequestID)
		{
			MRequestID=RequestID;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public string											RequestID
		{
			get
			{
				return(MRequestID);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public bool												ShowRequestID
		{
			get
			{
				if (string.IsNullOrEmpty(MRequestID)==false)
				{
					return(false);
				}
				else
				{
					return(true);
				}
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
