package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CClassDerivedMyInterface1MyInterface2 extends CClassBaseMyInterface1MyInterface2 implements IMyInterface1, IMyInterface2
{
//----------------------------------------------------------------------------------------------------------------------
	private String												interfaceValue1;
	private String												interfaceValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CClassDerivedMyInterface1MyInterface2(String baseClassValue, String interfaceValue1, String interfaceValue2)
	{
		super(baseClassValue);
		
		this.interfaceValue1=interfaceValue1;
		this.interfaceValue2=interfaceValue2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public String getInterfaceValue1()
	{
		return(interfaceValue1);
	}
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public void setInterfaceValue1(String value)
	{
		interfaceValue1=value;
	}
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public String getInterfaceValue2()
	{
		return(interfaceValue2);
	}
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public void setInterfaceValue2(String value)
	{
		interfaceValue2=value;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public String toString()
	{
		return(String.format("DERIVED TYPE TYPE - BASE CLASS VALUE [%s] INTERFACE 1 VALUE [%s] INTERFACE 2 VALUE [%s]",getBaseClassValue(),interfaceValue1,interfaceValue2));
	}
//----------------------------------------------------------------------------------------------------------------------
}
