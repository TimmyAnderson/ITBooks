using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Optimizations
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CSemanticError : CCompilerError
	{
//-------------------------------------------------------------------------------------------------------
		private readonly int?									MSourceCodeIndex;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSemanticError(string ErrorDescription, CToken Token)
			: base(ErrorDescription)
		{
			if (Token!=null)
			{
				MSourceCodeIndex=Token.SourceCodeIndex;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int?												SourceCodeIndex
		{
			get
			{
				return(MSourceCodeIndex);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string GetError(CSourceCode SourceCode)
		{
			StringBuilder										SB=new StringBuilder();

			SB.AppendFormat(base.GetError(SourceCode));

			if (MSourceCodeIndex!=null)
			{
				SB.AppendFormat(", SourceCodeLine: [{0}]",SourceCode.GetSourceCodeLineAtIndex(MSourceCodeIndex.Value).GetSouceCodeLineWithoutNewLineCharacter());
			}

			return(SB.ToString());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
