using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Constructors
{
//-------------------------------------------------------------------------------------------------------
	public class CClassWithValueType
	{
//-------------------------------------------------------------------------------------------------------
		private SStruct											MStruct1;
		private SStruct											MStruct2;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CClassWithValueType()
		{
			// !!! VALUE TYPE je mozne inicializovat pomocou EXPLICITNEHO VOLANIA CONSTRUCTOR.
			MStruct1=new SStruct(111,222);

			// !!! Pre MStruct2 sa CONSTRUCTOR EXPLICITNE NEVOLA a kedze PARAMETER-LESS CONSTRUCTOR VALUE TYPES NEMOZU MAT v C#, tak sa nezavola ZIADEN CONSTRUCTOR. Je to OPTIMISATION FEATURE.
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void SetStruct1(SStruct Struct1)
		{
			MStruct1=Struct1;
		}
//-------------------------------------------------------------------------------------------------------
		public void SetStruct2(SStruct Struct2)
		{
			MStruct2=Struct2;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void ShowValues()
		{
			Console.WriteLine(string.Format("MStruct1: [{0}], MStruct2: [{1}] !",MStruct1,MStruct2));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------