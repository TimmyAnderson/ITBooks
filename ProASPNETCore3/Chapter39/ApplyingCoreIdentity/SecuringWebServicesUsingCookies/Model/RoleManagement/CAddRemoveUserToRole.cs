	// !!! CLASS reprezentuje DATA pre pridavanie a odoberanie USERS z ROLES v RAZOR PAGES.
	public sealed class CAddRemoveUserToRole
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MRoleID;
		private string											MUserID;
		private string											MUserName;
