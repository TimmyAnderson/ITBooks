using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CMultiImplementationServiceModel
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly CMultiImplementationServiceData		MDefaultService;
		private readonly CMultiImplementationServiceData[]		MServices;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CMultiImplementationServiceModel(CMultiImplementationServiceData DefaultService, CMultiImplementationServiceData[] Services)
		{
			MDefaultService=DefaultService;
			MServices=Services;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CMultiImplementationServiceData					DefaultService
		{
			get
			{
				return(MDefaultService);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CMultiImplementationServiceData[]				Services
		{
			get
			{
				return(MServices);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------