using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//-------------------------------------------------------------------------------------------------------
namespace Interfaces
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CComparableEquatableClass : IComparable<CComparableEquatableClass>, IEquatable<CComparableEquatableClass>
	{
//-------------------------------------------------------------------------------------------------------
		private int												MValue;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CComparableEquatableClass(int Value)
		{
			MValue=Value;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int CompareTo(CComparableEquatableClass Other)
		{
			return(MValue.CompareTo(Other.MValue));
		}
//-------------------------------------------------------------------------------------------------------
		public bool Equals(CComparableEquatableClass Other)
		{
			return(MValue.Equals(Other.MValue));
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("CComparableEquatableClass [{0}] !",MValue));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------