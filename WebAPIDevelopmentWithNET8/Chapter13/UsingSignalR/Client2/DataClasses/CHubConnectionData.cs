using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Microsoft.AspNetCore.SignalR.Client;
//--------------------------------------------------------------------------------------------------------------------------------
namespace Client2
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CHubConnectionData
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly string									MUserName;
		private readonly HubConnection							MHubConnection;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CHubConnectionData(string UserName, HubConnection HubConnection)
		{
			MUserName=UserName;
			MHubConnection=HubConnection;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public HubConnection									HubConnection
		{
			get
			{
				return(MHubConnection);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
