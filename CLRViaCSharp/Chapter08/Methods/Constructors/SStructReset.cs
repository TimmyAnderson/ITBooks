using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Constructors
{
//-------------------------------------------------------------------------------------------------------
	public struct SStructReset
	{
//-------------------------------------------------------------------------------------------------------
		private int												MValue1;
		private int												MValue2;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public SStructReset(int Value1, int Value2)
		{
			MValue1=Value1;
			MValue2=Value2;

			ShowValues();

			// !!!!! Tato SPECIALNA SYNTAX umoznuje vynulovat vsetky FIELDS.
			this=new SStructReset();

			ShowValues();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void ShowValues()
		{
			Console.WriteLine(string.Format("VALUES - MValue1: [{0}], MValue2: [{1}] !",MValue1,MValue2));
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("MValue1: [{0}], MValue2: [{1}] !",MValue1,MValue2));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
