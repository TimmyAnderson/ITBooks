using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Windows;
using System.Windows.Controls;
//-----------------------------------------------------------------------------------------------------------------
namespace WindowsApplication
{
//-----------------------------------------------------------------------------------------------------------------
	public partial class YellowPadHelp
	{
//-----------------------------------------------------------------------------------------------------------------
        public YellowPadHelp()
        {
            InitializeComponent();

            (MTree.Items[0] as TreeViewItem).IsSelected=true;
            MTree.Focus();
        }
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
        void HelpOnSelectedItemChanged(object Sender, RoutedPropertyChangedEventArgs<object> Args)
        {
            TreeViewItem		Item=Args.NewValue as TreeViewItem;

            if (Item.Tag==null)
                return;

            MFrame.Navigate(new Uri(Item.Tag as string, UriKind.Relative));
        }
//-----------------------------------------------------------------------------------------------------------------
	}
//-----------------------------------------------------------------------------------------------------------------
}
