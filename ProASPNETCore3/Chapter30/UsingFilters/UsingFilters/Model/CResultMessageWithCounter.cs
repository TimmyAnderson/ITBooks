using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFilters.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CResultMessageWithCounter
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MMessage;
		private readonly int									MCounter;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CResultMessageWithCounter(string Message, int Counter)
		{
			MMessage=Message;
			MCounter=Counter;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Message
		{
			get
			{
				return(MMessage);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Counter
		{
			get
			{
				return(MCounter);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------