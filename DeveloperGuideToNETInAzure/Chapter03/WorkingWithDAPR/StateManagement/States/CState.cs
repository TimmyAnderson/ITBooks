using System;
//----------------------------------------------------------------------------------------------------------------------
namespace StateManagement
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CState
	{
//----------------------------------------------------------------------------------------------------------------------
		private DateTime										MFirstRequestTime;
		private DateTime										MLastRequestTime;
		private int												MNumberOfRequests;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CState()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CState(DateTime FirstRequestTime, DateTime LastRequestTime, int NumberOfRequests)
		{
			MFirstRequestTime=FirstRequestTime;
			MLastRequestTime=LastRequestTime;
			MNumberOfRequests=NumberOfRequests;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public DateTime											FirstRequestTime
		{
			get
			{
				return(MFirstRequestTime);
			}
			set
			{
				MFirstRequestTime=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public DateTime											LastRequestTime
		{
			get
			{
				return(MLastRequestTime);
			}
			set
			{
				MLastRequestTime=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												NumberOfRequests
		{
			get
			{
				return(MNumberOfRequests);
			}
			set
			{
				MNumberOfRequests=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------