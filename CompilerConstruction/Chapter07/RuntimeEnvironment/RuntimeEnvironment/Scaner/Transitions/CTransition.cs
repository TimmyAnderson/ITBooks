using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace RuntimeEnvironment
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CTransition
	{
//-------------------------------------------------------------------------------------------------------
		private readonly CInputChar								MInputChar;
		private readonly CState									MNewState;
		private readonly bool									MIsConsumingCharTransition;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTransition(CInputChar InputChar, CState NewState, bool IsConsumingCharTransition)
		{
			MInputChar=InputChar;
			MNewState=NewState;
			MIsConsumingCharTransition=IsConsumingCharTransition;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CInputChar										InputChar
		{
			get
			{
				return(MInputChar);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public CState											NewState
		{
			get
			{
				return(MNewState);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public bool												IsConsumingCharTransition
		{
			get
			{
				return(MIsConsumingCharTransition);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("[{0}] -> [{1}], IC: [{2}]",MInputChar,MNewState,MIsConsumingCharTransition));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------