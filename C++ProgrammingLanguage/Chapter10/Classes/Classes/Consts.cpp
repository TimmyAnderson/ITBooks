#include "Consts.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------------------------------
const std::wstring												CConsts::STATIC_TEXT_CONSTANT=L"Timmy Anderson";
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
CConsts::CConsts(void)
	: INT_CONSTANT(10)
{
	wprintf_s(L"CONSTRUCTOR [%d] !\n",INT_CONSTANT);
}
//-------------------------------------------------------------------------------------------------------
CConsts::CConsts(int Value)
	: INT_CONSTANT(Value)
{
	wprintf_s(L"NON-DEFAULT CONSTRUCTOR [%d] !\n",INT_CONSTANT);
}
//-------------------------------------------------------------------------------------------------------
CConsts::~CConsts(void)
{
	wprintf_s(L"DESTRUCTOR [%d] !\n",INT_CONSTANT);
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
void CConsts::Print(void)
{
	wprintf_s(L"INT_CONSTANT [%d], STATIC_TEXT_CONSTANT [%s] !\n",INT_CONSTANT,STATIC_TEXT_CONSTANT.c_str());
}
