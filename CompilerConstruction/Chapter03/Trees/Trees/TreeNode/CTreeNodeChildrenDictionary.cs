using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Trees
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CTreeNodeChildrenDictionary
	{
//-------------------------------------------------------------------------------------------------------
		private Dictionary<CTreeNodeID,List<CTreeNodeID>>	MLinks=new Dictionary<CTreeNodeID,List<CTreeNodeID>>();
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeChildrenDictionary()
		{
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public List<CTreeNodeID>								this[CTreeNodeID NodeID]
		{
			get
			{
				if (MLinks.ContainsKey(NodeID)==true)
					return(MLinks[NodeID]);
				else
					return(null);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void Add(CTreeNodeID ParentNodeID, CTreeNodeID ChildNodeID)
		{
			if (MLinks.ContainsKey(ParentNodeID)==true)
			{
				MLinks[ParentNodeID].Add(ChildNodeID);
			}
			else
			{
				List<CTreeNodeID>		Children=new List<CTreeNodeID>();

				Children.Add(ChildNodeID);

				MLinks.Add(ParentNodeID,Children);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------