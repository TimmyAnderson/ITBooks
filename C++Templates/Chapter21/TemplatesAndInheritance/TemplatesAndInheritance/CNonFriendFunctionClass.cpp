//----------------------------------------------------------------------------------------------------------------------
#include "CNonFriendFunctionClass.h"
//----------------------------------------------------------------------------------------------------------------------
using namespace std;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CNonFriendFunctionClass::CNonFriendFunctionClass(void)
	: MField()
{
}
//----------------------------------------------------------------------------------------------------------------------
CNonFriendFunctionClass::CNonFriendFunctionClass(const wchar_t* Field)
	: MField(Field)
{
}
//----------------------------------------------------------------------------------------------------------------------
CNonFriendFunctionClass::CNonFriendFunctionClass(const wstring& Field)
	: MField(Field)
{
}
//----------------------------------------------------------------------------------------------------------------------
CNonFriendFunctionClass::~CNonFriendFunctionClass(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
const wstring& CNonFriendFunctionClass::GetField(void) const noexcept
{
	return(MField);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
wostream& operator<<(wostream& Stream, const CNonFriendFunctionClass& Value)
{
	Stream << Value.MField;

	return(Stream);
}
