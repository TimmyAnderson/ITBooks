using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//-------------------------------------------------------------------------------------------------------
namespace TypeFundamentals
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CClassUsingMutableClass
	{
//-------------------------------------------------------------------------------------------------------
		private CMutableClass									MMutableClass;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CClassUsingMutableClass(CMutableClass Text)
		{
			MMutableClass=Text;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CMutableClass									MutableClass
		{
			get
			{
				return(MMutableClass);
			}
			set
			{
				MMutableClass=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CClassUsingMutableClass CreateClone()
		{
			CClassUsingMutableClass								Clone=(CClassUsingMutableClass) MemberwiseClone();

			return(Clone);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("MutableClass: [{0}]",MMutableClass));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------