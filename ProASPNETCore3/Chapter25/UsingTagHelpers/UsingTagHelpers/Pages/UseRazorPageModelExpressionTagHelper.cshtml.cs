using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingTagHelpers.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingTagHelpers.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class UseRazorPageModelExpressionTagHelperModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly CName									MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public UseRazorPageModelExpressionTagHelperModel()
		{
			MName=new CName(0,"Timmy","Anderson",12);
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName											Name
		{
			get
			{
				return(MName);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnGet()
        {
			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
