#pragma once
//-----------------------------------------------------------------------------
#include <chrono>
#include <thread>
#include "MyDebug.h"
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
class CTaskFunctor
{
//-----------------------------------------------------------------------------
	private:
		int														MIndex;

	public:
		void operator()(int Index)
		{
			std::wcout << L"TASK OPERATOR() [" << MIndex << L"," << Index << L"] STARTED !" << std::endl;

			// Caka sa 5 sekund.
			std::this_thread::sleep_for(std::chrono::duration<int,std::ratio<1,1>>(5));

			std::wcout << L"TASK OPERATOR() [" << MIndex << L"," << Index << L"] FINISHED !" << std::endl;
		}

	public:
		CTaskFunctor(int Index)
			: MIndex(Index)
		{
		}
//-----------------------------------------------------------------------------
};
//-----------------------------------------------------------------------------