package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CGenericsMethodTypeInference
{
//----------------------------------------------------------------------------------------------------------------------
	public static <TType> TType GenericsMethod(TType value1, TType value2, boolean First)
	{
		if (First==true)
		{
			return(value1);
		}
		else
		{
			return(value2);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
