using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//------------------------------------------------------------------------------
namespace TreeViewsAndListViews
{
//------------------------------------------------------------------------------
    public class CSystemParam
    {
//------------------------------------------------------------------------------
        private string											MStrName;
        private object											MObjValue;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public string											Name
        {
            get
			{
				return(MStrName);
			}
            set
			{
				MStrName=value;
			}
        }
//------------------------------------------------------------------------------
        public object											Value
        {
            get
			{
				return(MObjValue);
			}
            set
			{
				MObjValue=value;
			}
        }
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public override string ToString()
        {
            return(string.Format("[{0}] = [{1}] !",Name,Value));
        }
//------------------------------------------------------------------------------
    }
//------------------------------------------------------------------------------
}
