using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingFilters.CustomFilters;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingFilters.Pages
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! Vsetky RAZOR PAGE HANDLERS POMOCOU CUSTOM FILTER kontroluju ci je vyvolana cez HTTPS PROTOCOL a ak NIE, tak hodia ERROR.
	[CRequireHttps]
    public class PageFilterOnRazorPageModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private string											MMessage;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Message
		{
			get
			{
				return(MMessage);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnGet()
        {
			MMessage="RAZOR PAGE HANDLER is EXECUTED via HTTPS !";

			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
