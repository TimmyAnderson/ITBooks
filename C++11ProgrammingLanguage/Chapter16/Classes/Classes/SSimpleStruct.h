struct SSimpleStruct
{
//-----------------------------------------------------------------------------
	// BY DEFAULT ACCESS CONTROL je pre STRUCTURES nastaveny na PUBLIC.
		std::wstring											MField;

	public:
		const std::wstring& GetField(void) const noexcept
		{
			return(MField);
		}

	public:
		SSimpleStruct(const std::wstring& Field)
			: MField(Field)
		{
		}

		~SSimpleStruct(void)
		{
		}
//-----------------------------------------------------------------------------
};
