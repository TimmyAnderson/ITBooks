using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CInterfaceService : IInterfaceService
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly Dictionary<int,CName>					MNames;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CInterfaceService()
		{
			MNames=new Dictionary<int,CName>();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void AddName(CName Name)
		{
			MNames.Add(Name.ID,Name);
		}
//----------------------------------------------------------------------------------------------------------------------
		public void RemoveName(int ID)
		{
			MNames.Remove(ID);
		}
//----------------------------------------------------------------------------------------------------------------------
		public CName[] GetNames()
		{
			return(MNames.Values.ToArray());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
