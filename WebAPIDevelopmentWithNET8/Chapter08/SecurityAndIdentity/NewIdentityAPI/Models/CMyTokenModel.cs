//----------------------------------------------------------------------------------------------------------------------
using Newtonsoft.Json;

namespace NewIdentityAPI
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS sa MUSI zhodovat s NEW IDENTITY API, ktory sa da zistit pomocou SWAGGER.
	public sealed class CMyTokenModel
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MTokenType;
		private string											MAccessToken;
		private long											MExpiresIn;
		private string											MRefreshToken;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CMyTokenModel()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CMyTokenModel(string TokenType, string AccessToken, long ExpiresIn, string RefreshToken)
		{
			MTokenType=TokenType;
			MAccessToken=AccessToken;
			MExpiresIn=ExpiresIn;
			MRefreshToken=RefreshToken;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("tokenType")]
		public string											TokenType
		{
			get
			{
				return(MTokenType);
			}
			set
			{
				MTokenType=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("accessToken")]
		public string											AccessToken
		{
			get
			{
				return(MAccessToken);
			}
			set
			{
				MAccessToken=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("expiresIn")]
		public long												ExpiresIn
		{
			get
			{
				return(MExpiresIn);
			}
			set
			{
				MExpiresIn=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("refreshToken")]
		public string											RefreshToken
		{
			get
			{
				return(MRefreshToken);
			}
			set
			{
				MRefreshToken=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------