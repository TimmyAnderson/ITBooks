CExceptionDerived2::CExceptionDerived2(const wstring& Value)
	: CExceptionBase(Value)
{
	wcout << L"CONSTRUCTOR [" << __func__ << L"()] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
CExceptionDerived2::~CExceptionDerived2(void)
{
	wcout << L"DESTRUCTOR [" << __func__ << L"()] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
wstring CExceptionDerived2::GetTextMessage(void) const noexcept
{
	wstring														Message;

	Message.append(L"EXCEPTION [CExceptionDerived2] - VALUE [");
	Message.append(GetValue());
	Message.append(L"] !");

	return(Message);
}
