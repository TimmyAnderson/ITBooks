using System;
using System.Collections.Generic;
using System.Text;
using Helpers;
//----------------------------------------------------------------------------------------------------------------------
namespace PerformanceTuning2
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CDataDependent
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MDependentDataID;
		private int												MDependentDataIntValue;
		private string											MDependentDataStringValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CDataDependent()
		{
			MDependentDataID=default;
			MDependentDataIntValue=default;
			MDependentDataStringValue=default;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CDataDependent(int DependentDataID, int DependentDataIntValue, string DependentDataStringValue)
		{
			MDependentDataID=DependentDataID;
			MDependentDataIntValue=DependentDataIntValue;
			MDependentDataStringValue=DependentDataStringValue;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												DependentDataID
		{
			get
			{
				return(MDependentDataID);
			}
			set
			{
				MDependentDataID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												DependentDataIntValue
		{
			get
			{
				return(MDependentDataIntValue);
			}
			set
			{
				MDependentDataIntValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											DependentDataStringValue
		{
			get
			{
				return(MDependentDataStringValue);
			}
			set
			{
				MDependentDataStringValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------