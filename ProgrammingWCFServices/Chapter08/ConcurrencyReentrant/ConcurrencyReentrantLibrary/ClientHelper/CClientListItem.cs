using System;
using System.Collections.Generic;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ClientHelper
{
//-------------------------------------------------------------------------------------------------------
	internal class CClientListItem<T>
	{
//-------------------------------------------------------------------------------------------------------
		private	T												MClient;
		private string											MName;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		internal CClientListItem(T Client, string Name)
		{
			MClient=Client;
			MName=Name;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		internal T												Client
		{
			get
			{
				return(MClient);
			}
		}
//-------------------------------------------------------------------------------------------------------
		internal string											Name
		{
			get
			{
				return(MName);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("Client: {0}",MName));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------