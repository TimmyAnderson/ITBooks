CTuple<> TupleReverse(const CTuple<>& Tuple);
//----------------------------------------------------------------------------------------------------------------------
template<typename THead, typename... TTail>
typename CTupleReverseType<CTuple<THead,TTail...>>::TYPE TupleReverse(const CTuple<THead,TTail...>& Tuple)
{
	typename CTupleReverseType<CTuple<THead,TTail...>>::TYPE	Result=TuplePushBack(TupleReverse(Tuple.GetTail()),Tuple.GetHead());

	return(Result);
}
