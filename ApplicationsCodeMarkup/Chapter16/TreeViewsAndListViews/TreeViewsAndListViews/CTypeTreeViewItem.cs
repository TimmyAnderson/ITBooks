using System;
using System.Windows;
using System.Windows.Controls;
//------------------------------------------------------------------------------
namespace TreeViewsAndListViews
{
//------------------------------------------------------------------------------
    public class CTypeTreeViewItem : TreeViewItem
    {
//------------------------------------------------------------------------------
        private Type											MType;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public CTypeTreeViewItem()
        {
        }
//------------------------------------------------------------------------------
        public CTypeTreeViewItem(Type Type)
        {
            this.Type=Type;
        }
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public Type												Type
        {
            get
            {
                return MType;
            }
            set
            {
                MType=value;

                if (MType.IsAbstract==true)
                    Header=string.Format("{0} (abstract)",MType.Name);
                else
                    Header=MType.Name;
            }
        }
//------------------------------------------------------------------------------
    }
//------------------------------------------------------------------------------
}
