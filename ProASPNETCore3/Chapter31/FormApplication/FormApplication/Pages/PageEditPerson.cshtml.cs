using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using FormApplication.Model;
using FormApplication.Services;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
//----------------------------------------------------------------------------------------------------------------------
namespace FormApplication.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class PageEditPersonModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly IDBOperations							MDBOperations;
//----------------------------------------------------------------------------------------------------------------------
		private CPerson											MPerson;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public PageEditPersonModel(IDBOperations DBOperations)
		{
			MDBOperations=DBOperations;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[BindProperty]
		public CPerson											Person
		{
			get
			{
				return(MPerson);
			}
			set
			{
				MPerson=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnGet(int ID)
        {
			MPerson=MDBOperations.GetPerson(ID);

			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult OnPost()
		{
			if (ModelState.IsValid==true)
			{
				MDBOperations.UpdatePerson(MPerson);

				IActionResult									Result=RedirectToPage("PageViewPersons");

				return(Result);
			}
			else
			{
				PageResult										Result=Page();

				return(Result);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------