using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace StartingWithNetCore.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CTime
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MTime;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CTime()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CTime(string Time)
		{
			MTime=Time;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Time
		{
			get
			{
				return(MTime);
			}
			set
			{
				MTime=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
