//----------------------------------------------------------------------------------------------------------------------
package MultipleBeansPrimary;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;
//----------------------------------------------------------------------------------------------------------------------
// !!! Pouzije sa ANNOTATION [@Component], no mohol by sa pouzit aj ANNOTATION [@Bean] aplikovany na FACTORY METHOD v CONFIGURATION CLASS.
@Component
public class CMultipleBeansPrimaryParent
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue;
	private CMultipleBeansPrimaryChild							MChild;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	// !!! ANNOTATION [@Autowired] sa aplikuje na CONSTRUCTOR, aby CONSTRUCTOR PARAMETER bol automaticky nastaveny SPRING FRAMEWORK pomocou DEPENDENCY INJECTION. 
	@Autowired
	public CMultipleBeansPrimaryParent(CMultipleBeansPrimaryChild Child)
	{
		MValue="PARENT BEAN !!!";
	
		MChild=Child;
	} 	
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue()
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue(String Value)
	{
		MValue=Value;
	}
//----------------------------------------------------------------------------------------------------------------------
	public CMultipleBeansPrimaryChild getChild()
	{
		return(MChild);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setChild(CMultipleBeansPrimaryChild Child)
	{
		MChild=Child;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------