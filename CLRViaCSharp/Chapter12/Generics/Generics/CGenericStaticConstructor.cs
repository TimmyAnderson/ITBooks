using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Generics
{
//-------------------------------------------------------------------------------------------------------
	public class CGenericStaticConstructor<TType>
	{
//-------------------------------------------------------------------------------------------------------
		static CGenericStaticConstructor()
		{
			if (typeof(TType)==typeof(string))
			{
				throw(new Exception("String is NOT PERMITTED to be TYPE PARAMETER fo this type !"));
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void ClassMethod(TType Parameter)
		{
			Console.WriteLine(string.Format("Generics CLASS METHOD CALLED with GENERICS parameter: [{0}], Type: [{1}] !",Parameter,typeof(TType).Name));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------