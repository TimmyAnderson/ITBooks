using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CFactoryFunctionService : IFactoryFunctionService
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly CName[]								MNames;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! Kedze CLASS NEMA DEFAULT CONSTRUCTOR, je nutne definovat CUSTOM DELEGATE, ktory pre DEPENDENCY INJECTION FRAMEWORK vytvori instanciu tejto CLASS.
		public CFactoryFunctionService(CName[] Names)
		{
			MNames=Names;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName[] GetNames()
		{
			return(MNames);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
