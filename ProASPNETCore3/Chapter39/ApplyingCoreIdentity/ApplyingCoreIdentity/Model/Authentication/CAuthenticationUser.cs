using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Claims;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Identity;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CAuthenticationUser
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MUserID;
		private string											MUserName;
		private string											MUserDescription;
		private CAuthenticationRole[]							MRoles;
		private Claim[]											MClaims;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CAuthenticationUser()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CAuthenticationUser(string UserID, string UserName, string UserDescription, CAuthenticationRole[] Roles, Claim[] Claims)
		{
			MUserID=UserID;
			MUserName=UserName;
			MUserDescription=UserDescription;
			MRoles=Roles;
			MClaims=Claims;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											UserID
		{
			get
			{
				return(MUserID);
			}
			set
			{
				MUserID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
			set
			{
				MUserName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											UserDescription
		{
			get
			{
				return(MUserDescription);
			}
			set
			{
				MUserDescription=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CAuthenticationRole[]							Roles
		{
			get
			{
				return(MRoles);
			}
			set
			{
				MRoles=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public Claim[]											Claims
		{
			get
			{
				return(MClaims);
			}
			set
			{
				MClaims=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------