using Grpc.Core;
using UsingGRPC;
//--------------------------------------------------------------------------------------------------------------------------------
namespace UsingGRPCNames.Services
{
//--------------------------------------------------------------------------------------------------------------------------------
	public class NamesService : Names.NamesBase
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<NamesService>					MLogger;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public NamesService(ILogger<NamesService> Logger)
		{
			MLogger=Logger;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public override Task<NamesResponse> ProcessNames(NamesRequest Request, ServerCallContext Context)
		{
			NamesResponse										ResponseContent=new NamesResponse();

			ResponseContent.ResponseMessage=$"HELLO - FIRST NAME [{Request.FirstName}] LAST NAME [{Request.LastName}] AGE [{Request.Age}].";

			Task<NamesResponse>									ResponseTask=Task.FromResult(ResponseContent);

			return(ResponseTask);
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------------------------------------------------------