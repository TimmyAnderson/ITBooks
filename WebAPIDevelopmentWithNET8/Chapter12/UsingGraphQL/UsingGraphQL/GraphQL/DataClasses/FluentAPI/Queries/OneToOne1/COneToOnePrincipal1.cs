using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class COneToOnePrincipal1
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly int									MPrincipalID;
		private readonly string									MPrincipalValue;
		private readonly COneToOneDependent1					MDependent;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public COneToOnePrincipal1(int PrincipalID, string PrincipalValue)
		{
			MPrincipalID=PrincipalID;
			MPrincipalValue=PrincipalValue;
		}
//----------------------------------------------------------------------------------------------------------------------
		public COneToOnePrincipal1(int PrincipalID, string PrincipalValue, COneToOneDependent1 Dependent)
		{
			MPrincipalID=PrincipalID;
			MPrincipalValue=PrincipalValue;
			MDependent=Dependent;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												PrincipalID
		{
			get
			{
				return(MPrincipalID);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											PrincipalValue
		{
			get
			{
				return(MPrincipalValue);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public COneToOneDependent1								Dependent
		{
			get
			{
				return(MDependent);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
