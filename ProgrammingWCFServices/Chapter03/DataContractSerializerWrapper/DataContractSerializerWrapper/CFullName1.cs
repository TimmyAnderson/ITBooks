using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace DataContractSerializerWrapper
{
//-------------------------------------------------------------------------------------------------------
	//[DataContract(Namespace="MyNamespace")]
	[DataContract]
	public class CFullName1
	{
//-------------------------------------------------------------------------------------------------------
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CFullName1(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											FirstName
		{
			get
			{
				Console.WriteLine("FirstName - get !");

				return(MFirstName);
			}
			private set
			{
				Console.WriteLine("FirstName - set !");

				MFirstName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											LastName
		{
			get
			{
				Console.WriteLine("LastName - get !");

				return(MLastName);
			}
			private set
			{
				Console.WriteLine("LastName - set !");

				MLastName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public int												Age
		{
			get
			{
				Console.WriteLine("Age - get !");

				return(MAge);
			}
			private set
			{
				Console.WriteLine("Age - set !");

				MAge=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("FN: {0}, LN: {1}, Age: {2}",MFirstName,MLastName,MAge));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
