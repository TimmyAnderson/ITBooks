using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace DCGenericsLibrary
{
//-------------------------------------------------------------------------------------------------------
	// Nastavim Name v [DataContract] aby sa nepouzilo to nusne hash meno.
	[DataContract(Name="CGenericClass2")]
	public class CGenericClass2<T>
	{
//-------------------------------------------------------------------------------------------------------
		private	T												MData;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CGenericClass2(T Data)
		{
			MData=Data;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public	T												Data
		{
			get
			{
				return(MData);
			}
			set
			{
				MData=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("{0}: {1}","CGenericClass2",MData.ToString()));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------