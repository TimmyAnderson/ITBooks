using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Optimizations
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CSourceTerminalsQueueItem
	{
//-------------------------------------------------------------------------------------------------------
		private readonly CTerminal								MTerminal;
		private readonly CToken									MToken;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSourceTerminalsQueueItem(CTerminal Terminal, CToken Token)
		{
			MTerminal=Terminal;
			MToken=Token;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTerminal										Terminal
		{
			get
			{
				return(MTerminal);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public CToken											Token
		{
			get
			{
				return(MToken);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			StringBuilder										SB=new StringBuilder();

			SB.AppendFormat("Terminal: [{0}]",MTerminal);
			SB.AppendFormat(", Token: [{0}]",MToken);

			return(SB.ToString());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
