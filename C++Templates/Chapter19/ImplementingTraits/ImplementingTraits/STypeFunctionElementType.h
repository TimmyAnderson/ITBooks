//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <vector>
#include <list>
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! PRIMARY TEMPLATE CLASS.
template<typename TType>
struct STypeFunctionElementType;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE CLASS SPECIALIZATION pre TYPE [vector<TType>].
template<typename TType>
struct STypeFunctionElementType<std::vector<TType>> final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													ElementType=TType;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE CLASS SPECIALIZATION pre TYPE [list<TType>].
template<typename TType>
struct STypeFunctionElementType<std::list<TType>> final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													ElementType=TType;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE CLASS SPECIALIZATION pre C-ARRAY s DEFINED LENGTH.
template<typename TType, size_t SIZE>
struct STypeFunctionElementType<TType[SIZE]> final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													ElementType=TType;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE CLASS SPECIALIZATION pre C-ARRAY s UNDEFINED LENGTH.
template<typename TType>
struct STypeFunctionElementType<TType[]> final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													ElementType=TType;
//----------------------------------------------------------------------------------------------------------------------
};
