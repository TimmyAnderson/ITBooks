NTSTATUS CompleteRequest(PIRP Irp, NTSTATUS Status, ULONG_PTR Info, CCHAR Boost)
{
	Irp->IoStatus.Status=Status;
	Irp->IoStatus.Information=Info;

	IoCompleteRequest(Irp,Boost);

	return(Status);
}
