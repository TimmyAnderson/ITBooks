using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Azure.Functions.Worker;
using Microsoft.Extensions.Logging;
//----------------------------------------------------------------------------------------------------------------------
namespace MyAzureFunctions
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class FunctionHelloWorld
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<FunctionHelloWorld>			MLogger;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public FunctionHelloWorld(ILogger<FunctionHelloWorld> Logger)
		{
			MLogger=Logger;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! ATTRIBUTE [Function] prideluje nazov AZURE FUNCTION.
		[Function("HelloWorld")]
		// !!! ATTRIBUTE [HttpTrigger] v ENUM [AuthorizationLevel] urcuje AUTHORIZATION LEVEL pre AZURE FUNCTION.
		public IActionResult Run([HttpTrigger(AuthorizationLevel.Anonymous,"get")] HttpRequest Request)
		{
			MLogger.LogInformation("HTTP FUNCTION [HELLO WORLD] CALLED.");

			IActionResult										Result=new OkObjectResult("Hello AZURE FUNCTIONS.");

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------