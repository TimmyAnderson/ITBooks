using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using UsingDependencyInjection.Service;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Helpers
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS vyuziva dalsie 3 SERVICES.
	public sealed class CUsing3Services
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly ITextService1							MService1;
		private readonly ITextService2							MService2;
		private readonly ITextService3							MService3;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CUsing3Services(ITextService1 Service1, ITextService2 Service2, ITextService3 Service3)
		{
			MService1=Service1;
			MService2=Service2;
			MService3=Service3;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string GetText1()
		{
			return(MService1.GetText());
		}
//----------------------------------------------------------------------------------------------------------------------
		public string GetText2()
		{
			return(MService2.GetText());
		}
//----------------------------------------------------------------------------------------------------------------------
		public string GetText3()
		{
			return(MService3.GetText());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------