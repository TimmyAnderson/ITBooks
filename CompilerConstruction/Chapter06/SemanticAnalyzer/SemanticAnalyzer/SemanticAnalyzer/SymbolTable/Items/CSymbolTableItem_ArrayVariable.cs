using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace SemanticAnalyzer
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CSymbolTableItem_ArrayVariable : CSymbolTableItem_Variable
	{
//-------------------------------------------------------------------------------------------------------
		private readonly int									MArraySize;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSymbolTableItem_ArrayVariable(string SymbolName, ESymbolType SymbolType, int ArraySize)
			: base(SymbolName,SymbolType)
		{
			MArraySize=ArraySize;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int												ArraySize
		{
			get
			{
				return(MArraySize);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
