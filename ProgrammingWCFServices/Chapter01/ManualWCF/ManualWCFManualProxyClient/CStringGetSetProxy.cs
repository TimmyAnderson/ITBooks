using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using ManualWCFLibrary;
//-------------------------------------------------------------------------------------------------------
namespace ManualWCFManualProxyClient
{
//-------------------------------------------------------------------------------------------------------
	class CStringGetSetProxy : ClientBase<IStringGetSetContract>, IStringGetSetContract
	{
//-------------------------------------------------------------------------------------------------------
		public CStringGetSetProxy(Binding ProxyBinding, string EndpointAddress)
			: base(ProxyBinding, new EndpointAddress(EndpointAddress))
		{
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string GetStringValue()
		{
			return(Channel.GetStringValue());
		}
//-------------------------------------------------------------------------------------------------------
		public void SetStringValue(string Value)
		{
			Channel.SetStringValue(Value);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
