using System;
using System.Collections.Generic;
using System.Collections.ObjectModel;
using System.ComponentModel.DataAnnotations;
using System.Text;
using Helpers;
//----------------------------------------------------------------------------------------------------------------------
namespace NonrelationalProperties
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! ENTITY CLASS ma definovany COMPOSITE PRIMARY KEY BY FLUENT API.
	public sealed class CEntityPrimaryKeyFluentAPI
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID1;
		private int												MID2;
		private string											MValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityPrimaryKeyFluentAPI()
		{
			MID1=default(int);
			MID2=default(int);
			MValue=default(string);
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityPrimaryKeyFluentAPI(int ID1, int ID2, string Value)
		{
			MID1=ID1;
			MID2=ID2;
			MValue=Value;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY je oznacena ako COMPOSITE PRIMARY KEY BY FLUENT API.
		public int												ID1
		{
			get
			{
				return(MID1);
			}
			private set
			{
				MID1=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY je oznacena ako COMPOSITE PRIMARY KEY BY FLUENT API.
		public int												ID2
		{
			get
			{
				return(MID2);
			}
			private set
			{
				MID2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Value
		{
			get
			{
				return(MValue);
			}
			private set
			{
				MValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static void PrintToConsole(CEntityPrimaryKeyFluentAPI[] Entities)
		{
			for(int Index=0;Index<Entities.Length;Index++)
			{
				CEntityPrimaryKeyFluentAPI						Entity=Entities[Index];
				string											EntityText=Entity.Print(Index+1);
							
				if (Index>0)
				{
					CConsoleHelpers.WriteLineSeparator();
				}

				CConsoleHelpers.Write(EntityText,ConsoleColor.Green);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string Print(int? Index)
		{
			StringBuilder										SB=new StringBuilder();

			if (Index!=null)
			{
				SB.AppendLine($"ENTITY PRIMARY DATA ANNOTATIONS [{Index}]:");
			}
			else
			{
				SB.AppendLine($"ENTITY PRIMARY DATA ANNOTATIONS:");
			}

			SB.AppendLine($"\tID1 [{MID1}] !");
			SB.AppendLine($"\tID2 [{MID2}] !");

			SB.AppendLine($"\tVALUE [{MValue}] !");

			return(SB.ToString());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
