//-------------------------------------------------------------------------------------------------------
#pragma once
//-------------------------------------------------------------------------------------------------------
#include <string>
#include <functional>
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
class CTypeWithMoveOperator
{
//-------------------------------------------------------------------------------------------------------
	private:
		wstring													MValue;

	public:
		CTypeWithMoveOperator& operator=(CTypeWithMoveOperator&& Other)
		{
			MValue=std::move(Other.MValue);

			return(*this);
		}

	public:
		const wstring& GetValue(void) noexcept
		{
			return(MValue);
		}

	public:
		CTypeWithMoveOperator(const wstring& Value)
			: MValue(Value)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
//-------------------------------------------------------------------------------------------------------