using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Diagnostics;
//-------------------------------------------------------------------------------------------------------
namespace CSharpAssembly
{
//-------------------------------------------------------------------------------------------------------
	class Program
	{
//-------------------------------------------------------------------------------------------------------
		public static void DoTest()
		{
			Console.WriteLine("DEBUGGER will be STARTED !");
			Debugger.Break();

			Random												RND=new Random();

			for(int Index=0;Index<10;Index++)
			{
				int												RandomValue=RND.Next();

				if ((RandomValue % 2)==0)
				{
					Console.WriteLine(string.Format("VALUE [{0}] is EVEN !",RandomValue));
				}
				else
				{
					Console.WriteLine(string.Format("VALUE [{0}] is ODD !",RandomValue));
				}
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		static void Main(string[] args)
		{
			DoTest();

			Console.ReadLine();
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
