using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CNotificationWriteToLog : INotification
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MOperation;
		private CDtoFullName									MFullName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CNotificationWriteToLog()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CNotificationWriteToLog(string Operation, CDtoFullName FullName)
		{
			MOperation=Operation;
			MFullName=FullName;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Operation
		{
			get
			{
				return(MOperation);
			}
			set
			{
				MOperation=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CDtoFullName										FullName
		{
			get
			{
				return(MFullName);
			}
			set
			{
				MFullName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------