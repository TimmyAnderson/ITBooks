//-------------------------------------------------------------------------------------------------------
#include "LocalVariableClass.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------------------------------
CLocalVariableClass::CLocalVariableClass(wstring Value)
	: MValue(Value)
{
	wprintf_s(L"CONSTRUCTOR: [%s] !\n",MValue.c_str());
}
//-------------------------------------------------------------------------------------------------------
CLocalVariableClass::~CLocalVariableClass(void)
{
	wprintf_s(L"DESTRUCTOR: [%s] !\n",MValue.c_str());
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
void CLocalVariableClass::PrintValue(void)
{
	wprintf_s(L"VALUE: [%s] !\n",MValue.c_str());
}
