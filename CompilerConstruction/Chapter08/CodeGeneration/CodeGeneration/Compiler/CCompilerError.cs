using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CodeGeneration
{
//-------------------------------------------------------------------------------------------------------
	public class CCompilerError
	{
//-------------------------------------------------------------------------------------------------------
		private readonly string									MErrorDescription;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CCompilerError(string ErrorDescription)
		{
			MErrorDescription=ErrorDescription;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											ErrorDescription
		{
			get
			{
				return(MErrorDescription);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public virtual string GetError(CSourceCode SourceCode)
		{
			StringBuilder			SB=new StringBuilder();

			SB.AppendFormat("ErrorDescription: [{0}]",MErrorDescription);

			return(SB.ToString());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------