using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace LOH
{
//-------------------------------------------------------------------------------------------------------
	class LOH
	{
//-------------------------------------------------------------------------------------------------------
        public void Run()
        {
            byte[]			B=null;

            Console.WriteLine("Press any key to allocate on LOH !");
            Console.ReadKey();

            B=new byte[100000];

            Console.WriteLine("Press any key to GC !");
            Console.ReadKey();

            B=null;
            GC.Collect();

            Console.WriteLine("Press any key to exit !");
            Console.ReadKey();
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		static void Main(string[] args)
		{
            LOH				L=new LOH();

            L.Run();
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
