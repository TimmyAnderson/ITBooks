using System.Diagnostics;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
//--------------------------------------------------------------------------------------------------------------------------------
namespace EntraExternalIDWebPages
{
//--------------------------------------------------------------------------------------------------------------------------------
	[Authorize]
	public class HomeController:Controller
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<HomeController>				MLogger;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public HomeController(ILogger<HomeController> Logger)
		{
			MLogger=Logger;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public IActionResult Index()
		{
			return View();
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public IActionResult Privacy()
		{
			return View();
		}
//--------------------------------------------------------------------------------------------------------------------------------
	    [AllowAnonymous]
		[ResponseCache(Duration=0,Location=ResponseCacheLocation.None,NoStore=true)]
		public IActionResult Error()
		{
			CErrorViewModel										ErrorViewModel=new CErrorViewModel(Activity.Current?.Id ?? HttpContext.TraceIdentifier);
			IActionResult										Result=View(ErrorViewModel);

			return(Result);
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
