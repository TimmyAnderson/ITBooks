//----------------------------------------------------------------------------------------------------------------------
// !!!!! IMPLEMENTACIE VALUE LIST OPERATIONS, ktore boli definovane povodne pre TYPE LIST.
// !!!!! Kedze OPERATIONS pre TYPE LIST su definovane ako TEMPLATE CLASSES, pre VALUE LIST staci definovat ich TEMPLATE CLASS SPECIALIZATIONS, aby tieto OPERATIONS boli implementovane aj pre VALUE LISTS.
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType, TType... VALUES>
struct CTypeListIsEmptyType<CValueList1<TType,VALUES...>>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									VALUE=(sizeof...(VALUES)==0);
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType, TType Head, TType... Tail>
struct CTypeListFrontType<CValueList1<TType,Head,Tail...>>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													TYPE=CCompileTimeValue1<TType,Head>;

	public:
		static constexpr TType									VALUE=Head;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType, TType... VALUES, TType New>
struct CTypeListPushFrontType<CValueList1<TType,VALUES...>,CCompileTimeValue1<TType,New>>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													TYPE=CValueList1<TType,New,VALUES...>;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType, TType Head, TType... Tail>
struct CTypeListPopFrontType<CValueList1<TType,Head,Tail...>>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													TYPE=CValueList1<TType,Tail...>;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TType, TType... VALUES, TType New>
struct CTypeListPushBack1Type<CValueList1<TType,VALUES...>,CCompileTimeValue1<TType,New>>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		using													TYPE=CValueList1<TType,VALUES...,New>;
//----------------------------------------------------------------------------------------------------------------------
};
