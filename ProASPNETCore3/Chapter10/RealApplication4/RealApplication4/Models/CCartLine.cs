using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication4.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CCartLine
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MCartLineID;
		private CProduct										MProduct;
		private int												MQuantity;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCartLine()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CCartLine(CProduct Product, int Quantity)
		{
			MProduct=Product;
			MQuantity=Quantity;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CCartLine(int CartLineID, CProduct Product, int Quantity)
		{
			MCartLineID=CartLineID;
			MProduct=Product;
			MQuantity=Quantity;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Key]
		public int												CartLineID
		{
			get
			{
				return(MCartLineID);
			}
			set
			{
				MCartLineID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CProduct											Product
		{
			get
			{
				return(MProduct);
			}
			set
			{
				MProduct=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Quantity
		{
			get
			{
				return(MQuantity);
			}
			set
			{
				MQuantity=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------