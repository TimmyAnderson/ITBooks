//----------------------------------------------------------------------------------------------------------------------
#include "CDeductionDefaultArguments.h"
//----------------------------------------------------------------------------------------------------------------------
using namespace std;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CDeductionDefaultArguments::CDeductionDefaultArguments(int Value1, int Value2)
	: MValue1(Value1), MValue2(Value2)
{
}
//----------------------------------------------------------------------------------------------------------------------
CDeductionDefaultArguments::~CDeductionDefaultArguments(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
int CDeductionDefaultArguments::GetValue1(void) const noexcept
{
	return(MValue1);
}
//----------------------------------------------------------------------------------------------------------------------
int CDeductionDefaultArguments::GetValue2(void) const noexcept
{
	return(MValue2);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
wostream& operator<<(wostream& Stream, const CDeductionDefaultArguments& Value)
{
	Stream << L"VALUE 1 [" << Value.GetValue1() << L"] VALUE 2 [" << Value.GetValue2() << L"]";

	return(Stream);
}
