//----------------------------------------------------------------------------------------------------------------------
package DI1;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;
//----------------------------------------------------------------------------------------------------------------------
// !!! Pouzije sa ANNOTATION [@Component], no mohol by sa pouzit aj ANNOTATION [@Bean] aplikovany na FACTORY METHOD v CONFIGURATION CLASS.
@Component
public class CDIFieldParent
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue;
	// !!! ANNOTATION [@Autowired] sa aplikuje na FIELD, aby FIELD bol automaticky nastaveny SPRING FRAMEWORK pomocou DEPENDENCY INJECTION. 
	@Autowired
	private CDIFieldChild										MChild;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CDIFieldParent()
	{
		System.out.println("CDIFieldParent CREATED.");
		
		MValue="PARENT set in CONSTRUCTOR !!!";
	} 	
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue()
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue(String Value)
	{
		MValue=Value;
	}
//----------------------------------------------------------------------------------------------------------------------
	public CDIFieldChild getChild()
	{
		return(MChild);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setChild(CDIFieldChild Child)
	{
		MChild=Child;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------