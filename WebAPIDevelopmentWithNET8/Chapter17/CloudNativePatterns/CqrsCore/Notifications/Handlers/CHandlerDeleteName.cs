using System.Threading;
using System.Threading.Tasks;
using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CHandlerDeleteName : INotificationHandler<CNotificationDeleteName>
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly INameRepository						MNameRepository;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CHandlerDeleteName(INameRepository NameRepository)
		{
			MNameRepository=NameRepository;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public async Task Handle(CNotificationDeleteName Request, CancellationToken CancellationToken)
		{
			await MNameRepository.DeleteName(Request.ID);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------