CObject::CObject(const wstring& Value)
	: MValue(Value)
{
	wcout << L"CONSTRUCTOR [" << __func__ << L"()] with VALUE [" << MValue << L"] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
CObject::~CObject(void)
{
	wcout << L"DESTRUCTOR [" << __func__ << L"()] with VALUE [" << MValue << L"] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
const wstring& CObject::GetValue(void) const noexcept
{
	return(MValue);
}
