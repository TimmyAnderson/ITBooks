//----------------------------------------------------------------------------------------------------------------------
#include "CHasDefaultConstructor.h"
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CHasDefaultConstructor::CHasDefaultConstructor(void)
	: MValue(0)
{
}
//----------------------------------------------------------------------------------------------------------------------
CHasDefaultConstructor::CHasDefaultConstructor(int Value)
	: MValue(Value)
{
}
//----------------------------------------------------------------------------------------------------------------------
CHasDefaultConstructor::~CHasDefaultConstructor(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
int CHasDefaultConstructor::GetValue(void) const noexcept
{
	return(MValue);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CHasDefaultConstructor operator+(const CHasDefaultConstructor& Value1, int Value2)
{
	CHasDefaultConstructor										Result(Value1.GetValue()+Value2);

	return(Result);
}
