namespace WebAPICommonPracticesWeb
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CMyCacheResponse
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private CMyCachedData									MValue;
		private bool?											MIsValueFromCache;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyCacheResponse()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyCacheResponse(CMyCachedData Value, bool? IsValueFromCache)
		{
			MValue=Value;
			MIsValueFromCache=IsValueFromCache;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyCachedData									Value
		{
			get
			{
				return(MValue);
			}
			set
			{
				MValue=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public bool?											IsValueFromCache
		{
			get
			{
				return(MIsValueFromCache);
			}
			set
			{
				MIsValueFromCache=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
