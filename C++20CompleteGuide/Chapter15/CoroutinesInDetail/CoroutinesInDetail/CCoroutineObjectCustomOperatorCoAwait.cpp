CCoroutineObjectCustomOperatorCoAwait::CCoroutineObjectCustomOperatorCoAwait(coroutine_handle<SCoroutineObjectCustomOperatorCoAwaitPromiseType<CCoroutineObjectCustomOperatorCoAwait>> Handle)
	: MHandle(Handle)
{
}
//----------------------------------------------------------------------------------------------------------------------
CCoroutineObjectCustomOperatorCoAwait::CCoroutineObjectCustomOperatorCoAwait(CCoroutineObjectCustomOperatorCoAwait&& Other) noexcept
	: MHandle(move(Other.MHandle))
{
	Other.MHandle=nullptr;
}
//----------------------------------------------------------------------------------------------------------------------
CCoroutineObjectCustomOperatorCoAwait::~CCoroutineObjectCustomOperatorCoAwait(void) noexcept
{
	if (static_cast<bool>(MHandle)==true)
	{
		MHandle.destroy();
		MHandle=nullptr;
	}
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CCoroutineObjectCustomOperatorCoAwait& CCoroutineObjectCustomOperatorCoAwait::operator=(CCoroutineObjectCustomOperatorCoAwait&& Other) noexcept
{
	if (this!=&Other)
	{
		if (static_cast<bool>(MHandle)==true)
		{
			MHandle.destroy();
			MHandle=nullptr;
		}

		MHandle=move(Other.MHandle);

		Other.MHandle=nullptr;
	}

	return(*this);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
bool CCoroutineObjectCustomOperatorCoAwait::Resume(void) const
{
	if (static_cast<bool>(MHandle)==false || MHandle.done()==true)
	{
		return(false);
	}

	MHandle.resume();

	bool														CanResume=(!MHandle.done());

	return(CanResume);
}
