using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace CreatingRESTfulWebServices.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CValidationErrors
	{
//----------------------------------------------------------------------------------------------------------------------
		private CValidationError[]								MErrors;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CValidationErrors()
		{
			MErrors=new CValidationError[0];
		}
//----------------------------------------------------------------------------------------------------------------------
		public CValidationErrors(CValidationError[] Errors)
		{
			MErrors=Errors;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CValidationError[]								Errors
		{
			get
			{
				return(MErrors);
			}
			set
			{
				MErrors=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------