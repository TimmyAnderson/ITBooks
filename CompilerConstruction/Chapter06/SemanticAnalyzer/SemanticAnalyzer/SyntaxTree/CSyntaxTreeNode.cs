using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace SemanticAnalyzer
{
//-------------------------------------------------------------------------------------------------------
	// Aj ked je to TREE NODE, tak v jeho hrany su definovane az v DERIVED CLASSES a ich pocet zavisi od typu NODE.
	public abstract class CSyntaxTreeNode
	{
//-------------------------------------------------------------------------------------------------------
		private readonly CChoice								MChoice;
//-------------------------------------------------------------------------------------------------------
		protected int											MChoiceNumber;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSyntaxTreeNode(CChoice Choice)
		{
			MChoice=Choice;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CChoice											Choice
		{
			get
			{
				return(MChoice);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public abstract string PrintTree();
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(PrintTree());
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
