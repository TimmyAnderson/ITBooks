using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ParameterlessProperties
{
//-------------------------------------------------------------------------------------------------------
	public class CInitializersTest
	{
//-------------------------------------------------------------------------------------------------------
		private string											MSimpleProperty;
		private string[]										MArrayProperty;
		private List<string>									MListProperty;
		private Queue<string>									MQueueProperty;
		private Dictionary<string,int>							MDictionaryProperty;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CInitializersTest()
		{
			MListProperty=new List<string>();
			MQueueProperty=new Queue<string>();
			MDictionaryProperty=new Dictionary<string,int>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											SimpleProperty
		{
			get
			{
				return(MSimpleProperty);
			}
			set
			{
				MSimpleProperty=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string[]											ArrayProperty
		{
			get
			{
				return(MArrayProperty);
			}
			set
			{
				MArrayProperty=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		public List<string>										ListProperty
		{
			get
			{
				return(MListProperty);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public Queue<string>									QueueProperty
		{
			get
			{
				return(MQueueProperty);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public Dictionary<string,int>							DictionaryProperty
		{
			get
			{
				return(MDictionaryProperty);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------