using Microsoft.AspNetCore.Authorization;
//----------------------------------------------------------------------------------------------------------------------
namespace SecurityAndIdentity
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CSecurityAuthorizationRequirement2 : IAuthorizationRequirement
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MClaimsEndsWith;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CSecurityAuthorizationRequirement2(string ClaimsEndsWith)
		{
			MClaimsEndsWith=ClaimsEndsWith;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											ClaimsEndsWith
		{
			get
			{
				return(MClaimsEndsWith);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
