ULONG CGlobals::GetPIDsCount(void)
{
	return(MPIDsCount);
}
//----------------------------------------------------------------------------------------------------------------------
void CGlobals::SetPIDsCount(ULONG Value)
{
	MPIDsCount=Value;
}
//----------------------------------------------------------------------------------------------------------------------
void CGlobals::IncrementPIDsCount(ULONG Value)
{
	MPIDsCount+=Value;
}
//----------------------------------------------------------------------------------------------------------------------
void CGlobals::DecrementPIDsCount(ULONG Value)
{
	MPIDsCount-=Value;
}
//----------------------------------------------------------------------------------------------------------------------
ULONG CGlobals::GetPID(int Index)
{
	if (Index>=0 && Index<MAX_PIDS)
	{
		ULONG													PID=MPIDs[Index];

		return(PID);
	}
	else
	{
		return(0);
	}
}
//----------------------------------------------------------------------------------------------------------------------
void CGlobals::SetPID(int Index, ULONG PID)
{
	if (Index>=0 && Index<MAX_PIDS)
	{
		MPIDs[Index]=PID;
	}
}
