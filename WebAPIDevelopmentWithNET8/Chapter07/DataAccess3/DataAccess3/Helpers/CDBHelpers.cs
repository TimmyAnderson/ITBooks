using System;
using Microsoft.EntityFrameworkCore;
//----------------------------------------------------------------------------------------------------------------------
namespace DataAccess3
{
//----------------------------------------------------------------------------------------------------------------------
	public static class CDBHelpers
	{
//----------------------------------------------------------------------------------------------------------------------
		public static void WriteEntityState<TEntity>(string Text, DbContext Context, TEntity Entity, ConsoleColor Color=ConsoleColor.Blue) where TEntity : class
		{
			Context.ChangeTracker.DetectChanges();

			CConsoleHelpers.WriteLine($"{Text} - ENTRY STATE [{Context.Entry(Entity).State}] !",Color);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------