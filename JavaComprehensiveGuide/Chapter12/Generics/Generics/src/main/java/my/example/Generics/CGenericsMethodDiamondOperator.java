package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CGenericsMethodDiamondOperator<TClassType>
{
//----------------------------------------------------------------------------------------------------------------------
	private final TClassType									classValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsMethodDiamondOperator(TClassType classValue)
	{
		this.classValue=classValue;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public static <TType> CGenericsMethodDiamondOperator<TType> GenericsMethod(TType value)
	{
		// !!! Pouzije sa DIAMOND OPERATOR.
		CGenericsMethodDiamondOperator<TType>					object=new CGenericsMethodDiamondOperator<>(value);
		
		return(object);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public TClassType getClassValue()
	{
		return(classValue);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------