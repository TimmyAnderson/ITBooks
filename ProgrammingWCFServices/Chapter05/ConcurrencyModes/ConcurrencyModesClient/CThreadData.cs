using System;
using System.Collections.Generic;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ConcurrencyModesClient
{
//-------------------------------------------------------------------------------------------------------
	class CThreadData
	{
//-------------------------------------------------------------------------------------------------------
		private	string											MClientName;
		private	CConcurrencyModesProxy							MProxy;
		private	string											MText;
		private	int												MClientTimeout;
		private	int												MServerTimeout;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CThreadData(string ClientName, CConcurrencyModesProxy Proxy, string Text, int ClientTimeout, int ServerTimeout)
		{
			MClientName=ClientName;
			MProxy=Proxy;
			MText=Text;
			MClientTimeout=ClientTimeout;
			MServerTimeout=ServerTimeout;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											ClientName
		{
			get
			{
				return(MClientName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public CConcurrencyModesProxy							Proxy
		{
			get
			{
				return(MProxy);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public	string											Text
		{
			get
			{
				return(MText);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public	int												ServerTimeout
		{
			get
			{
				return(MServerTimeout);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public	int												ClientTimeout
		{
			get
			{
				return(MClientTimeout);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------