using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace AdvancedControllersAndViews.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CNameRepository : INameRepository
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly List<CName>							MNames;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CNameRepository()
		{
			MNames=new List<CName>();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName[]											Names
		{
			get
			{
				return(MNames.ToArray());
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void AddName(CName Name)
		{
			MNames.Add(Name);
		}
//----------------------------------------------------------------------------------------------------------------------
		public CName[] GetNames()
		{
			CName[]												Names=MNames.ToArray();

			return(Names);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
