package my.example.Generics;

import java.util.ArrayList;
import java.util.List;

//----------------------------------------------------------------------------------------------------------------------
public final class CList<TItem>
{
//----------------------------------------------------------------------------------------------------------------------
	private final List<TItem>									list;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CList()
	{
		list=new ArrayList<TItem>();
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	// !!! METHOD nie je READ METHOD, ani WRITE METHOD, pretoze v RETURN VALUE TYPE, ani PARAMETER TYPES nepouziva TYPE PARAMETER [TItem].
	public int Size()
	{
		int														size=list.size();
		
		return(size);
	}
//----------------------------------------------------------------------------------------------------------------------
	// !!! METHOD je READ METHOD, pretoze RETURN VALUE TYPE je TYPE PARAMETER [TItem].
	public TItem GetItem(int index)
	{
		TItem													item=list.get(index);
		
		return(item);
	}
//----------------------------------------------------------------------------------------------------------------------
	// !!! METHOD je WRITE METHOD, pretoze METHOD PARAMETER TYPE je TYPE PARAMETER [TItem].
	public void AddItem(TItem item)
	{
		list.add(item);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------