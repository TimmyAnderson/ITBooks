namespace Testing2
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CUserProperties
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly string									MUserName;
		private readonly string									MPassword;
		private readonly string[]								MRoles;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CUserProperties(string UserName, string Password, string[] Roles)
		{
			MUserName=UserName;
			MPassword=Password;
			MRoles=Roles;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public string											Password
		{
			get
			{
				return(MPassword);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public string[]											Roles
		{
			get
			{
				return(MRoles);
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
