using System;
using System.Collections.Generic;
using System.Linq;
using System.Net.Http;
using System.Threading.Tasks;
using Dapr;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
//----------------------------------------------------------------------------------------------------------------------
namespace PublishSubscribeSubscriber1
{
//----------------------------------------------------------------------------------------------------------------------
	[ApiController]
	[Route("[controller]")]
	public class MySubscriberService1Controller : ControllerBase
	{
//----------------------------------------------------------------------------------------------------------------------
		private const string									SERVICE_NAME="pubsub";
		private const string									TOPIC_NAME="MyTopic";
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<MySubscriberService1Controller>	MLogger;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public MySubscriberService1Controller(ILogger<MySubscriberService1Controller> Logger)
		{
			MLogger=Logger;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Topic(SERVICE_NAME,TOPIC_NAME)]
		[HttpPost]
		public string CallService(CMessage Message)
		{
			string												Value=$"VALUE 1 [{Message.Value1}] VALUE 2 [{Message.Value2}].";

			MLogger.LogInformation($"SUBSCRIBER SERVICE 1 CALLED with VALUE [{Value}] !");

			return(Value);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
