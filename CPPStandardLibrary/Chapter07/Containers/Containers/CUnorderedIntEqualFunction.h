#include "CUnorderedInt.h"
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
class CUnorderedIntEqualFunction
{
//-------------------------------------------------------------------------------------------------------
	private:
		int														MValue;

	public:
		bool operator()(const CUnorderedInt& Value1, const CUnorderedInt& Value2) const
		{
			if (Value1==Value2)
			{
				return(true);
			}
			else
			{
				return(false);
			}
		}

	public:
		int GetValue(void) const noexcept
		{
			return(MValue);
		}

	public:
		CUnorderedIntEqualFunction(int Value)
			: MValue(Value)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
//-------------------------------------------------------------------------------------------------------