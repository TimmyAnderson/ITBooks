using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace DCGenericsLibrary
{
//-------------------------------------------------------------------------------------------------------
	// Nastavim Name v [DataContract] s pouzitim mane typu: {0} a hash: {#}.
	[DataContract(Name="CGenericClass4_{0}_Hash_{#}")]
	public class CGenericClass4<T>
	{
//-------------------------------------------------------------------------------------------------------
		private	T												MData;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CGenericClass4(T Data)
		{
			MData=Data;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public	T												Data
		{
			get
			{
				return(MData);
			}
			set
			{
				MData=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("{0}: {1}","CGenericClass4",MData.ToString()));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------