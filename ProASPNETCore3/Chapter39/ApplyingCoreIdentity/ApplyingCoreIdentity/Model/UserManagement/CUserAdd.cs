using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Identity;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Model
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS reprezentuje DATA pre pridavanie novych USERS v RAZOR PAGES.
	public sealed class CUserAdd
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MUserName;
		private string											MEmail;
		private string											MPassword;
		private string											MDescription;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CUserAdd()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CUserAdd(string UserName, string Email, string Password, string Description)
		{
			MUserName=UserName;
			MEmail=Email;
			MPassword=Password;
			MDescription=Description;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
			set
			{
				MUserName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Email
		{
			get
			{
				return(MEmail);
			}
			set
			{
				MEmail=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Password
		{
			get
			{
				return(MPassword);
			}
			set
			{
				MPassword=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Description
		{
			get
			{
				return(MDescription);
			}
			set
			{
				MDescription=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------