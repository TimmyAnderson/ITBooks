using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace DCGenericsLibrary
{
//-------------------------------------------------------------------------------------------------------
	// Necham [DataContrat] ako je.
	[DataContract]
	public class CGenericClass1<T>
	{
//-------------------------------------------------------------------------------------------------------
		private	T												MData;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CGenericClass1(T Data)
		{
			MData=Data;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public	T												Data
		{
			get
			{
				return(MData);
			}
			set
			{
				MData=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("{0}: {1}","CGenericClass1",MData.ToString()));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------