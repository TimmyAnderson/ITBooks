using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;
//------------------------------------------------------------------------------------------------------
namespace CreateBagNet
{
//------------------------------------------------------------------------------------------------------
	[DataContract]
	public sealed class CBagNetRecord
	{
//------------------------------------------------------------------------------------------------------
		private string											MFileName;
		private bool											MIsEXE;
		private string											MAssemblyIdentity;
		private byte[]											MFileData;
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
		public CBagNetRecord(string FileName, bool IsEXE, string AssemblyIdentity, byte[] FileData)
		{
			MFileName=FileName;
			MIsEXE=IsEXE;
			MAssemblyIdentity=AssemblyIdentity;
			MFileData=FileData;
		}
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											FileName
		{
			get
			{
				return(MFileName);
			}
			private set
			{
				MFileName=value;
			}
		}
//------------------------------------------------------------------------------------------------------
		[DataMember]
		public bool												IsEXE
		{
			get
			{
				return(MIsEXE);
			}
			private set
			{
				MIsEXE=value;
			}
		}
//------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											AssemblyIdentity
		{
			get
			{
				return(MAssemblyIdentity);
			}
			private set
			{
				MAssemblyIdentity=value;
			}
		}
//------------------------------------------------------------------------------------------------------
		[DataMember]
		public byte[]											FileData
		{
			get
			{
				return(MFileData);
			}
			private set
			{
				MFileData=value;
			}
		}
//------------------------------------------------------------------------------------------------------
	}
//------------------------------------------------------------------------------------------------------
}
//------------------------------------------------------------------------------------------------------