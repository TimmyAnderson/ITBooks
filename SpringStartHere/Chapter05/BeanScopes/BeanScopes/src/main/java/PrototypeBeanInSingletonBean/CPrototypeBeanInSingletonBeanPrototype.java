//----------------------------------------------------------------------------------------------------------------------
package PrototypeBeanInSingletonBean;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.beans.factory.config.BeanDefinition;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;
//----------------------------------------------------------------------------------------------------------------------
// !!! SPRING BEAN ma PROTOTYPE SCOPE.
@Component
@Scope(BeanDefinition.SCOPE_PROTOTYPE) 
public class CPrototypeBeanInSingletonBeanPrototype
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CPrototypeBeanInSingletonBeanPrototype()
	{
		System.out.println("BEAN [CPrototypeBeanInSingletonBeanPrototype] CREATED.");
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue()
	{
		return(MValue);	
	} 	
//----------------------------------------------------------------------------------------------------------------------
	public void setValue(String Value)
	{
		MValue=Value;
	} 	
//----------------------------------------------------------------------------------------------------------------------
	public void Print()
	{
		System.out.format("OBJECT [CPrototypeBeanInSingletonBeanPrototype] with VALUE [%s] CALLED.\n",MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------