#pragma once
//-------------------------------------------------------------------------------------------------------
#include <iostream>
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
class CInt
{
//-------------------------------------------------------------------------------------------------------
	private:
		int														MValue;

	public:
		// !!! MUSI sa pouzit KEYWORD [const].
		bool operator==(const CInt& Other) const
		{
			std::wcout << L"OPERATOR== CALLED !" << std::endl;

			if (MValue==Other.MValue)
			{
				return(true);
			}
			else
			{
				return(false);
			}
		}

		// !!! MUSI sa pouzit KEYWORD [const].
		bool operator<(const CInt& Other) const
		{
			std::wcout << L"OPERATOR< CALLED !" << std::endl;

			if (MValue<Other.MValue)
			{
				return(true);
			}
			else
			{
				return(false);
			}
		}

	public:
		int GetValue(void)
		{
			return(MValue);
		}

	public:
		CInt(int Value)
			: MValue(Value)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
