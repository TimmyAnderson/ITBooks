package Chapter08.SpringBootAndSpringMVC;
//----------------------------------------------------------------------------------------------------------------------
import java.util.ArrayList;
import java.util.List;
import org.springframework.stereotype.Service;
//----------------------------------------------------------------------------------------------------------------------
// !!! Je to BEAN OBJECT.
@Service
public class CPersonsService
{
//----------------------------------------------------------------------------------------------------------------------
	private final Object										MLock=new Object();
	private final List<CPerson>									MPersons;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CPersonsService() 	
	{
		MPersons=new ArrayList<CPerson>();
		
		MPersons.add(new CPerson("Timmy","Anderson",12));
		MPersons.add(new CPerson("Jenny","Thompson",13));
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public void AddName(CPerson Person)
	{
		synchronized(MLock)
		{
			MPersons.add(Person);
		}
	}	
//----------------------------------------------------------------------------------------------------------------------
	public List<CPerson> GetPersons()
	{
		synchronized(MLock)
		{
			List<CPerson>											Persons=new ArrayList<CPerson>(MPersons);

			return(Persons);
		}
	}	
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------