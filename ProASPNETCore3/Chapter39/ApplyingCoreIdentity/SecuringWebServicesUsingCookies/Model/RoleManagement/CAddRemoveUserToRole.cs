				MRoleID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											UserID
		{
			get
			{
				return(MUserID);
			}
			set
			{
				MUserID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
			set
			{
				MUserName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
