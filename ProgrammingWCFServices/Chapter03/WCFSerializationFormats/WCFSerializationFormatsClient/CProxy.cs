		public CName1 Method1(CName1 Name1)
		{
			return(Channel.Method1(Name1));
		}
//-------------------------------------------------------------------------------------------------------
		public CName2 Method2(CName2 Name2)
		{
			return(Channel.Method2(Name2));
		}
//-------------------------------------------------------------------------------------------------------
	}
