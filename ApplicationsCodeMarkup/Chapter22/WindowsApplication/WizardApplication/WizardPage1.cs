using System;
using System.Windows;
using System.Windows.Controls;
//-----------------------------------------------------------------------------------------------------------------
namespace WizardApplication
{
//-----------------------------------------------------------------------------------------------------------------
    public partial class WizardPage1: Page
    {
//-----------------------------------------------------------------------------------------------------------------
        private Vitals											MVitals;
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
        public WizardPage1(Vitals Vitals)
        {
            InitializeComponent();

            MVitals=Vitals;
        }
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
        private void PreviousButtonOnClick(object Sender, RoutedEventArgs Args)
        {
            NavigationService.GoBack();
        }
//-----------------------------------------------------------------------------------------------------------------
        private void NextButtonOnClick(object Sender, RoutedEventArgs Args)
        {
            MVitals.Name=MTxtBoxName.Text;
            MVitals.Home=Vitals.GetCheckedRadioButton(MGrpBoxHome).Content as string;
            MVitals.Gender=Vitals.GetCheckedRadioButton(MGrpBoxGender).Content as string;

            if (NavigationService.CanGoForward)
                NavigationService.GoForward();
            else
            {
                WizardPage2			Page=new WizardPage2(MVitals);

                NavigationService.Navigate(Page);
            }
        }
//-----------------------------------------------------------------------------------------------------------------
    }
//-----------------------------------------------------------------------------------------------------------------
}
