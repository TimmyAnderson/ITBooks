using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Newtonsoft.Json;
//----------------------------------------------------------------------------------------------------------------------
namespace Client
{
//----------------------------------------------------------------------------------------------------------------------
    public sealed class CGraphQLQuery
    {
//----------------------------------------------------------------------------------------------------------------------
		private string											MQuery;
		private object											MVariables;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CGraphQLQuery()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CGraphQLQuery(string Query)
		{
			MQuery=Query;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CGraphQLQuery(string Query, object Variables)
		{
			MQuery=Query;
			MVariables=Variables;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("query")]
		public string											Query
		{
			get
			{
				return(MQuery);
			}
			set
			{
				MQuery=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("variables")]
		public object											Variables
		{
			get
			{
				return(MVariables);
			}
			set
			{
				MVariables=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------