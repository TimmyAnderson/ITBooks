using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.ComponentModel;
//------------------------------------------------------------------------------
namespace EAP
{
//------------------------------------------------------------------------------
	public class CXXXProgressChangedEventArgs : ProgressChangedEventArgs
	{
//------------------------------------------------------------------------------
		private string								MID;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
		public CXXXProgressChangedEventArgs(int ProgressPercentage, object UserState, string ID)
			: base(ProgressPercentage,UserState)
		{
			MID=ID;
		}
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("ProgressPercentage: [{0}], ID: [{1}] !",base.ProgressPercentage,MID));
		}
//------------------------------------------------------------------------------
	}
//------------------------------------------------------------------------------
}
//------------------------------------------------------------------------------