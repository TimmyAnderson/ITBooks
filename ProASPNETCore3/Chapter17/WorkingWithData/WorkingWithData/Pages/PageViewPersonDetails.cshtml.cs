using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using WorkingWithData.Services;
using WorkingWithData.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace WorkingWithData.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class PageViewPersonDetailsModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly IDBOperations							MDBOperations;
//----------------------------------------------------------------------------------------------------------------------
		private CPerson											MPerson;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public PageViewPersonDetailsModel(IDBOperations DBOperations)
		{
			MDBOperations=DBOperations;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CPerson											Person
		{
			get
			{
				return(MPerson);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
        public IActionResult OnGet(int ID)
        {
			MPerson=MDBOperations.GetPerson(ID);

			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
