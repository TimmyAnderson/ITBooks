using System;
using System.Collections.Generic;
using System.Text;
//------------------------------------------------------------------------
namespace TreeViewTestLibrary
{
//------------------------------------------------------------------------
	public class CGender
	{
//------------------------------------------------------------------------
		private string						MGender;
		private CNameCollection				MNames;
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public CGender(string Gender, CNameCollection Names)
		{
			MGender=Gender;
			MNames=Names;
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public string						Gender
		{
			get
			{
				return(MGender);
			}
		}
//------------------------------------------------------------------------
		public CNameCollection				Names
		{
			get
			{
				return(MNames);
			}
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public override string ToString()
		{
			StringBuilder			SB=new StringBuilder();

			SB.AppendFormat("Gender: {0}",MGender);
			SB.AppendFormat(", Names: {0} !",MNames);

			return(SB.ToString());
		}
//------------------------------------------------------------------------
	}
//------------------------------------------------------------------------
}
