using System;
using System.Collections.Generic;
using System.Linq;
using System.Net;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace SecuringWebServicesBearerTokens.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CNameResult
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly HttpStatusCode							MStatusCode;
		private readonly CName[]								MNames;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CNameResult(HttpStatusCode StatusCode)
		{
			MStatusCode=StatusCode;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CNameResult(HttpStatusCode StatusCode, CName[] Names)
		{
			MStatusCode=StatusCode;
			MNames=Names;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public HttpStatusCode									StatusCode
		{
			get
			{
				return(MStatusCode);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CName[]											Names
		{
			get
			{
				return(MNames);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------