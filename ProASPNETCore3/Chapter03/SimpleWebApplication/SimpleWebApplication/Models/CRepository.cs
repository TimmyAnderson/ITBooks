using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace SimpleWebApplication.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public static class CRepository
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly static List<CGuestResponse>			MResponses;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		static CRepository()
		{
			MResponses=new List<CGuestResponse>();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static IEnumerable<CGuestResponse>				Responses
		{
			get
			{
				return(MResponses);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static void AddResponse(CGuestResponse Response)
		{
			MResponses.Add(Response);
		}		
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------