using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using DCInterfaceLibrary;
//-------------------------------------------------------------------------------------------------------
namespace DCInterfaceManualClient
{
//-------------------------------------------------------------------------------------------------------
	class DCInterfaceManualProxy : ClientBase<IDCInterfaceContract>, IDCInterfaceContract
	{
//-------------------------------------------------------------------------------------------------------
		public DCInterfaceManualProxy(Binding ProxyBinding, string EndpointAddress)
			: base(ProxyBinding, new EndpointAddress(EndpointAddress))
		{
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public IName GetName()
		{
 			return(Channel.GetName());
		}
//-------------------------------------------------------------------------------------------------------
		public void SendName(IName Name)
		{
 			Channel.SendName(Name);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
