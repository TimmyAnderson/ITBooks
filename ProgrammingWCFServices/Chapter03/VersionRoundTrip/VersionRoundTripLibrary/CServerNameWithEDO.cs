using System;
using System.Collections.Generic;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace VersionRoundTripLibrary
{
//-------------------------------------------------------------------------------------------------------
	[DataContract(Namespace="VersionRoundTripLibrary")]
	public class CServerNameWithEDO : IExtensibleDataObject
	{
//-------------------------------------------------------------------------------------------------------
		protected string										MFirstName;
		protected string										MLastName;
		protected ExtensionDataObject							MExtensionDataObject;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CServerNameWithEDO(string FirstName, string LastName, ExtensionDataObject ExtensionDataObject)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MExtensionDataObject=ExtensionDataObject;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
			set
			{
				MFirstName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			internal set
			{
				MLastName=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		// Implementacia rozhrania IExtensibleDataObject.
		public ExtensionDataObject								ExtensionData
		{
			get
			{
				return(MExtensionDataObject);
			}
			set
			{
				MExtensionDataObject=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("CServerNameWithEDO - FirstName: {0}, LastName: {1} !",MFirstName, MLastName));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
