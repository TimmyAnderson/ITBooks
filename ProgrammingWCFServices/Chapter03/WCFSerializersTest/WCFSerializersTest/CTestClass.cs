using System;
using System.Collections.Generic;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace WCFSerializersTest
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	class CTestClass
	{
//-------------------------------------------------------------------------------------------------------
		[DataMember]
		private string											MFirstName;
		[DataMember]
		private string											MLastName;
		[DataMember]
		private int												MAge;
		// Nebude serializovany.
		private string											MCity;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		// !!! Pri deserializacii sa tento constructor vobec nevola.
		public CTestClass()
		{
			MFirstName="NOT SERIALIZED";
			MLastName="NOT SERIALIZED";
			MAge=0;
			MCity="NOT SERIALIZED";
		}
//-------------------------------------------------------------------------------------------------------
		public CTestClass(string FirstName, string LastName, int Age, string City)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
			MCity=City;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public int												Age
		{
			get
			{
				return(MAge);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											City
		{
			get
			{
				return(MCity);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("FirstName: {0}, LastName: {1}, Age: {2}, City: {3}",MFirstName,MLastName,MAge,MCity));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------