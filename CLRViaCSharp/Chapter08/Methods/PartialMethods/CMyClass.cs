using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace PartialMethods
{
//-------------------------------------------------------------------------------------------------------
	public partial class CMyClass
	{
//-------------------------------------------------------------------------------------------------------
		partial void PartialMethod1();
//-------------------------------------------------------------------------------------------------------
		partial void PartialMethod2();
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		// !!! Tato metoda sluzi na prezretie v ILDASM.
		public void Execute()
		{
			Console.WriteLine("CMyClass ---> STEP 1 !");

			PartialMethod1();

			Console.WriteLine("CMyClass ---> STEP 2 !");

			PartialMethod2();

			Console.WriteLine("CMyClass ---> STEP 3 !");
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
