package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CGenericsWildcard<TTypeParameter1,TTypeParameter2>
{
//----------------------------------------------------------------------------------------------------------------------
	private int													nonGenericsValue;
	private TTypeParameter1										genericsValue1;
	private TTypeParameter2										genericsValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsWildcard(int nonGenericsValue, TTypeParameter1 genericsValue1, TTypeParameter2 genericsValue2)
	{
		this.nonGenericsValue=nonGenericsValue;
		this.genericsValue1=genericsValue1;
		this.genericsValue2=genericsValue2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public int getNonGenericsValue()
	{
		return(nonGenericsValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setNonGenericsValue(int value)
	{
		nonGenericsValue=value;
	}
//----------------------------------------------------------------------------------------------------------------------
	public TTypeParameter1 getGenericsValue1()
	{
		return(genericsValue1);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setGenericsValue1(TTypeParameter1 value)
	{
		genericsValue1=value;
	}
//----------------------------------------------------------------------------------------------------------------------
	public TTypeParameter2 getGenericsValue2()
	{
		return(genericsValue2);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setGenericsValue2(TTypeParameter2 value)
	{
		genericsValue2=value;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------