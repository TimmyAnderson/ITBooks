using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//-------------------------------------------------------------------------------------------------------
namespace FinalizerTest
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CDisposable : IDisposable
	{
//-------------------------------------------------------------------------------------------------------
		private readonly string									MObjectName;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CDisposable(string ObjectName)
		{
			MObjectName=ObjectName;

			Console.WriteLine(string.Format("OBJECT [{0}] was CREATED !",MObjectName));
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void Dispose()
		{
			Console.WriteLine(string.Format("OBJECT [{0}] was DISPOSED !",MObjectName));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------