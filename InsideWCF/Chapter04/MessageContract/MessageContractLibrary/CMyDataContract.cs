using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace MessageContractLibrary
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	public class CMyDataContract
	{
//-------------------------------------------------------------------------------------------------------
		private int					MIntValue;
		private string				MStringValue;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CMyDataContract(int IntValue, string StringValue)
		{
			MIntValue=IntValue;
			MStringValue=StringValue;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	    [DataMember]
		public int					IntValue
		{
			get
			{
				return(MIntValue);
			}
			set
			{
				MIntValue=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
	    [DataMember]
		public string				StringValue
		{
			get
			{
				return(MStringValue);
			}
			set
			{
				MStringValue=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("IntValue: {0}, StringValue: {1}",MIntValue,MStringValue));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------