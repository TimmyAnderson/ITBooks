using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace SimpleWebApplication.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CGuestResponse
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MName;
		private string											MEmail;
		private string											MPhone;
		private bool?											MWillAttend;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CGuestResponse()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CGuestResponse(string Name, string Email, string Phone, bool? WillAttend)
		{
			MName=Name;
			MEmail=Email;
			MPhone=Phone;
			MWillAttend=WillAttend;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="Please enter your name.")]
		public string											Name
		{
			get
			{
				return(MName);
			}
			set
			{
				MName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="Please enter your email address.")]
		[EmailAddress]
		public string											Email
		{
			get
			{
				return(MEmail);
			}
			set
			{
				MEmail=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="Please enter your phone number.")]
		public string											Phone
		{
			get
			{
				return(MPhone);
			}
			set
			{
				MPhone=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="Please specify whether you'll attend.")]
		public bool?											WillAttend
		{
			get
			{
				return(MWillAttend);
			}
			set
			{
				MWillAttend=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------