using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//-----------------------------------------------------------------------------
namespace RealApplication1.Models.ViewModels
{
//-----------------------------------------------------------------------------
	public sealed class CProductsListViewModel
	{
//-----------------------------------------------------------------------------
		private CProduct[]										MProducts;
		private CPagingInfo										MPagingInfo;
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
		public CProductsListViewModel(CProduct[] Products, CPagingInfo PagingInfo)
		{
			MProducts=Products;
			MPagingInfo=PagingInfo;
		}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
		public CProduct[]										Products
		{
			get
			{
				return(MProducts);
			}
			set
			{
				MProducts=value;
			}
		}
//-----------------------------------------------------------------------------
		public CPagingInfo										PagingInfo
		{
			get
			{
				return(MPagingInfo);
			}
			set
			{
				MPagingInfo=value;
			}
		}
//-----------------------------------------------------------------------------
	}
//-----------------------------------------------------------------------------
}
//-----------------------------------------------------------------------------