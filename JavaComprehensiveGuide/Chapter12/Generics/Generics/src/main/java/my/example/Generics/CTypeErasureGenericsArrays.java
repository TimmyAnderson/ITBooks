package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public final class CTypeErasureGenericsArrays<TType>
{
//----------------------------------------------------------------------------------------------------------------------
	private TType												field;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CTypeErasureGenericsArrays()
	{
		this.field=null;
	}
//----------------------------------------------------------------------------------------------------------------------
	public CTypeErasureGenericsArrays(TType field)
	{
		this.field=field;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public TType getField()
	{
		return(field);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setField(TType value)
	{
		field=value;
	}
//----------------------------------------------------------------------------------------------------------------------
}
