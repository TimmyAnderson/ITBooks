#pragma once
//----------------------------------------------------------------------------------------------------------------------
// !!! Toto je MIXIN, ktory je mozne rozsirit o dalsie BASE CLASSES.
template<typename... TTypes>
class CMixinClassMixins final : public TTypes...
{
//----------------------------------------------------------------------------------------------------------------------
	private:
		int														MValueInt;

	public:
		int GetValueInt(void) const;
		void SetValueInt(int Value);

	public:
		CMixinClassMixins(void);
		virtual ~CMixinClassMixins(void) noexcept;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename... TTypes>
CMixinClassMixins<TTypes...>::CMixinClassMixins(void)
	: TTypes()..., MValueInt()
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename... TTypes>
CMixinClassMixins<TTypes...>::~CMixinClassMixins(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename... TTypes>
int CMixinClassMixins<TTypes...>::GetValueInt(void) const
{
	return(MValueInt);
}
//----------------------------------------------------------------------------------------------------------------------
template<typename... TTypes>
void CMixinClassMixins<TTypes...>::SetValueInt(int Value)
{
	MValueInt=Value;
}
