using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace AdvancedControllersAndViews.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CNamesWithHeader
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MHeader;
		private CName[]											MNames;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CNamesWithHeader()
		{
			MHeader="";
			MNames=new CName[0];
		}
//----------------------------------------------------------------------------------------------------------------------
		public CNamesWithHeader(string Header, CName[] Names)
		{
			MHeader=Header;
			MNames=Names;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Header
		{
			get
			{
				return(MHeader);
			}
			set
			{
				MHeader=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CName[]											Names
		{
			get
			{
				return(MNames);
			}
			set
			{
				MNames=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------