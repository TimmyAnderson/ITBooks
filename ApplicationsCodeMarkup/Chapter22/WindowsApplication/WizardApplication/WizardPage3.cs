namespace WizardApplication
{
//-----------------------------------------------------------------------------------------------------------------
    public partial class WizardPage3: Page
    {
//-----------------------------------------------------------------------------------------------------------------
        private Vitals											MVitals;
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
        public WizardPage3(Vitals Vitals)
        {
            InitializeComponent();

            MVitals=Vitals;
        }
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------
        private void PreviousButtonOnClick(object Sender, RoutedEventArgs Args)
        {
            NavigationService.GoBack();
        }
//-----------------------------------------------------------------------------------------------------------------
        private void FinishButtonOnClick(object Sender, RoutedEventArgs Args)
        {
            MVitals.MomsMaidenName=MTxtBoxMom.Text;
            MVitals.Pet=Vitals.GetCheckedRadioButton(MGrpBoxPet).Content as string;
            MVitals.Income=Vitals.GetCheckedRadioButton(MGrpBoxIncome).Content as string;

            WizardPage4			Page=new WizardPage4(MVitals);

            NavigationService.Navigate(Page);
        }
//-----------------------------------------------------------------------------------------------------------------
    }
