package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
// !!! Deklaruju sa 2 TYPE PARAMETERS.
public final class CGenericsClass<TParameter1,TParameter2>
{
//----------------------------------------------------------------------------------------------------------------------
	private final TParameter1									value1;
	private final TParameter2									value2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsClass()
	{
		this.value1=null;
		this.value2=null;
	}
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsClass(TParameter1 value1, TParameter2 value2)
	{
		this.value1=value1;
		this.value2=value2;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public TParameter1 getValue1()
	{
		return(value1);
	}
//----------------------------------------------------------------------------------------------------------------------
	public TParameter2 getValue2()
	{
		return(value2);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CGenericsClass<TParameter1,TParameter2> CreateCopy()
	{
		CGenericsClass<TParameter1,TParameter2>					copy=new CGenericsClass<TParameter1,TParameter2>(value1,value2);

		return(copy);
	}
//----------------------------------------------------------------------------------------------------------------------
}
