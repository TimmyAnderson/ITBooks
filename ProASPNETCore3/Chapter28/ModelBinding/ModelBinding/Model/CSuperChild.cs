using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace ModelBinding.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CSuperChild
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MSuperID;
		private string											MSuperFirstName;
		private string											MSuperLastName;
		private int												MSuperAge;
		private ESex											MSuperSex;
		private CSuperChildAddress								MSuperAddress;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CSuperChild()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CSuperChild(int SuperID, string SuperFirstName, string SuperLastName, int SuperAge, ESex SuperSex, CSuperChildAddress SuperAddress)
		{
			MSuperID=SuperID;
			MSuperFirstName=SuperFirstName;
			MSuperLastName=SuperLastName;
			MSuperAge=SuperAge;
			MSuperSex=SuperSex;
			MSuperAddress=SuperAddress;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												SuperID
		{
			get
			{
				return(MSuperID);
			}
			set
			{
				MSuperID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											SuperFirstName
		{
			get
			{
				return(MSuperFirstName);
			}
			set
			{
				MSuperFirstName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											SuperLastName
		{
			get
			{
				return(MSuperLastName);
			}
			set
			{
				MSuperLastName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												SuperAge
		{
			get
			{
				return(MSuperAge);
			}
			set
			{
				MSuperAge=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public ESex												SuperSex
		{
			get
			{
				return(MSuperSex);
			}
			set
			{
				MSuperSex=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CSuperChildAddress								SuperAddress
		{
			get
			{
				return(MSuperAddress);
			}
			set
			{
				MSuperAddress=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return($"[{MSuperFirstName}] [{MSuperLastName}] [{MSuperAge}] [{MSuperSex}] [{MSuperAddress}]");
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
