using System;
using System.Collections.Generic;
using System.Text;
//------------------------------------------------------------------------
namespace MySharedResources
{
//------------------------------------------------------------------------
	// Sluzi ako resource.
	public class CMySimpleText
	{
//------------------------------------------------------------------------
		private	string						MSimpleText;
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public CMySimpleText()
		{
			MSimpleText="EMPTY";
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public	string						SimpleText
		{
			get
			{
				return(MSimpleText);
			}
			set
			{
				MSimpleText=value;
			}
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("ST: {0} !",MSimpleText));
		}
//------------------------------------------------------------------------
	}
//------------------------------------------------------------------------
}
//------------------------------------------------------------------------