#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <string>
#include <iostream>
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! DECLARATION, ale nie DEFINITION PRIMARY TEMPLATE.
template<typename... TTypes>
class CVariadicTemplate;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE SPECIALIZATION pre 0 PARAMETERS.
template<>
class CVariadicTemplate<> final
{
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE SPECIALIZATION pre 1-N PARAMETERS.
template<typename TFirst, typename... TTypes>
class CVariadicTemplate<TFirst,TTypes...> final
{
//----------------------------------------------------------------------------------------------------------------------
	// !!!!! V TEMPLATE CLASS pre 'M' PARAMETERS spravi FRIEND TEMPLATE CLASS pre 'N' PARAMETERS.
	template<typename... TLocalTypes>
	friend class CVariadicTemplate;

	private:
		TFirst													MField;
		CVariadicTemplate<TTypes...>							MFields;

	private:
		void PrintInternal(const std::wstring& Text, size_t Index) const;

	public:
		void Print(const std::wstring& Text) const;

	public:
		CVariadicTemplate(void);
		CVariadicTemplate(TFirst First, TTypes... Types);
		virtual ~CVariadicTemplate(void) noexcept;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplate<TFirst,TTypes...>::CVariadicTemplate(void)
	: MField(), MFields()
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplate<TFirst,TTypes...>::CVariadicTemplate(TFirst First, TTypes... Types)
	: MField(First), MFields(Types...)
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplate<TFirst,TTypes...>::~CVariadicTemplate(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
void CVariadicTemplate<TFirst,TTypes...>::PrintInternal(const std::wstring& Text, size_t Index) const
{
	std::wcout << Text << L" - PARAMETER [" << Index << L"] - TYPE [" << GetTypeInfoName<TFirst>() << L"] - VALUE [" << MField << L"]." << std::endl;

	if constexpr (sizeof...(TTypes)>0)
	{
		MFields.PrintInternal(Text,Index+1);
	}
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
void CVariadicTemplate<TFirst,TTypes...>::Print(const std::wstring& Text) const
{
	PrintInternal(Text,1);
}
