using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace DataAccess3
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CEntityBulkOperations
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private int												MCategory;
		private int												MValue1;
		private int												MValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityBulkOperations()
		{
			MID=default;
			MCategory=default;
			MValue1=default;
			MValue2=default;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityBulkOperations(int Category, int Value1, int Value2)
		{
			MCategory=Category;
			MValue1=Value1;
			MValue2=Value2;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityBulkOperations(int ID, int Category, int Value1, int Value2)
		{
			MID=ID;
			MCategory=Category;
			MValue1=Value1;
			MValue2=Value2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Category
		{
			get
			{
				return(MCategory);
			}
			set
			{
				MCategory=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static void PrintToConsole(CEntityBulkOperations[] Entities)
		{
			for(int Index=0;Index<Entities.Length;Index++)
			{
				CEntityBulkOperations							Entity=Entities[Index];
				string											EntityText=Entity.Print(Index+1);
							
				if (Index>0)
				{
					Console.WriteLine(new string('-',80));
				}

				Console.Write(EntityText);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string Print(int? Index)
		{
			StringBuilder										SB=new StringBuilder();

			if (Index!=null)
			{
				SB.AppendLine($"ENTITY BULK OPERATIONS [{Index}]:");
			}
			else
			{
				SB.AppendLine($"ENTITY BULK OPERATIONS:");
			}

			SB.AppendLine($"\tID [{MID}] !");
			SB.AppendLine($"\tCATEGORY [{MCategory}] !");
			SB.AppendLine($"\tVALUE 1 [{MValue1}] !");
			SB.AppendLine($"\tVALUE 2 [{MValue2}] !");

			return(SB.ToString());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------