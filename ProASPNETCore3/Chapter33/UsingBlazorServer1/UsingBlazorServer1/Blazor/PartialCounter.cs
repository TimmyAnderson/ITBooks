using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Components;
using Microsoft.AspNetCore.Components.Rendering;
using Microsoft.AspNetCore.Components.Web;
//----------------------------------------------------------------------------------------------------------------------
// !!! CODE BEHIND CLASS MUSI byt definovana v ROVNAKOM NAMESPACE ako [PartialCounter.razor].
namespace UsingBlazorServer1.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CODE BEHIND CLASS BLAZOR COMPONENT MUSI byt DERIVED z CLASS [ComponentBase].
	// !!! CODE BEHIND CLASS BLAZOR COMPONENT MUSI byt PARTIAL.
	public partial class PartialCounter : ComponentBase
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MCounter=1;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! C# PROPERTY sa mapuje na HTML ELEMENT.
		public int												Counter
		{
			get
			{
				return(MCounter);
			}
			set
			{
				MCounter=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! EVENT METHOD, ktoru pouziva HTML ELEMENT.
		public void IncrementCounter(MouseEventArgs E)
		{
			MCounter++;
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
