using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication2.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CCart
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly List<CCartLine>						MLineCollection;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCart()
		{
			MLineCollection=new List<CCartLine>();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IEnumerable<CCartLine>							Lines
		{
			get
			{
				return(MLineCollection);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void AddItem(CProduct Product, int Quantity)
		{
			CCartLine											Line=MLineCollection.Where(P => P.Product.ProductID==Product.ProductID).FirstOrDefault();

			if (Line==null)
			{
				MLineCollection.Add(new CCartLine(Product,Quantity));
			}
			else
			{
				Line.Quantity+=Quantity;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public void RemoveLine(CProduct Product)
		{
			MLineCollection.RemoveAll(P => P.Product.ProductID==Product.ProductID);
		}
//----------------------------------------------------------------------------------------------------------------------
		public decimal ComputeTotalValue()
		{
			return(MLineCollection.Sum(P => P.Product.Price*P.Quantity));
		}
//----------------------------------------------------------------------------------------------------------------------
		public void Clear()
		{
			MLineCollection.Clear();
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
