package SingletonEagerVsLazy;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.stereotype.Component;
import org.springframework.context.annotation.Lazy;
//----------------------------------------------------------------------------------------------------------------------
// !!! SPRING BEAN je vytvoreny pomocou LAZY INSTANTIATION.
@Component
// !!! ANNOTATION [@Lazy] urcuje, aby SPRING BEAN bol vytvoreny pomocou LAZY INSTANTIATION.
@Lazy
public class CSingletonEagerVsLazyBeanLazy
{
//----------------------------------------------------------------------------------------------------------------------
	public CSingletonEagerVsLazyBeanLazy()
	{
		System.out.format("BEAN [CSingletonEagerVsLazyLazy] CREATED.\n");
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public void Print()
	{
		System.out.format("HELLO BEAN [CSingletonEagerVsLazyLazy].\n");
	}	
//----------------------------------------------------------------------------------------------------------------------
}
