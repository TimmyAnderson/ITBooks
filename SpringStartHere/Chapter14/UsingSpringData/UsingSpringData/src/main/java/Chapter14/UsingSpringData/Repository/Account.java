package Chapter14.UsingSpringData.Repository;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.data.annotation.Id;
//----------------------------------------------------------------------------------------------------------------------
// !!! CLASS obsahuje RECORD z DATABASE TABLE 'Account'.
public class Account
{
//----------------------------------------------------------------------------------------------------------------------
	// !!!!! V SPRING DATA musi byt PRIMARY KEY FIELD oznaceny pomocou ANNOTATION [@Id].
	@Id
	private int													id;
	private String												name;
	private int													amount;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public int getId()
	{
    	return(id);
  	}
//----------------------------------------------------------------------------------------------------------------------
	public void setId(int id)
	{
    	this.id=id;
	}
//----------------------------------------------------------------------------------------------------------------------
	public String getName()
	{
		return(name);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setName(String name)
	{
		this.name=name;
	}
//----------------------------------------------------------------------------------------------------------------------
	public int getAmount()
	{
		return(amount);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setAmount(int amount)
	{
		this.amount=amount;
	}
}
//----------------------------------------------------------------------------------------------------------------------