using System;
using System.Collections.Generic;
using System.Text;
using Helpers;
//----------------------------------------------------------------------------------------------------------------------
namespace TipsAndTechniques
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CGroupByFunctionValue
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MNumber;
		private string											MNumberAsText;
		private int												MSum;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CGroupByFunctionValue()
		{
			MNumber=default(int);
			MNumberAsText=default(string);
			MSum=default(int);
		}
//----------------------------------------------------------------------------------------------------------------------
		public CGroupByFunctionValue(int Number, string NumberAsText, int Sum)
		{
			MNumber=Number;
			MNumberAsText=NumberAsText;
			MSum=Sum;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												Number
		{
			get
			{
				return(MNumber);
			}
			set
			{
				MNumber=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											NumberAsText
		{
			get
			{
				return(MNumberAsText);
			}
			set
			{
				MNumberAsText=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Sum
		{
			get
			{
				return(MSum);
			}
			set
			{
				MSum=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static void PrintToConsole(CGroupByFunctionValue[] Entities)
		{
			for(int Index=0;Index<Entities.Length;Index++)
			{
				CGroupByFunctionValue							Entity=Entities[Index];
				string											EntityText=Entity.Print(Index+1);
							
				if (Index>0)
				{
					CConsoleHelpers.WriteLineSeparator();
				}

				CConsoleHelpers.Write(EntityText,ConsoleColor.Green);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string Print(int? Index)
		{
			StringBuilder										SB=new StringBuilder();

			if (Index!=null)
			{
				SB.AppendLine($"GROUP BY FUNCTION VALUE [{Index}]:");
			}
			else
			{
				SB.AppendLine($"GROUP BY FUNCTION VALUE:");
			}

			SB.AppendLine($"\tNUMBER [{MNumber}] !");
			SB.AppendLine($"\tNUMBER AS TEXT [{MNumberAsText}] !");
			SB.AppendLine($"\tSUM [{MSum}] !");

			return(SB.ToString());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------