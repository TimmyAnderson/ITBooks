//-------------------------------------------------------------------------------------------------------
template<typename TType>
class CTemplateClass
{
//-------------------------------------------------------------------------------------------------------
	private:
		TType													MValue;

	public:
		TType GetValue(void)
		{
			return(MValue);
		}

		TType IncrementValue(void)
		{
			MValue++;

			return(MValue);
		}

	public:
		CTemplateClass(TType Value)
		{
			MValue=Value;
		}
//-------------------------------------------------------------------------------------------------------
};
//-------------------------------------------------------------------------------------------------------