using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Razor.TagHelpers;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingTagHelpers.TagHelpers
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! TAG HELPER sa aplikuje na HTML ELEMENT [<div>], ktory ma definovany HTML ATTRIBUTE 'UseBlue'.
	[HtmlTargetElement("div",Attributes="UseBlue")]
	public sealed class COrder1BlueTagHelper : TagHelper
	{
//----------------------------------------------------------------------------------------------------------------------
		public override int										Order
		{
			get
			{
				return(1);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public override void Process(TagHelperContext Context, TagHelperOutput Output)
		{
			Output.Attributes.SetAttribute("style","color:blue");
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------