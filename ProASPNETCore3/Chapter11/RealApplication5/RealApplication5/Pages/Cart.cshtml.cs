using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using RealApplication5.Infrastructure;
using RealApplication5.Models;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication5.Pages
{
//----------------------------------------------------------------------------------------------------------------------
    public class CartModel : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly IStoreRepository						MStoreRepository;
//----------------------------------------------------------------------------------------------------------------------
		private CCart											MCart;
		private string											MReturnUrl;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CartModel(IStoreRepository StoreRepository, CCart Cart)
		{
			MStoreRepository=StoreRepository;
			MCart=Cart;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCart											Cart
		{
			get
			{
				return(MCart);
			}
			set
			{
				MCart=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ReturnUrl
		{
			get
			{
				return(MReturnUrl);
			}
			set
			{
				MReturnUrl=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void OnGet(string ReturnUrl)
		{
			this.ReturnUrl=ReturnUrl ?? "/";
		}
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult OnPost(long ProductID, string ReturnUrl)
		{
			CProduct											Product=MStoreRepository.Products.FirstOrDefault(P => P.ProductID==ProductID);

			Cart.AddItem(Product,1);

			IActionResult										Result=RedirectToPage(new {ReturnUrl = ReturnUrl});

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult OnPostRemove(long ProductID, string ReturnUrl)
		{
			Cart.RemoveLine(Cart.Lines.First(P => P.Product.ProductID==ProductID).Product);

			IActionResult										Result=RedirectToPage(new {ReturnUrl = ReturnUrl});

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------