using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Reflection;
using System.Windows.Media;
//------------------------------------------------------------------------------
namespace ListBoxes
{
//------------------------------------------------------------------------------
	public class CNamedColor
	{
//------------------------------------------------------------------------------
        private static CNamedColor[]				MNClrs;
//------------------------------------------------------------------------------
        private Color								MClr;
        private string								MStr;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        static CNamedColor()
        {
            PropertyInfo[]		Props=typeof(Colors).GetProperties();

			// Vytvorim instancie CNamedColor.
            MNClrs=new CNamedColor[Props.Length];

            for (int i=0;i<Props.Length;i++)
                MNClrs[i]=new CNamedColor(Props[i].Name, (Color)Props[i].GetValue(null, null));
        }
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        private CNamedColor(string Str, Color Clr)
        {
            this.MStr=Str;
            this.MClr=Clr;
        }
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public static CNamedColor[]					All
        {
            get
			{
				return(MNClrs);
			}
        }
//------------------------------------------------------------------------------
        public Color								Color
        {
            get
			{
				return(MClr);
			}
        }
//------------------------------------------------------------------------------
        public string								Name
        {
            get
            {
                string		StrSpaced=MStr[0].ToString();

                for (int i=1;i<MStr.Length;i++)
                    StrSpaced+=(char.IsUpper(MStr[i]) ? " " : "") + MStr[i].ToString();

                return(StrSpaced);
            }
        }
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
        public override string ToString()
        {
            return MStr;
        }
//------------------------------------------------------------------------------
	}
//------------------------------------------------------------------------------
}
