using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingRazorPages.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingRazorPages.Pages
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS implementuje RAZOR PAGE MODEL v SEPARATNOM FILE.
    public class ShowNamesGetAndPost : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private readonly INameProvider							MNameProvider;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! Pouzije sa DEPENDENCY INJECTION na RESOLVING INTERFACE.
		public ShowNamesGetAndPost(INameProvider NameProvider)
		{
			MNameProvider=NameProvider;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName[]											Names
		{
			get
			{
				return(MNameProvider.GetNames());
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! METHOD sa vola ako reakcia na HTTP GET VERB.
        public IActionResult OnGet()
        {
			IActionResult										Result=Page();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
		// !!! METHOD sa vola ako reakcia na HTTP POST VERB.
        public IActionResult OnPost(string FirstName, string LastName, int Age)
        {
			// !!! RECORD sa prida do DB.
			MNameProvider.AddName(new CName(0,FirstName,LastName,Age));

			// !!! Spravi sa REDIRECT na CURRENT RAZOR PAGE, aby sa zobrazil zoznam NAMES.
			IActionResult										Result=RedirectToPage();

			return(Result);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
