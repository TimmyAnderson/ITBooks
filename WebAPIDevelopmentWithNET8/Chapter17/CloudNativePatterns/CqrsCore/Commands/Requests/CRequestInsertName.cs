using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CRequestInsertName : IRequest<CDtoFullName>
	{
//----------------------------------------------------------------------------------------------------------------------
		private CDtoNameToInsert								MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CRequestInsertName()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CRequestInsertName(CDtoNameToInsert Name)
		{
			MName=Name;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CDtoNameToInsert									Name
		{
			get
			{
				return(MName);
			}
			set
			{
				MName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------