using Microsoft.AspNetCore.Mvc;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace ModelBinding.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CMultiSourceData
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MIDFromRouteVariable;
		private string											MNameFromQueryString;
		private string											MAcceptHeaderFromHeader;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CMultiSourceData()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY bude MAPPED z ROUTE VARIABLE.
		[FromRoute(Name="ID")]
		public int												IDFromRouteVariable
		{
			get
			{
				return(MIDFromRouteVariable);
			}
			set
			{
				MIDFromRouteVariable=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY bude MAPPED z QUERY STRING VARIABLE.
		[FromQuery(Name="Name")]
		public string											NameFromQueryString
		{
			get
			{
				return(MNameFromQueryString);
			}
			set
			{
				MNameFromQueryString=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY bude MAPPED z REQUEST FORM HEADER.
		[FromHeader(Name="Accept")]
		public string											AcceptHeaderFromHeader
		{
			get
			{
				return(MAcceptHeaderFromHeader);
			}
			set
			{
				MAcceptHeaderFromHeader=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return($"[{MIDFromRouteVariable}] [{MNameFromQueryString}] [{MAcceptHeaderFromHeader}]");
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------