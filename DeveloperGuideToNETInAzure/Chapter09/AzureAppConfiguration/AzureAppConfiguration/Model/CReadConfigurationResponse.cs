using Newtonsoft.Json;
//----------------------------------------------------------------------------------------------------------------------
namespace AzureAppConfiguration
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CReadConfigurationResponse
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MValue1;
		private int												MValue2;
		private string											MComplexConfigurationValue1;
		private int												MComplexConfigurationValue2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CReadConfigurationResponse()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CReadConfigurationResponse(string Value1, int Value2, string ComplexConfigurationValue1, int ComplexConfigurationValue2)
		{
			MValue1=Value1;
			MValue2=Value2;
			MComplexConfigurationValue1=ComplexConfigurationValue1;
			MComplexConfigurationValue2=ComplexConfigurationValue2;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[JsonProperty("value1")]
		public string											Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("value2")]
		public int												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("complexConfigurationValue1")]
		public string											ComplexConfigurationValue1
		{
			get
			{
				return(MComplexConfigurationValue1);
			}
			set
			{
				MComplexConfigurationValue1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("complexConfigurationValue2")]
		public int												ComplexConfigurationValue2
		{
			get
			{
				return(MComplexConfigurationValue2);
			}
			set
			{
				MComplexConfigurationValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------