using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CodeGeneration
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CNonterminal : CSymbol
	{
//-------------------------------------------------------------------------------------------------------
		private readonly List<CChoice>							MChoices;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CNonterminal(string Name)
			: base(Name)
		{
			MChoices=new List<CChoice>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CChoice[]										Choices
		{
			get
			{
				return(MChoices.ToArray());
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void AddChoice(CChoice Choice)
		{
			MChoices.Add(Choice);
		}
//-------------------------------------------------------------------------------------------------------
		public CChoice GetChoiceWithSameSymbols(CSymbol[] Symbols)
		{
			foreach(CChoice Choice in MChoices)
			{
				if (Choice.ContainsSymbols(Symbols)==true)
				{
					return(Choice);
				}
			}

			return(null);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
