//----------------------------------------------------------------------------------------------------------------------
#include "KernelHeaders.h"
#include "CExecutiveResource.h"
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
namespace KTL
{
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::Init(void)
	{
		ExInitializeResourceLite(&MResource);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::Delete(void)
	{
		ExDeleteResourceLite(&MResource);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::Lock(void)
	{
		ExEnterCriticalRegionAndAcquireResourceExclusive(&MResource);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::Unlock(void)
	{
		ExReleaseResourceAndLeaveCriticalRegion(&MResource);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::LockShared(void)
	{
		ExEnterCriticalRegionAndAcquireResourceShared(&MResource);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CExecutiveResource::UnlockShared(void)
	{
		Unlock();
	}
//----------------------------------------------------------------------------------------------------------------------
}
