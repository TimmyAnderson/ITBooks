using System;
using System.Collections.Generic;
using System.Text;
using System.IO;
using System.Runtime.Serialization;
using System.ServiceModel;
using System.ServiceModel.Channels;
//-------------------------------------------------------------------------------------------------------
namespace MessageContractLibrary
{
//-------------------------------------------------------------------------------------------------------
	[MessageContract]
	public class CMyMessageContract
	{
//-------------------------------------------------------------------------------------------------------
		private int					MMCIntValue;
		private CMyDataContract		MMCMyDataContract;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CMyMessageContract()
		{
		}
//-------------------------------------------------------------------------------------------------------
		public CMyMessageContract(int MCIntValue, CMyDataContract MCMyDataContract)
		{
			MMCIntValue=MCIntValue;
			MMCMyDataContract=MCMyDataContract;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	    [MessageHeader]
		public int					MCIntValue
		{
			get
			{
				return(MMCIntValue);
			}
			set
			{
				MMCIntValue=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
	    [MessageBodyMember]
		public CMyDataContract		MCMyDataContract
		{
			get
			{
				return(MMCMyDataContract);
			}
			set
			{
				MMCMyDataContract=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("MCIntValue: {0}, MCMyDataContract: {1}",MMCIntValue,MMCMyDataContract.ToString()));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------