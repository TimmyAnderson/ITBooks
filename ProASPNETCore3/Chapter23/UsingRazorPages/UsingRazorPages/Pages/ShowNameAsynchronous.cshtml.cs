using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingRazorPages.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingRazorPages.Pages
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS implementuje RAZOR PAGE MODEL.
    public class ShowNameAsynchronous : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private CName											MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName											Name
		{
			get
			{
				return(MName);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! METHOD sa vola ako reakcia na HTTP GET VERB.
		// !!!!! METHOD je ASYNCHRONOUS verzia METHOD [void OnGet()].
        public Task OnGetAsync()
        {
			MName=new CName(0,"Timmy","Anderson",12);

			return(Task.CompletedTask);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
