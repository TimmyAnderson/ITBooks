using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace CSharpEssentialFeatures
{
//----------------------------------------------------------------------------------------------------------------------
	class CIndexerCollection
	{
//----------------------------------------------------------------------------------------------------------------------
		private Dictionary<string,CClass2>						MCollection;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CIndexerCollection()
		{
			MCollection=new Dictionary<string,CClass2>();
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CClass2											this[string Value]
		{
			get
			{
				return(MCollection[Value]);
			}
			set
			{
				Console.WriteLine("INDEXER SET METHOD CALLED !");

				MCollection[Value]=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
