using System;
using System.Collections.Generic;
using System.Text;
using ConcurrencyModesLibrary;
//-------------------------------------------------------------------------------------------------------
namespace ConcurrencyModesClient
{
//-------------------------------------------------------------------------------------------------------
	public class CConcurrencyModesCatcher : IConcurrencyModesCallback
	{
//-------------------------------------------------------------------------------------------------------
		private string											MClientName;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CConcurrencyModesCatcher(string ClientName)
		{
			MClientName=ClientName;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void OnCallback(string Message)
		{
			Console.WriteLine(string.Format("PROXY: {0} FROM SERVER: {1} !",MClientName,Message));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
