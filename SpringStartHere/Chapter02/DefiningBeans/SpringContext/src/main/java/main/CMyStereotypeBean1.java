package main;
//----------------------------------------------------------------------------------------------------------------------
import org.springframework.stereotype.Component;
//----------------------------------------------------------------------------------------------------------------------
// !!!!! ANNOTATION [@Component] urcuje, ze instancia CLASS [CMyStereotypeBean1] bude vytvorena ako SPRING BEAN pomocou STEREOTYPES.
@Component
class CMyStereotypeBean1
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getValue()
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setValue(String Value)
	{
		MValue=Value;
	}
//----------------------------------------------------------------------------------------------------------------------
}
