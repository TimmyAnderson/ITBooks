//-------------------------------------------------------------------------------------------------------
#include "IntImplicit.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
CIntImplicit::CIntImplicit(void)
	: MValue(0)
{
	wprintf(L"DEFAULT CONSTRUCTOR !\n");
}
//-------------------------------------------------------------------------------------------------------
CIntImplicit::CIntImplicit(int Value)
	: MValue(Value)
{
	wprintf(L"CONSTRUCTOR [%d] !\n",MValue);
}
//-------------------------------------------------------------------------------------------------------
CIntImplicit::CIntImplicit(const CIntImplicit& Value)
	: MValue(Value.MValue)
{
	wprintf(L"COPY CONSTRUCTOR [%d] !\n",MValue);
}
//-------------------------------------------------------------------------------------------------------
CIntImplicit::~CIntImplicit(void)
{
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
void CIntImplicit::Print(void)
{
	wprintf_s(L"Value: [%d] !\n",MValue);
}
