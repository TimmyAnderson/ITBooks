using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class COneToManyPrincipal
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly int									MPrincipalID;
		private readonly string									MPrincipalValue;
		private readonly IList<COneToManyDependent>				MDependents;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public COneToManyPrincipal(int PrincipalID, string PrincipalValue)
		{
			MPrincipalID=PrincipalID;
			MPrincipalValue=PrincipalValue;
		}
//----------------------------------------------------------------------------------------------------------------------
		public COneToManyPrincipal(int PrincipalID, string PrincipalValue, IList<COneToManyDependent> Dependents)
		{
			MPrincipalID=PrincipalID;
			MPrincipalValue=PrincipalValue;
			MDependents=Dependents;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												PrincipalID
		{
			get
			{
				return(MPrincipalID);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											PrincipalValue
		{
			get
			{
				return(MPrincipalValue);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public IList<COneToManyDependent>						Dependents
		{
			get
			{
				return(MDependents);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------