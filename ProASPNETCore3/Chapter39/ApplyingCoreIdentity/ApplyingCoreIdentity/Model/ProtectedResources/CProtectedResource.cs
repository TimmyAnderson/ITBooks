using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Model
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS reprezentuje PROTECTED RESOURCE. PROTECTED RESOURCE je RESOURCE, ktory pocas procesu AUTHORIZATION MUSI byt verifikovany v CODE ci je k nemu prihlaseny USER AUTHORIZED, alebo nie.
	public sealed class CProtectedResource
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MOwner;
		private readonly string									MResourceData;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CProtectedResource(string Owner, string ResourceData)
		{
			MOwner=Owner;
			MResourceData=ResourceData;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Owner
		{
			get
			{
				return(MOwner);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ResourceData
		{
			get
			{
				return(MResourceData);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------