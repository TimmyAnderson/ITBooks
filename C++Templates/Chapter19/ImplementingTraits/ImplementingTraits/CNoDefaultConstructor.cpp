#include "CNoDefaultConstructor.h"
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CNoDefaultConstructor::CNoDefaultConstructor(int Value)
	: MValue(Value)
{
}
//----------------------------------------------------------------------------------------------------------------------
CNoDefaultConstructor::~CNoDefaultConstructor(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
int CNoDefaultConstructor::GetValue(void) const noexcept
{
	return(MValue);
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CNoDefaultConstructor operator+(const CNoDefaultConstructor& Value1, int Value2)
{
	CNoDefaultConstructor										Result(Value1.GetValue()+Value2);

	return(Result);
}
