using System;
using System.Collections.Generic;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ManualWCFLibrary
{
//-------------------------------------------------------------------------------------------------------
	public class CHelloWorldService : IHelloWorldContract, IThrowExceptionContract
	{
//-------------------------------------------------------------------------------------------------------
		public CHelloWorldService()
		{
			Console.WriteLine("CHelloWorldService CONSTRUCTOR called !");
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void SayHelloWorld(string Name)
		{
			Console.WriteLine(String.Format("{0} says Hello World !",Name));
		}
//-------------------------------------------------------------------------------------------------------
		public void ThrowException()
		{
			throw(new Exception("Sorry but I FAILED !!!"));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
