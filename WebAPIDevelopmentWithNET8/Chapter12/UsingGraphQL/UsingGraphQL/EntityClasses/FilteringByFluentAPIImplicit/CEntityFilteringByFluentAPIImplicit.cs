using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
    public sealed class CEntityFilteringByFluentAPIImplicit
    {
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private string											MStringValue;
		private int												MIntValue;
		private long											MLongValue;
		private double											MDoubleValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityFilteringByFluentAPIImplicit()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityFilteringByFluentAPIImplicit(int ID, string StringValue, int IntValue, long LongValue, double DoubleValue)
		{
			MID=ID;
			MStringValue=StringValue;
			MIntValue=IntValue;
			MLongValue=LongValue;
			MDoubleValue=DoubleValue;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											StringValue
		{
			get
			{
				return(MStringValue);
			}
			set
			{
				MStringValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												IntValue
		{
			get
			{
				return(MIntValue);
			}
			set
			{
				MIntValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public long												LongValue
		{
			get
			{
				return(MLongValue);
			}
			set
			{
				MLongValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public double											DoubleValue
		{
			get
			{
				return(MDoubleValue);
			}
			set
			{
				MDoubleValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
