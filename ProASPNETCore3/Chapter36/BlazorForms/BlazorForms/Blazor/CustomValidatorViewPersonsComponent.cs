using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Components;
using Microsoft.AspNetCore.Components.Web;
using BlazorForms.Services;
using BlazorForms.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace BlazorForms.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
    public partial class CustomValidatorViewPersonsComponent : ComponentBase
    {
//----------------------------------------------------------------------------------------------------------------------
		private IDBOperationsInMemory							MDBOperations;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Inject]
		public IDBOperationsInMemory							DBOperations
		{
			get
			{
				return(MDBOperations);
			}
			set
			{
				MDBOperations=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CPerson[]										Persons
		{
			get
			{
				CPerson[]										Value=MDBOperations.GetPersons();

				return(Value);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string GetEditPersonLink(CPerson Person)
		{
			string												Link=$"/CustomValidatorEditPerson/{Person.ID}";

			return(Link);
		}
//----------------------------------------------------------------------------------------------------------------------
    }
