using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.CompilerServices;
//-------------------------------------------------------------------------------------------------------
namespace ParameterfulProperties
{
//-------------------------------------------------------------------------------------------------------
	public class CNames
	{
//-------------------------------------------------------------------------------------------------------
		private List<CName>										MNames;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CNames()
		{
			MNames=new List<CName>();
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[IndexerName("MySuperIndexer")]
		public CName											this[int Index]
		{
			get
			{
				return(MNames[Index]);
			}
		}
//-------------------------------------------------------------------------------------------------------
		// INDEXERS podporuju aj VIACERO PARAMETERS.
		[IndexerName("MySuperIndexer")]
		public CName											this[string FirstName, string LastName, int Age]
		{
			get
			{
				return(MNames.Where(P => P.FirstName==FirstName && P.LastName==LastName && P.Age==Age).FirstOrDefault());
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void Add(CName Name)
		{
			MNames.Add(Name);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------