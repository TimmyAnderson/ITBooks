using System.Threading;
using System.Threading.Tasks;
using MediatR;
//----------------------------------------------------------------------------------------------------------------------
namespace CqrsCore
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CHandlerGetNameByID : IRequestHandler<CRequestGetNameByID,CDtoFullName>
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly INameRepository						MNameRepository;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CHandlerGetNameByID(INameRepository NameRepository)
		{
			MNameRepository=NameRepository;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public async Task<CDtoFullName> Handle(CRequestGetNameByID Request, CancellationToken CancellationToken)
		{
			CDtoFullName										FullName=await MNameRepository.GetName(Request.ID);

			return(FullName);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
