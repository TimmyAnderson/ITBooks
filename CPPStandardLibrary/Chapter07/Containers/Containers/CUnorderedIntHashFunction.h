#pragma once
//-------------------------------------------------------------------------------------------------------
#include "CUnorderedInt.h"
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
class CUnorderedIntHashFunction
{
//-------------------------------------------------------------------------------------------------------
	private:
		int														MValue;

	public:
		std::size_t operator()(const CUnorderedInt& Value) const
		{
			std::size_t											Hash=static_cast<std::size_t>(Value.GetValue());

			return(Hash);
		}

	public:
		int GetValue(void) const noexcept
		{
			return(MValue);
		}

	public:
		CUnorderedIntHashFunction(int Value)
			: MValue(Value)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
//-------------------------------------------------------------------------------------------------------