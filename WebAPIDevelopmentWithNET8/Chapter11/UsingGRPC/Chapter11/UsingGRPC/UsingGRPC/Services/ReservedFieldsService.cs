using System.Threading.Tasks;
using Grpc.Core;
using Microsoft.Extensions.Logging;
using UsingGRPC;
//--------------------------------------------------------------------------------------------------------------------------------
namespace UsingGRPCNames.Services
{
//--------------------------------------------------------------------------------------------------------------------------------
	public class ReservedFieldsService : ReservedFields.ReservedFieldsBase
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<ReservedFieldsService>			MLogger;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public ReservedFieldsService(ILogger<ReservedFieldsService> Logger)
		{
			MLogger=Logger;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public override Task<ReservedFieldsResponse> ProcessReservedFields(ReservedFieldsRequest Request, ServerCallContext Context)
		{
			ReservedFieldsResponse								ResponseContent=new ReservedFieldsResponse();

			ResponseContent.Field1=$"METHOD 1 - {Request.Field1} {Request.Field2}";
			ResponseContent.Field2+=1000;

			Task<ReservedFieldsResponse>						ResponseTask=Task.FromResult(ResponseContent);

			return(ResponseTask);
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
