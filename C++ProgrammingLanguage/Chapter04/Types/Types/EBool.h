//-------------------------------------------------------------------------------------------------------
#include <iostream>
//-------------------------------------------------------------------------------------------------------
enum EBool
{
	E_B_FALSE=0,
	E_B_TRUE=1,
};
//-------------------------------------------------------------------------------------------------------
EBool& operator++(EBool& Param)
{
	std::wcout << L"OPERATOR ++ CALLED !\n";

	if (Param==E_B_TRUE)
	{
		Param=E_B_FALSE;
	}
	else
	{
		Param=E_B_TRUE;
	}

	return(Param);
}
