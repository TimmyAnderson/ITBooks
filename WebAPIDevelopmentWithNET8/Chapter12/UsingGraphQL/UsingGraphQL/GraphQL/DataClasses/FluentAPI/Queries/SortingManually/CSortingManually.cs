using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingGraphQL
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CSortingManually
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly int									MID;
		private readonly string									MValueString;
		private readonly int									MValueInt;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CSortingManually(int ID, string ValueString, int ValueInt)
		{
			MID=ID;
			MValueString=ValueString;
			MValueInt=ValueInt;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ValueString
		{
			get
			{
				return(MValueString);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												ValueInt
		{
			get
			{
				return(MValueInt);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------