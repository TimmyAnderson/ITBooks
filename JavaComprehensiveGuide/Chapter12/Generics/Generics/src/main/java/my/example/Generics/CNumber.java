package my.example.Generics;
//----------------------------------------------------------------------------------------------------------------------
public abstract class CNumber
{
//----------------------------------------------------------------------------------------------------------------------
	public abstract Object GetRawValue();
	public abstract void SetRawValue(Object value);
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	@Override
	public String toString()
	{
		Object													rawValue=GetRawValue();
		String													text=String.format("%s",rawValue);

		return(text);
	}
//----------------------------------------------------------------------------------------------------------------------
}
