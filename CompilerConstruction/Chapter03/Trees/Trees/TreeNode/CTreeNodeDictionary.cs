using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Trees
{
//-------------------------------------------------------------------------------------------------------
	public sealed class CTreeNodeDictionary
	{
//-------------------------------------------------------------------------------------------------------
		private Dictionary<CTreeNodeID,CTreeNode>				MNodes=new Dictionary<CTreeNodeID,CTreeNode>();
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeDictionary()
		{
		}
//-------------------------------------------------------------------------------------------------------
		public CTreeNodeDictionary(Dictionary<CTreeNodeID,CTreeNode> Nodes)
		{
			MNodes=Nodes;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CTreeNode										this[CTreeNodeID NodeID]
		{
			get
			{
				if (MNodes.ContainsKey(NodeID)==true)
					return(MNodes[NodeID]);
				else
					return(null);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void Add(CTreeNodeID TreeNodeID, CTreeNode TreeNode)
		{
			MNodes.Add(TreeNodeID,TreeNode);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------