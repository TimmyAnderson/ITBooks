using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using UsingRazorPages.Model;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingRazorPages.Pages
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS implementuje RAZOR PAGE MODEL.
    public class ShowNameUsingRoutedParameters : PageModel
    {
//----------------------------------------------------------------------------------------------------------------------
		private CName											MName;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CName											Name
		{
			get
			{
				return(MName);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! METHOD sa vola ako reakcia na HTTP GET VERB.
		// !!! Vdaka CUSTOM ROUTING ma METHOD PARAMETERS mapovane na SEGMENTS zadanej URL.
        public void OnGet(int ID, string FirstName, string LastName, int Age)
        {
			MName=new CName(ID,FirstName,LastName,Age);
        }
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
