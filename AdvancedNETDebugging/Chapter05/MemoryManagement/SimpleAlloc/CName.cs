using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace SimpleAlloc
{
//-------------------------------------------------------------------------------------------------------
	public class CName
	{
//-------------------------------------------------------------------------------------------------------
		private string											MFirst;
        private string											MLast;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        public CName(string F, string L)
        {
            MFirst=F;
			MLast=L;
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        public string											First
		{
			get
			{
				return(MFirst); 
			}
		}
//------------------------------------------------------------------------------------------------------
        public string											Last
		{
			get
			{
				return(MLast);
			}
		}
//------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
