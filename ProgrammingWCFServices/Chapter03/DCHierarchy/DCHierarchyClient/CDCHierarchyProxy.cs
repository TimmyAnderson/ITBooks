using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using DCHierarchyLibrary;
//-------------------------------------------------------------------------------------------------------
namespace DCHierarchyClient
{
//-------------------------------------------------------------------------------------------------------
	public class CDCHierarchyProxy : ClientBase<IDCHierarchyContract>, IDCHierarchyContract
	{
//-------------------------------------------------------------------------------------------------------
		public CDCHierarchyProxy(Binding ProxyBinding, string EndpointAddress)
			: base(ProxyBinding, new EndpointAddress(EndpointAddress))
		{
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CFullName GetName()
		{
 			return(Channel.GetName());
		}
//-------------------------------------------------------------------------------------------------------
		public void SendFullName(CFullName Name)
		{
			Channel.SendFullName(Name);
		}
//-------------------------------------------------------------------------------------------------------
		public void SendName(CName Name)
		{
			Channel.SendName(Name);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
