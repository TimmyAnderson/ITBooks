using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace ExceptionSample
{
//-------------------------------------------------------------------------------------------------------
	class Program
	{
//-------------------------------------------------------------------------------------------------------
        private void Func(int A)
        {
            if(A==0)
                throw(new ArgumentException("Some EXCEPTION !"));
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        static void Main(string[] args)
        {
            Program			Sample=new Program();

            try
            {
                Sample.Func(0);
            }
            catch (ArgumentException E)
            {
                Console.WriteLine("Exception encountered [{0}] !", E);
            }
        }
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
