//----------------------------------------------------------------------------------------------------------------------
#include "CValue.h"
#include <iostream>
//----------------------------------------------------------------------------------------------------------------------
using namespace std;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
CValue::CValue(const wstring& Value)
	: MValue(Value)
{
	wcout << L"CONSTRUCTOR [" << MValue << L"] CALLED." << endl;
}
//----------------------------------------------------------------------------------------------------------------------
CValue::CValue(const CValue& Value)
	: MValue(Value.GetValue())
{
	wcout << L"COPY CONSTRUCTOR [" << MValue << L"] CALLED." << endl;
}
//----------------------------------------------------------------------------------------------------------------------
CValue::~CValue(void) noexcept
{
	wcout << L"DESTRUCTOR [" << MValue << L"] CALLED." << endl;
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
const wstring& CValue::GetValue(void) const noexcept
{
	return(MValue);
}
