enum EEnumOperators : int
{
//-----------------------------------------------------------------------------
	E_10=10,
	E_20=20,
	E_30=30,
//-----------------------------------------------------------------------------
};
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
enum class EEnumClassOperators : int
{
//-----------------------------------------------------------------------------
	E_10=10,
	E_20=20,
	E_30=30,
//-----------------------------------------------------------------------------
};
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
inline EEnumOperators operator+(EEnumOperators Left, EEnumOperators Right)
{
	std::wcout << "NON-MEMBER BINARY OPERATOR+ CALLED !" << std::endl;

	return(EEnumOperators(int(Left)+int(Right)));
}
//-----------------------------------------------------------------------------
inline EEnumClassOperators operator+(EEnumClassOperators Left, EEnumClassOperators Right)
{
	std::wcout << "NON-MEMBER BINARY OPERATOR+ CALLED !" << std::endl;

	return(EEnumClassOperators(int(Left)+int(Right)));
}
