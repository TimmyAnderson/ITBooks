class CCustomComparer
{
//-------------------------------------------------------------------------------------------------------
	private:
		bool													MLess;

	public:
		bool operator()(int Value1, int Value2) const
		{
			if (MLess==true)
			{
				if (Value1<Value2)
				{
					return(true);
				}
				else
				{
					return(false);
				}
			}
			else
			{
				if (Value1>Value2)
				{
					return(true);
				}
				else
				{
					return(false);
				}
			}
		}

	public:
		CCustomComparer(bool Less)
			: MLess(Less)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
