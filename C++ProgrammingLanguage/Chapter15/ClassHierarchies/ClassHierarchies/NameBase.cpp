wstring CNameBase::GetFullName(void)
{
	wstring														Result=L"";
	wchar_t														Buffer[100];

	swprintf_s(Buffer,L"%d",MAge);

	Result+=L"Name: [";
	Result+=MFirstName;
	Result+=L" ";
	Result+=MLastName;
	Result+=L"], Age: [";
	Result+=Buffer;
	Result+=L"]";

	return(Result);
}
