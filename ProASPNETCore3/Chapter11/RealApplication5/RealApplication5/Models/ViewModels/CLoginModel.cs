using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace RealApplication5.Models.ViewModels
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CLoginModel
	{
//----------------------------------------------------------------------------------------------------------------------
		private string											MName;
		private string											MPassword;
		private string											MReturnUrl;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CLoginModel()
		{
			MReturnUrl="/Page1";
		}
//----------------------------------------------------------------------------------------------------------------------
		public CLoginModel(string ReturnUrl)
		{
			MReturnUrl=ReturnUrl;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Name
		{
			get
			{
				return(MName);
			}
			set
			{
				MName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Required]
		public string											Password
		{
			get
			{
				return(MPassword);
			}
			set
			{
				MPassword=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ReturnUrl
		{
			get
			{
				return(MReturnUrl);
			}
			set
			{
				MReturnUrl=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------