var																LogContent="";
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
function AddLogRecord(LogRecord)
{
	if (LogContent!="")
	{
		LogContent+="<br/>";
	}

	LogContent+=LogRecord;
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
window.onload=function()
{
	var															LOG=window.document.getElementById("LOG");

	LOG.innerHTML=LogContent;
}
