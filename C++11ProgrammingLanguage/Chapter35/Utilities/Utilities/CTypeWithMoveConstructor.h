#include <string>
#include <functional>
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
class CTypeWithMoveConstructor
{
//-------------------------------------------------------------------------------------------------------
	private:
		wstring													MValue;

	public:
		const wstring& GetValue(void) noexcept
		{
			return(MValue);
		}

	public:
		CTypeWithMoveConstructor(const wstring& Value)
			: MValue(Value)
		{
		}

		CTypeWithMoveConstructor(CTypeWithMoveConstructor&& Other)
			: MValue(std::move(Other.MValue))
		{
			wcout << L"CTypeWithMoveConstructor MOVE CONSTRUCTOR USED !\n";
		}
//-------------------------------------------------------------------------------------------------------
};
