using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Net;
//--------------------------------------------------------------------------------------------------------------------------------
namespace WebServiceWithControllers
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CDependencyInjection
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private int												MMyTransient1;
		private int												MMyTransient2;
		private int												MMyScoped1;
		private int												MMyScoped2;
		private int												MMySingleton1;
		private int												MMySingleton2;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CDependencyInjection()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CDependencyInjection(int MyTransient1, int MyTransient2, int MyScoped1, int MyScoped2, int MySingleton1, int MySingleton2)
		{
			MMyTransient1=MyTransient1;
			MMyTransient2=MyTransient2;
			MMyScoped1=MyScoped1;
			MMyScoped2=MyScoped2;
			MMySingleton1=MySingleton1;
			MMySingleton2=MySingleton2;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MyTransient1
		{
			get
			{
				return(MMyTransient1);
			}
			set
			{
				MMyTransient1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MyTransient2
		{
			get
			{
				return(MMyTransient2);
			}
			set
			{
				MMyTransient2=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MyScoped1
		{
			get
			{
				return(MMyScoped1);
			}
			set
			{
				MMyScoped1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MyScoped2
		{
			get
			{
				return(MMyScoped2);
			}
			set
			{
				MMyScoped2=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MySingleton1
		{
			get
			{
				return(MMySingleton1);
			}
			set
			{
				MMySingleton1=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public int												MySingleton2
		{
			get
			{
				return(MMySingleton2);
			}
			set
			{
				MMySingleton2=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
