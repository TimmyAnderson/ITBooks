#include "KernelHeaders.h"
#include "SSpinLock.h"
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
namespace KTL
{
//----------------------------------------------------------------------------------------------------------------------
	void SSpinLock::Init(void)
	{
		KeInitializeSpinLock(&MLock);
	}
//----------------------------------------------------------------------------------------------------------------------
	void SSpinLock::Lock(void)
	{
		KeAcquireSpinLock(&MLock,&MOldIrql);
	}
//----------------------------------------------------------------------------------------------------------------------
	void SSpinLock::Unlock(void)
	{
		KeReleaseSpinLock(&MLock,MOldIrql);
	}
//----------------------------------------------------------------------------------------------------------------------
}
