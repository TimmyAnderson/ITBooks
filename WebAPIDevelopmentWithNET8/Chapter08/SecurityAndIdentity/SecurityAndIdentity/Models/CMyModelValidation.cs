using System.ComponentModel.DataAnnotations;
//--------------------------------------------------------------------------------------------------------------------------------
namespace SecurityAndIdentity
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CMyModelValidation
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyModelValidation()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyModelValidation(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="FirstName must be set.")]
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
			set
			{
				MFirstName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="LastName must be set.")]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			set
			{
				MLastName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[Required(ErrorMessage="Age must be set.")]
		// !!! CUSTOM VALIDATION.
		[CCheckAge]
		public int												Age
		{
			get
			{
				return(MAge);
			}
			set
			{
				MAge=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------------------------------------------------------