template<typename TType>
class CCustomEqualFunction
{
//-------------------------------------------------------------------------------------------------------
	private:
		int														MValue;

	public:
		bool operator()(TType Value1, TType Value2) const
		{
			if (Value1==Value2)
			{
				return(true);
			}
			else
			{
				return(false);
			}
		}

	public:
		int GetValue(void) const noexcept
		{
			return(MValue);
		}

	public:
		CCustomEqualFunction(int Value)
			: MValue(Value)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
//-------------------------------------------------------------------------------------------------------