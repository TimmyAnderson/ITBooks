using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace CSharpEssentialFeatures
{
//----------------------------------------------------------------------------------------------------------------------
	sealed class CClass1
	{
//----------------------------------------------------------------------------------------------------------------------
		private	string											MValue1;
		private	int?											MValue2;
		private	CClass2											MValue3;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CClass1()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CClass1(string Value1, int? Value2, CClass2 Value3)
		{
			MValue1=Value1;
			MValue2=Value2;
			MValue3=Value3;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Value1
		{
			get
			{
				return(MValue1);
			}
			set
			{
				MValue1=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int?												Value2
		{
			get
			{
				return(MValue2);
			}
			set
			{
				MValue2=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CClass2											Value3
		{
			get
			{
				return(MValue3);
			}
			set
			{
				MValue3=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string GetValue1()
		{
			return(MValue1);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
