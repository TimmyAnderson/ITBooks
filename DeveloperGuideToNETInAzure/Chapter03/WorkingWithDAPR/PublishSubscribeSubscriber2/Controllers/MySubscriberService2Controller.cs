using System;
using System.Collections.Generic;
using System.Linq;
using System.Net.Http;
using System.Threading.Tasks;
using Dapr;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
//----------------------------------------------------------------------------------------------------------------------
namespace PublishSubscribeSubscriber2
{
//----------------------------------------------------------------------------------------------------------------------
	[ApiController]
	[Route("[controller]")]
	public class MySubscriberService2Controller : ControllerBase
	{
//----------------------------------------------------------------------------------------------------------------------
		private const string									SERVICE_NAME="pubsub";
		private const string									TOPIC_NAME="MyTopic";
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		private readonly ILogger<MySubscriberService2Controller>	MLogger;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public MySubscriberService2Controller(ILogger<MySubscriberService2Controller> Logger)
		{
			MLogger=Logger;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Topic(SERVICE_NAME,TOPIC_NAME)]
		[HttpPost]
		public string CallService(CMessage Message)
		{
			string												Value=$"VALUE 1 [{Message.Value1}] VALUE 2 [{Message.Value2}].";

			MLogger.LogInformation($"SUBSCRIBER SERVICE 2 CALLED with VALUE [{Value}] !");

			return(Value);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
