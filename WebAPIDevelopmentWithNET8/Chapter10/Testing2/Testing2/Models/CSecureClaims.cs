using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace Testing2
{
//----------------------------------------------------------------------------------------------------------------------
    public sealed class CSecureClaims
    {
//----------------------------------------------------------------------------------------------------------------------
		private string											MAuthenticationType;
		private string											MUserName;
		private CSecureClaim[]									MClaims;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CSecureClaims()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CSecureClaims(string AuthenticationType, string UserName, CSecureClaim[] Claims)
		{
			MAuthenticationType=AuthenticationType;
			MUserName=UserName;
			MClaims=Claims;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											AuthenticationType
		{
			get
			{
				return(MAuthenticationType);
			}
			set
			{
				MAuthenticationType=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											UserName
		{
			get
			{
				return(MUserName);
			}
			set
			{
				MUserName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CSecureClaim[]									Claims
		{
			get
			{
				return(MClaims);
			}
			set
			{
				MClaims=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
    }
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------