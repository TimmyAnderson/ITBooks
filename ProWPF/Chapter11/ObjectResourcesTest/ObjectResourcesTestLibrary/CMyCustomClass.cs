using System;
using System.Collections.Generic;
using System.Text;
//------------------------------------------------------------------------
namespace ObjectResourcesTestLibrary
{
//------------------------------------------------------------------------
	public class CMyCustomClass
	{
//------------------------------------------------------------------------
		private string							MSomeValue;
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		// Musi mat empty konstruktor kvoli XAML.
		public CMyCustomClass()
		{
			MSomeValue="EMPTY";
		}
//------------------------------------------------------------------------
		public CMyCustomClass(string SomeValue)
		{
			MSomeValue=SomeValue;
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public string							SomeValue
		{
			get
			{
				return(MSomeValue);
			}
			set
			{
				MSomeValue=value;
			}
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("SV: {0}",MSomeValue));
		}
//------------------------------------------------------------------------
	}
//------------------------------------------------------------------------
}
