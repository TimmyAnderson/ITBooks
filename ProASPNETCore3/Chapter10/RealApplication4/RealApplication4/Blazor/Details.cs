namespace RealApplication4.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
	public partial class Details : ComponentBase
	{
//----------------------------------------------------------------------------------------------------------------------
		private IStoreRepository								MRepository;
		private long											MID;
		private CProduct										MProduct;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		[Inject]
		public IStoreRepository									Repository
		{
			get
			{
				return(MRepository);
			}
			set
			{
				MRepository=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		[Parameter]
		public long												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public CProduct											Product
		{
			get
			{
				return(MProduct);
			}
			set
			{
				MProduct=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											EditUrl
		{
			get
			{
				string											URL=$"/Admin/Products/Edit/{MProduct.ProductID}";

				return(URL);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		protected override void OnParametersSet()
		{
			MProduct=MRepository.Products.FirstOrDefault(P => P.ProductID==MID);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
