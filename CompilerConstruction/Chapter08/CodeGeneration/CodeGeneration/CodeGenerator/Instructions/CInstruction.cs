using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace CodeGeneration
{
//-------------------------------------------------------------------------------------------------------
	public abstract class CInstruction
	{
//-------------------------------------------------------------------------------------------------------
		private readonly static int							COMMENT_POSITION=50;
//-------------------------------------------------------------------------------------------------------
		protected readonly string							MComment;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CInstruction(string Comment)
		{
			MComment=Comment;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											Comment
		{
			get
			{
				return(MComment);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		protected string AddComment(string OriginalInstruction, string Comment)
		{
			int												PadSize=Math.Max(COMMENT_POSITION-OriginalInstruction.Length,0);
			string											CommentedInstruction=string.Format("{0}{1}; {2}",OriginalInstruction,"".PadLeft(PadSize,' '),Comment);

			return(CommentedInstruction);
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public abstract string GetInstructionString(bool PrintComments);
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
