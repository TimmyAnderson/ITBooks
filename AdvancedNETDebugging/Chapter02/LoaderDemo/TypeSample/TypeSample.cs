using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace TypeSample
{
//-------------------------------------------------------------------------------------------------------
	class TypeSample
	{
//-------------------------------------------------------------------------------------------------------
        private struct Coordinates
        {
            public int											X;
            public int											Y;
            public int											Z;
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        private Coordinates										MCoordinates;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        TypeSample(int X, int Y, int Z)
        {
            MCoordinates.X=X;
            MCoordinates.Y=Y;
            MCoordinates.Z=Z;
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        public void AddCoordinates()
        {
            int					HashCode=GetHashCode();

            lock (this)
            {
                Coordinates		TempCoord;

                TempCoord.X=MCoordinates.X+100;
                TempCoord.Y=MCoordinates.Y+50;
                TempCoord.Z=MCoordinates.Z+100;

                Console.WriteLine(string.Format("X={0}, Y={1}, Z={2} !", TempCoord.X, TempCoord.Y, TempCoord.Z));
            }
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		static void Main(string[] args)
		{
            TypeSample		Sample=new TypeSample(10,5,20);

            Sample.AddCoordinates();
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
