using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using UsingDependencyInjection.Model;
using UsingDependencyInjection.Service;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Controllers
{
//----------------------------------------------------------------------------------------------------------------------
	public class LifeTimeScopedController : Controller
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly Guid									MID1;
		private readonly Guid									MID2;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public LifeTimeScopedController(ILifeCycleScoped Instance1, ILifeCycleScoped Instance2)
		{
			// !!! Hodnoty oboch GUIDs budu ROVNAKE, no po kazdom REFRESH sa ZMENIA, pretoze novy HTTP REQUEST znamena vytvorenie noveho SCOPE.

			MID1=Instance1.ID;
			MID2=Instance2.ID;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public IActionResult ShowGuids()
		{
			CGuids												Model=new CGuids(MID1,MID2);
			ViewResult											Result=View("ShowGuids",Model);

			return(Result);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------