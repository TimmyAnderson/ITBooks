using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Authorization;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Security
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! CLASS implementuje CUSTOM AUTHORIZATION REQUIREMENT, ktory povoluje iba USERS s danym NAME.
	public sealed class CCustomAuthorizationRequirement : IAuthorizationRequirement
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string[]								MAllowedUsers;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCustomAuthorizationRequirement(string[] AllowedUsers)
		{
			MAllowedUsers=AllowedUsers;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string[]											AllowedUsers
		{
			get
			{
				return(MAllowedUsers);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
