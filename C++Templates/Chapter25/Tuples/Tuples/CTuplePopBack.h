template<typename... Types>
typename CTuplePopBackType<CTuple<Types...>>::TYPE TuplePopBack(const CTuple<Types...>& Tuple)
{
	typename CTuplePopBackType<CTuple<Types...>>::TYPE			Result=TupleReverse(TuplePopFront(TupleReverse(Tuple)));

	return(Result);
}
