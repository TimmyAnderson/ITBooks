using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace HelloWCFLibrary
{
//-------------------------------------------------------------------------------------------------------
	[DataContract]
	public class CPurchaseOrder
	{
//-------------------------------------------------------------------------------------------------------
        private int												MPONumber;
        private CVendorInfo										MVendorInfo;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CPurchaseOrder(int PONumber, CVendorInfo VendorInfo)
		{
			MPONumber=PONumber;
			MVendorInfo=VendorInfo;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		[DataMember]
        public int					PONumber
		{
			get
			{
				return(MPONumber);
			}
			set
			{
				MPONumber=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
		[DataMember]
        public CVendorInfo			VendorInfo
		{
			get
			{
				return(MVendorInfo);
			}
			set
			{
				MVendorInfo=value;
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("PONumber: {0}, VendorInfo: {1}",MPONumber,MVendorInfo.ToString()));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------