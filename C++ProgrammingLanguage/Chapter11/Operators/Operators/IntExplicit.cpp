//-------------------------------------------------------------------------------------------------------
#include "IntExplicit.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
CIntExplicit::CIntExplicit(void)
	: MValue(0)
{
	wprintf(L"DEFAULT CONSTRUCTOR !\n");
}
//-------------------------------------------------------------------------------------------------------
CIntExplicit::CIntExplicit(int Value)
	: MValue(Value)
{
	wprintf(L"CONSTRUCTOR [%d] !\n",MValue);
}
//-------------------------------------------------------------------------------------------------------
CIntExplicit::CIntExplicit(const CIntExplicit& Value)
	: MValue(Value.MValue)
{
	wprintf(L"COPY CONSTRUCTOR [%d] !\n",MValue);
}
//-------------------------------------------------------------------------------------------------------
CIntExplicit::~CIntExplicit(void)
{
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
void CIntExplicit::Print(void)
{
	wprintf_s(L"Value: [%d] !\n",MValue);
}
