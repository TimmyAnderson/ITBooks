using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Components;
using Microsoft.AspNetCore.Components.Rendering;
using Microsoft.AspNetCore.Components.Web;
//----------------------------------------------------------------------------------------------------------------------
namespace AdvancedBlazorFeatures.Blazor
{
//----------------------------------------------------------------------------------------------------------------------
	public partial class RefReferenceParent : ComponentBase
	{
//----------------------------------------------------------------------------------------------------------------------
		private const int										NUMBER_OF_CHILD_COMPONENTS=3;
//----------------------------------------------------------------------------------------------------------------------
		private RefReferenceChild[]								MChildComponents=new RefReferenceChild[NUMBER_OF_CHILD_COMPONENTS];
		private int												MTotalCounter;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public RefReferenceChild[]								ChildComponents
		{
			get
			{
				return(MChildComponents);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												TotalCounter
		{
			get
			{
				return(MTotalCounter);
			}
			set
			{
				MTotalCounter=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public void ChildComponentEvent(object Param)
		{
			int													Counter=0;

			foreach(RefReferenceChild RefReferenceChild in ChildComponents)
			{
				Counter+=RefReferenceChild.Counter;
			}

			TotalCounter=Counter;
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
