using System;
//----------------------------------------------------------------------------------------------------------------------
namespace DataAccess3
{
//----------------------------------------------------------------------------------------------------------------------
	public static class CTextHelpers
	{
//----------------------------------------------------------------------------------------------------------------------
		public static string GetDateText(DateTime Date)
		{
			string												TextDate=$"{Date.Day.ToString("00")}-{Date.Month.ToString("00")}-{Date.Year.ToString("0000")}";

			return(TextDate);
		}
//----------------------------------------------------------------------------------------------------------------------
		public static string GetTimeText(DateTime Time)
		{
			string												TextTime=$"{Time.Hour.ToString("00")}:{Time.Minute.ToString("00")}:{Time.Second.ToString("00")}.{Time.Millisecond.ToString("000")}";

			return(TextTime);
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------