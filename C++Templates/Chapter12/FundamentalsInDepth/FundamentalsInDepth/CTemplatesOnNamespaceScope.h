// !!! TEMPLATE CLASS na NAMESPACE SCOPE.
template<typename TType>
class CTemplateOnNamespaceScope final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		// !!! Toto NIE JE TEMPLATE VARIABLE.
		static constexpr int									STATIC_FIELD=999;

	private:
		TType													MField;

	public:
		// !!! TEMPLATE METHOD.
		const TType& GetField(void) const noexcept
		{
			return(MField);
		}

	public:
		// !!! TEMPLATE CONSTRUCTOR.
		CTemplateOnNamespaceScope(TType Value)
			: MField(Value)
		{
		}

		// !!! TEMPLATE DESTRUCTOR.
		virtual ~CTemplateOnNamespaceScope(void) noexcept
		{
		}
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE FUNCTION na NAMESPACE SCOPE.
template<typename TType>
void TemplateFunctionOnNamespaceScope(TType Parameter)
{
	std::wcout << L"TEMPLATE FUNCTION ON NAMESPACE SCOPE - VALUE [" << Parameter << L"]." << std::endl;
}
