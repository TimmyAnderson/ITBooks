using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Authorization;
//----------------------------------------------------------------------------------------------------------------------
namespace ApplyingCoreIdentity.Security
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CCustomResourceAuthorizationRequirement : IAuthorizationRequirement
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly bool									MAllowAll;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CCustomResourceAuthorizationRequirement(bool AllowAll)
		{
			MAllowAll=AllowAll;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public bool												AllowAll
		{
			get
			{
				return(MAllowAll);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------