using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Newtonsoft.Json;
//--------------------------------------------------------------------------------------------------------------------------------
namespace AzureCosmosDB
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CItemName
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private string											MID;
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
		private string											MSex;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CItemName()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CItemName(string ID, string FirstName, string LastName, int Age, string Sex)
		{
			MID=ID;
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
			MSex=Sex;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		// !!!!! Na CONTAINER musi byt tato PROPERTY nastavena ako PARTITION KEY. TYPE MUSI byt STRING.
		[JsonProperty("id")]
		public string											ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("firstName")]
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
			set
			{
				MFirstName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("lastName")]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			set
			{
				MLastName=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("age")]
		public int												Age
		{
			get
			{
				return(MAge);
			}
			set
			{
				MAge=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		[JsonProperty("sex")]
		public string											Sex
		{
			get
			{
				return(MSex);
			}
			set
			{
				MSex=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
