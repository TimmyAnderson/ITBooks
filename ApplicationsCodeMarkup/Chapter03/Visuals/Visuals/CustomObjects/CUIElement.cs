using System;
using System.Collections.Generic;
using System.Text;
using System.Windows;
using System.Windows.Media;
//------------------------------------------------------------------------
namespace Visuals
{
//------------------------------------------------------------------------
	// !!! Ma metodu OnRender(), ktora je volana, ak ma byt obsah UIElement renderovany.
	public class CUIElement : UIElement
	{
//------------------------------------------------------------------------
		private Point								MCenter;
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		public CUIElement(Point Center)
		{
			MCenter=Center;
		}
//------------------------------------------------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------
		protected override void OnRender(DrawingContext DC)
		{
			DC.DrawEllipse(new SolidColorBrush(Color.FromRgb(255,0,0)),new Pen(new SolidColorBrush(Color.FromRgb(255,0,0)),3),MCenter,10,10);
		}
//------------------------------------------------------------------------
	}
//------------------------------------------------------------------------
}
//------------------------------------------------------------------------