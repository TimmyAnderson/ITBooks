using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace DataAccess3
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CEntityRawSql
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private int												MCategory;
		private int												MValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CEntityRawSql()
		{
			MID=default;
			MCategory=default;
			MValue=default;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityRawSql(int Category, int Value)
		{
			MCategory=Category;
			MValue=Value;
		}
//----------------------------------------------------------------------------------------------------------------------
		public CEntityRawSql(int ID, int Category, int Value)
		{
			MID=ID;
			MCategory=Category;
			MValue=Value;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Category
		{
			get
			{
				return(MCategory);
			}
			set
			{
				MCategory=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public int												Value
		{
			get
			{
				return(MValue);
			}
			set
			{
				MValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static void PrintToConsole(CEntityRawSql[] Entities)
		{
			for(int Index=0;Index<Entities.Length;Index++)
			{
				CEntityRawSql									Entity=Entities[Index];
				string											EntityText=Entity.Print(Index+1);
							
				if (Index>0)
				{
					Console.WriteLine(new string('-',80));
				}

				Console.Write(EntityText);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string Print(int? Index)
		{
			StringBuilder										SB=new StringBuilder();

			if (Index!=null)
			{
				SB.AppendLine($"ENTITY RAW SQL [{Index}]:");
			}
			else
			{
				SB.AppendLine($"ENTITY RAW SQL:");
			}

			SB.AppendLine($"\tID [{MID}] !");
			SB.AppendLine($"\tCATEGORY [{MCategory}] !");
			SB.AppendLine($"\tVALUE [{MValue}] !");

			return(SB.ToString());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------