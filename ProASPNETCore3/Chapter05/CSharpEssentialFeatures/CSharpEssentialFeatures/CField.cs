using System;
using System.Collections.Generic;
using System.Text;
//----------------------------------------------------------------------------------------------------------------------
namespace CSharpEssentialFeatures
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CField
	{
//----------------------------------------------------------------------------------------------------------------------
		private	string											MValue;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CField(string Value)
		{
			MValue=Value;

			Console.WriteLine($"CONSTRUCTOR CField CALLED with VALUE [{Value}] !");
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Value
		{
			get
			{
				return(MValue);
			}
			set
			{
				MValue=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
