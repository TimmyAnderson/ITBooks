using System;
using System.Collections.Generic;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Channels;
using DCEventsLibrary;
//-------------------------------------------------------------------------------------------------------
namespace DCEventsClient
{
//-------------------------------------------------------------------------------------------------------
	public class CDCEventsProxy : ClientBase<IDCEventsContract>, IDCEventsContract
	{
//-------------------------------------------------------------------------------------------------------
		public CDCEventsProxy(Binding ProxyBinding, string EndpointAddress)
			: base(ProxyBinding, new EndpointAddress(EndpointAddress))
		{
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CName GetName()
		{
 			return(Channel.GetName());
		}
//-------------------------------------------------------------------------------------------------------
		public void SendName(CName Name)
		{
			Channel.SendName(Name);
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
