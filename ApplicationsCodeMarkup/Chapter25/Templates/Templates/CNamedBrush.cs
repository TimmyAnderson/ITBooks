using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Reflection;
using System.Windows.Media;
//-------------------------------------------------------------------------------------------------------
namespace Templates
{
//-------------------------------------------------------------------------------------------------------
	public class CNamedBrush
	{
//-------------------------------------------------------------------------------------------------------
        private static CNamedBrush[]							MNBrushes;
//-------------------------------------------------------------------------------------------------------
        private Brush											MBrush;
        private string											MStr;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        static CNamedBrush()
        {
            PropertyInfo[]		Props=typeof(Brushes).GetProperties();

            MNBrushes=new CNamedBrush[Props.Length];

            for (int i=0; i<Props.Length;i++)
                MNBrushes[i]=new CNamedBrush(Props[i].Name, (Brush)Props[i].GetValue(null, null));
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        private CNamedBrush(string Str, Brush Brush)
        {
            this.MStr=Str;
            this.MBrush=Brush;
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        public static CNamedBrush[]								All
        {
            get
			{
				return(MNBrushes);
			}
        }
//-------------------------------------------------------------------------------------------------------
        public Brush											Brush
        {
            get
			{
				return(MBrush);
			}
        }
//-------------------------------------------------------------------------------------------------------
        public string											Name
        {
            get
            {
                string		StrSpaced=MStr[0].ToString();

                for (int i=1;i<MStr.Length;i++)
                    StrSpaced+=(char.IsUpper(MStr[i]) ? " " : "") + MStr[i].ToString();

                return(StrSpaced);
            }
        }
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
        public override string ToString()
        {
            return(MStr);
        }
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------