using System;
//--------------------------------------------------------------------------------------------------------------------------------
namespace WebAPICommonPracticesWeb
{
//--------------------------------------------------------------------------------------------------------------------------------
	public sealed class CMyCachedData
	{
//--------------------------------------------------------------------------------------------------------------------------------
		private string											MValue;
		private DateTime										MDateTime;
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyCachedData()
		{
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public CMyCachedData(string Value, DateTime DateTime)
		{
			MValue=Value;
			MDateTime=DateTime;
		}
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------------
		public string											Value
		{
			get
			{
				return(MValue);
			}
			set
			{
				MValue=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
		public DateTime											DateTime
		{
			get
			{
				return(MDateTime);
			}
			set
			{
				MDateTime=value;
			}
		}
//--------------------------------------------------------------------------------------------------------------------------------
	}
//--------------------------------------------------------------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------------------------------------------------------