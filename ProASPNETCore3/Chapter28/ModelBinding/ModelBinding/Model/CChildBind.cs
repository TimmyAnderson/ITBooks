using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc.ModelBinding;
//----------------------------------------------------------------------------------------------------------------------
namespace ModelBinding.Model
{
//----------------------------------------------------------------------------------------------------------------------
	// !!! PROPERTY [BindRequiredAttribute] moze byt aplikovany na CLASS, PROPERTY a PARAMETER.
	public sealed class CChildBind
	{
//----------------------------------------------------------------------------------------------------------------------
		private int												MID;
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
		private ESex											MSex;
		private CChildAddress									MAddress;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CChildBind()
		{
		}
//----------------------------------------------------------------------------------------------------------------------
		public CChildBind(int ID, string FirstName, string LastName, int Age, ESex Sex, CChildAddress Address)
		{
			MID=ID;
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
			MSex=Sex;
			MAddress=Address;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY NEBUDE BOUND.
		[BindNever]
		public int												ID
		{
			get
			{
				return(MID);
			}
			set
			{
				MID=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! Ak by PROPERTY nebola BOUND, dojde k VALIDATION ERROR.
		[BindRequired]
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
			set
			{
				MFirstName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! Ak by PROPERTY nebola BOUND, dojde k VALIDATION ERROR.
		[BindRequired]
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
			set
			{
				MLastName=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! Ak by PROPERTY nebola BOUND, dojde k VALIDATION ERROR.
		[BindRequired]
		public int												Age
		{
			get
			{
				return(MAge);
			}
			set
			{
				MAge=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! Ak by PROPERTY nebola BOUND, dojde k VALIDATION ERROR.
		[BindRequired]
		public ESex												Sex
		{
			get
			{
				return(MSex);
			}
			set
			{
				MSex=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		// !!! PROPERTY NEBUDE BOUND.
		[BindNever]
		public CChildAddress									Address
		{
			get
			{
				return(MAddress);
			}
			set
			{
				MAddress=value;
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return($"[{MFirstName}] [{MLastName}] [{MAge}] [{MSex}] [{MAddress}]");
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
