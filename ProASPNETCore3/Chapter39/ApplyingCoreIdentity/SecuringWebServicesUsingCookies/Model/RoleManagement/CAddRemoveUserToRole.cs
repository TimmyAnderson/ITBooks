			MUserName=UserName;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											RoleID
		{
			get
			{
				return(MRoleID);
			}
