using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;
//-------------------------------------------------------------------------------------------------------
namespace SerializationDeserialization
{
//-------------------------------------------------------------------------------------------------------
	[Serializable]
	public class CDerivedNameISerializable : CBaseNameISerializable
	{
//-------------------------------------------------------------------------------------------------------
		private int												MAge;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		// DESERIALIZACNY KONSTURKTOR.
		// !!! MUSIM VOLAT DESERIALIZACNY KONSTRUKTOR BASE CLASS.
		private CDerivedNameISerializable(SerializationInfo SI, StreamingContext SC)
			: base(SI,SC)
		{
			Console.WriteLine("DESERIALIZATION CONSTRUCTOR - CDerivedNameISerializable() CALLED, SC.State: [{0}], SC.Context: [{1}] !",SC.State,SC.Context);

			MAge=SI.GetInt32("Age");
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CDerivedNameISerializable(string FirstName, string LastName, int Age)
			: base(FirstName,LastName)
		{
			MAge=Age;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public int												Age
		{
			get
			{
				return(MAge);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		// SERIALIZACNA METODA.
		public override void GetObjectData(SerializationInfo SI, StreamingContext SC)
		{
			Console.WriteLine("SERIALIZATION METHOD - CDerivedNameISerializable.GetObjectData() CALLED, SC.State: [{0}], SC.Context: [{1}] !",SC.State,SC.Context);

			// MUSIM VOLAT PARENT METODU.
			base.GetObjectData(SI,SC);

			SI.AddValue("Age",MAge);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("{0}, Age: [{1}]",base.ToString(),MAge));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------