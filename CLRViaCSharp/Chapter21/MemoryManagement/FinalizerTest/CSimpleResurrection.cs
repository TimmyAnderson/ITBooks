using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading;
//-------------------------------------------------------------------------------------------------------
namespace FinalizerTest
{
//-------------------------------------------------------------------------------------------------------
	public class CSimpleResurrection
	{
//-------------------------------------------------------------------------------------------------------
		private static CSimpleResurrection						MResurrectedObject=null;
		private static bool										MDoResurrection=true;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CSimpleResurrection(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		~CSimpleResurrection()
		{
			Console.WriteLine("Finalize() CALLED !");

			if (MDoResurrection==true)
			{
				// Spravim RESURRECTION OBJEKTU.
				MResurrectedObject=this;

				// !!! Prinutim GC, aby ZNOVA ZAVOLAL Finalize().
				GC.ReRegisterForFinalize(this);

				Console.WriteLine(string.Format("Name: [{0}] RESURRECTED !",this));
			}
			else
			{
				Console.WriteLine(string.Format("Name: [{0}] WON'T BE RESURRECTED and will DIE !",this));
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public int												Age
		{
			get
			{
				return(MAge);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public static void ResetResurrectedObjectToNULL()
		{
			// Zrusim ROOT na RESURRECTED objekt.
			MResurrectedObject=null;
			MDoResurrection=false;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("First name: [{0}], Last name: [{1}], Age: [{2}]",MFirstName,MLastName,MAge));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------