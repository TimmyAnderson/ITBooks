using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace TestingApplications.Models
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CProductDataSource : IDataSource
	{
//----------------------------------------------------------------------------------------------------------------------
		public IEnumerable<CProduct>							Products
		{
			get
			{
				IEnumerable<CProduct>							Result=GetProducts();

				return(Result);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public static CProduct[] GetProducts()
		{
			List<CProduct>										Products=new List<CProduct>();

			Products.Add(new CProduct("Kayak",275M));
			Products.Add(new CProduct("Lifejacket",48.95M));

			return(Products.ToArray());
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
