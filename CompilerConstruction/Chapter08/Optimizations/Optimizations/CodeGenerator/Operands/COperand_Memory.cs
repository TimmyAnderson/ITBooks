using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
//-------------------------------------------------------------------------------------------------------
namespace Optimizations
{
//-------------------------------------------------------------------------------------------------------
	public sealed class COperand_Memory : COperand
	{
//-------------------------------------------------------------------------------------------------------
		private ERegister										MRegister;
		private int?											MOffset;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public COperand_Memory(ERegister Register)
		{
			MRegister=Register;
		}
//-------------------------------------------------------------------------------------------------------
		public COperand_Memory(ERegister Register, int Offset)
		{
			MRegister=Register;
			MOffset=Offset;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public ERegister										Register
		{
			get
			{
				return(MRegister);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public int?												Offset
		{
			get
			{
				return(MOffset);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public override string GetOperandText()
		{
			string												RegisterName=MRegister.EXT_GetRegisterName();

			if (MOffset!=null)
			{
				string											OperandMemoryOperationText=(MOffset>=0) ? "+" : "";
				string											OperandText=string.Format("[{0}{1}{2}]",RegisterName,OperandMemoryOperationText,MOffset);

				return(OperandText);
			}
			else
			{
				string											OperandText=string.Format("[{0}]",RegisterName);

				return(OperandText);
			}
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------------------------