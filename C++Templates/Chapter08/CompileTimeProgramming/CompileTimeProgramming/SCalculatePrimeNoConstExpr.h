// !!! INTERNA TEMPLATE pocitajuca v COMPILE TIME ci cislo je prvocislo.
template<unsigned int NUMBER, unsigned int DIVISOR>
struct SCalculatePrimeNoConstExprInternal
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=(((NUMBER%DIVISOR)!=0) && SCalculatePrimeNoConstExprInternal<NUMBER,(DIVISOR-1)>::IS_PRIME);
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! INTERNA TEMPLATE SPECIALIZATION sluziaca ako koniec RECURSION.
template<unsigned int NUMBER>
struct SCalculatePrimeNoConstExprInternal<NUMBER,2>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=((NUMBER%2)!=0);
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE pocitajuca v COMPILE TIME ci cislo je prvocislo.
template<unsigned int NUMBER>
struct SCalculatePrimeNoConstExpr
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=SCalculatePrimeNoConstExprInternal<NUMBER,(NUMBER/2)>::IS_PRIME;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// !!! TEMPLATE SPECIALIZATIONS sluziace ako koniec RECURSION.
template<>
struct SCalculatePrimeNoConstExpr<0>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=false;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
template<>
struct SCalculatePrimeNoConstExpr<1>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=false;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
template<>
struct SCalculatePrimeNoConstExpr<2>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=true;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
template<>
struct SCalculatePrimeNoConstExpr<3>
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr bool									IS_PRIME=true;
//----------------------------------------------------------------------------------------------------------------------
};
