//-------------------------------------------------------------------------------------------------------
#include "IntConversions.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------------------------------
CIntConversions::CIntConversions(void)
	: MValue(0)
{
	wprintf(L"DEFAULT CONSTRUCTOR !\n");
}
//-------------------------------------------------------------------------------------------------------
CIntConversions::CIntConversions(int Value)
	: MValue(Value)
{
	wprintf(L"CONSTRUCTOR [%d] !\n",Value);
}
//-------------------------------------------------------------------------------------------------------
CIntConversions::~CIntConversions(void)
{
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
CIntConversions::operator int(void)
{
	wprintf(L"CONVERSION OPERATOR [%d] to INT !\n",MValue);

	return(MValue);
}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
void CIntConversions::Print(wstring Prefix)
{
	wprintf_s(L"%s: [%d] !\n",Prefix.c_str(),MValue);
}
