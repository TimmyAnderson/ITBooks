using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingUrlRouting.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CResult
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MUrl;
		private readonly string									MEndpoint;
		private readonly Dictionary<string,object>				MData;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CResult(string Url, string Endpoint)
		{
			MUrl=Url;
			MEndpoint=Endpoint;
			MData=new Dictionary<string,object>();
		}
//----------------------------------------------------------------------------------------------------------------------
		public CResult(string Url, string Endpoint, Dictionary<string,object> Data)
		{
			MUrl=Url;
			MEndpoint=Endpoint;
			MData=Data;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											Url
		{
			get
			{
				return(MUrl);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											Endpoint
		{
			get
			{
				return(MEndpoint);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public Dictionary<string,object>						Data
		{
			get
			{
				return(MData);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------