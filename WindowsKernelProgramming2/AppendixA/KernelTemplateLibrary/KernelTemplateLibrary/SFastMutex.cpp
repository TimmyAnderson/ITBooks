namespace KTL
{
//----------------------------------------------------------------------------------------------------------------------
	void SFastMutex::Init(void)
	{
		ExInitializeFastMutex(&MMutex);
	}
//----------------------------------------------------------------------------------------------------------------------
	void SFastMutex::Lock(void)
	{
		ExAcquireFastMutex(&MMutex);
	}
//----------------------------------------------------------------------------------------------------------------------
	void SFastMutex::Unlock(void)
	{
		ExReleaseFastMutex(&MMutex);
	}
//----------------------------------------------------------------------------------------------------------------------
}
