//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <string>
#include <iostream>
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename... TTypes>
class CVariadicTemplateClass;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<>
class CVariadicTemplateClass<> final
{
//----------------------------------------------------------------------------------------------------------------------
	public:
		static constexpr size_t									SIZE=0;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
class CVariadicTemplateClass<TFirst,TTypes...> final
{
//----------------------------------------------------------------------------------------------------------------------
	template<typename... TLocalTypes>
	friend class CVariadicTemplateClass;

	public:
		static constexpr size_t									SIZE=(1+sizeof...(TTypes));

	private:
		TFirst													MField;
		CVariadicTemplateClass<TTypes...>						MFields;

	private:
		void PrintInternal(const std::wstring& Text, size_t Index) const;

	public:
		void Print(const std::wstring& Text) const;

	public:
		CVariadicTemplateClass(void);
		CVariadicTemplateClass(TFirst First, TTypes... Types);
		virtual ~CVariadicTemplateClass(void) noexcept;
//----------------------------------------------------------------------------------------------------------------------
};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplateClass<TFirst,TTypes...>::CVariadicTemplateClass(void)
	: MField(), MFields()
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplateClass<TFirst,TTypes...>::CVariadicTemplateClass(TFirst First, TTypes... Types)
	: MField(First), MFields(Types...)
{
}
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
CVariadicTemplateClass<TFirst,TTypes...>::~CVariadicTemplateClass(void) noexcept
{
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
void CVariadicTemplateClass<TFirst,TTypes...>::PrintInternal(const std::wstring& Text, size_t Index) const
{
	std::wcout << Text << L" - PARAMETER [" << Index << L"] - TYPE [" << GetTypeInfoName<TFirst>() << L"] - VALUE [" << MField << L"]." << std::endl;

	if constexpr (sizeof...(TTypes)>0)
	{
		MFields.PrintInternal(Text,Index+1);
	}
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
template<typename TFirst, typename... TTypes>
void CVariadicTemplateClass<TFirst,TTypes...>::Print(const std::wstring& Text) const
{
	PrintInternal(Text,1);
}
