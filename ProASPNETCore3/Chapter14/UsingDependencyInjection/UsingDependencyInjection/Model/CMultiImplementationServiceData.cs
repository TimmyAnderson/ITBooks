using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
//----------------------------------------------------------------------------------------------------------------------
namespace UsingDependencyInjection.Model
{
//----------------------------------------------------------------------------------------------------------------------
	public sealed class CMultiImplementationServiceData
	{
//----------------------------------------------------------------------------------------------------------------------
		private readonly string									MServiceName;
		private readonly string									MServiceText;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public CMultiImplementationServiceData(string ServiceName, string ServiceText)
		{
			MServiceName=ServiceName;
			MServiceText=ServiceText;
		}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
		public string											ServiceName
		{
			get
			{
				return(MServiceName);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
		public string											ServiceText
		{
			get
			{
				return(MServiceText);
			}
		}
//----------------------------------------------------------------------------------------------------------------------
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------