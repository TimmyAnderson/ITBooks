//-----------------------------------------------------------------------------
CExceptionMove::CExceptionMove(const wstring& Value)
	: MValue(Value)
{
	wcout << L"CONSTRUCTOR [" << __func__ << L"()] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
CExceptionMove::CExceptionMove(CExceptionMove&& Value) noexcept
	: MValue(std::move(Value.MValue))
{
	wcout << L"!!!!! MOVE CONSTRUCTOR [" << __func__ << L"()] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
CExceptionMove::~CExceptionMove(void)
{
	wcout << L"DESTRUCTOR [" << __func__ << L"()] CALLED !" << endl;
}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
CExceptionMove& CExceptionMove::operator=(CExceptionMove&& Value) noexcept
{
	wcout << L"!!!!! MOVE OPERATOR= [" << __func__ << L"()] CALLED !" << endl;

	MValue=move(Value.MValue);

	return(*this);
}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
const wstring& CExceptionMove::GetValue(void) const noexcept
{
	return(MValue);
}
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
wstring CExceptionMove::GetTextMessage(void) const noexcept
{
	wstring														Message;

	Message.append(L"EXCEPTION [CExceptionMove] - VALUE [");
	Message.append(MValue);
	Message.append(L"] !");

	return(Message);
}
