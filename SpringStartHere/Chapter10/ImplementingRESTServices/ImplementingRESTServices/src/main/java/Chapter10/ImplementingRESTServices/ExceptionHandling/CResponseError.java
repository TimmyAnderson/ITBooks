package Chapter10.ImplementingRESTServices.ExceptionHandling;
//----------------------------------------------------------------------------------------------------------------------
// !!! CLASS reprezentuje HTTP RESPONSE BODY ak WEB SERVICE OPERATION skonci hodenim EXCEPTION.
public class CResponseError
{
//----------------------------------------------------------------------------------------------------------------------
	private String												MErrorMessage;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public CResponseError()
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	public CResponseError(String ErrorMessage)
	{
		MErrorMessage=ErrorMessage;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	public String getErrorMessage()
	{
		return(MErrorMessage);
	}
//----------------------------------------------------------------------------------------------------------------------
	public void setErrorMessage(String ErrorMessage)
	{
		MErrorMessage=ErrorMessage;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------