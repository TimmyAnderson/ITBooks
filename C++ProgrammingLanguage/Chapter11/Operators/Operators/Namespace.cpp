//-------------------------------------------------------------------------------------------------------
#include "Namespace.h"
#include <stdio.h>
//-------------------------------------------------------------------------------------------------------
namespace MyNamespace
{
//-------------------------------------------------------------------------------------------------------
	// GLOBALNY OPERATOR.
	CNamespace operator-(CNamespace& LeftOperand, CNamespace& RightOperand)
	{
		wprintf(L"GLOBAL OPERATOR-(CInt&,CInt&) !\n");

		CNamespace												Result(LeftOperand.GetValue()-RightOperand.GetValue());

		return(Result);
	}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	// GLOBALNA FUNKCIA.
	void PrintGlobalFunction(CNamespace& Object)
	{
		wprintf_s(L"Value: [%d] !\n",Object.GetValue());
	}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	CNamespace::CNamespace(void)
		: MValue(0)
	{
	}
//-------------------------------------------------------------------------------------------------------
	CNamespace::CNamespace(int Value)
		: MValue(Value)
	{
	}
//-------------------------------------------------------------------------------------------------------
	CNamespace::~CNamespace(void)
	{
	}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	CNamespace CNamespace::operator+(CNamespace& RightOperand)
	{
		wprintf(L"MEMBER OPERATOR+ !\n");

		CNamespace												Result(MValue+RightOperand.MValue);

		return(Result);
	}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
	void CNamespace::Print(void)
	{
		wprintf_s(L"Value: [%d] !\n",MValue);
	}
//-------------------------------------------------------------------------------------------------------
	void PrintGlobalFunctionEmpty(int Value)
	{
		wprintf_s(L"Value: [%d] !\n",Value);
	}
//-------------------------------------------------------------------------------------------------------
}
