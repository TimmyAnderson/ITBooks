using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Remoting.Lifetime;
//-------------------------------------------------------------------------------------------------------
namespace AppDomains
{
//-------------------------------------------------------------------------------------------------------
	// !!! Kedze NEDEDI z MarshalByRefObject ANI NIE JE SERIALIZOVATELNA, NEMOZE PREKROCIT HRANICE APP DOMAIN.
	public class CName_NotMarshaled
	{
//-------------------------------------------------------------------------------------------------------
		private string											MFirstName;
		private string											MLastName;
		private int												MAge;
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public CName_NotMarshaled()
		{
			MFirstName="Timmy";
			MLastName="Anderson";
			MAge=12;
		}
//-------------------------------------------------------------------------------------------------------
		public CName_NotMarshaled(string FirstName, string LastName, int Age)
		{
			MFirstName=FirstName;
			MLastName=LastName;
			MAge=Age;
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		~CName_NotMarshaled()
		{
			string												DomainName=AppDomain.CurrentDomain.FriendlyName;
			string												Text=string.Format("!!! FINALIZE - Domain: [{0}] - Name: [{1}] !",DomainName,this.ToString());

			Console.WriteLine(Text);
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public string											FirstName
		{
			get
			{
				return(MFirstName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public string											LastName
		{
			get
			{
				return(MLastName);
			}
		}
//-------------------------------------------------------------------------------------------------------
		public int												Age
		{
			get
			{
				return(MAge);
			}
		}
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------
		public void PrintFullName()
		{
			string												DomainName=AppDomain.CurrentDomain.FriendlyName;
			string												Text=string.Format("CName_NotMarshaled.PrintFullName() - Domain: [{0}] - Name: [{1}] !",DomainName,this.ToString());

			Console.WriteLine(Text);
		}
//-------------------------------------------------------------------------------------------------------
		public override string ToString()
		{
			return(string.Format("First name: [{0}], Last name: [{1}], Age: [{2}]",MFirstName,MLastName,MAge));
		}
//-------------------------------------------------------------------------------------------------------
	}
//-------------------------------------------------------------------------------------------------------
}
