//-------------------------------------------------------------------------------------------------------
template<typename TType1=std::wstring, typename TType2=int>
class CDefaultParameters
{
//-------------------------------------------------------------------------------------------------------
	private:
		TType1													MValue1;
		TType2													MValue2;

	public:
		TType1 GetValue1(void)
		{
			return(MValue1);
		}

		TType2 GetValue2(void)
		{
			return(MValue2);
		}

	public:
		CDefaultParameters(TType1 Value1, TType2 Value2)
			: MValue1(Value1), MValue2(Value2)
		{
		}
//-------------------------------------------------------------------------------------------------------
};
