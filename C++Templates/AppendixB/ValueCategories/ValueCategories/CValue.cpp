//----------------------------------------------------------------------------------------------------------------------
#include "CValue.h"
#include <iostream>
//----------------------------------------------------------------------------------------------------------------------
using namespace std;
//----------------------------------------------------------------------------------------------------------------------
CValue::CValue(void)
	: MValue(0)
{
	wcout << L"DEFAULT CONSTRUCTOR CALLED !" << endl;
}
//----------------------------------------------------------------------------------------------------------------------
CValue::CValue(int Value)
	: MValue(Value)
{
	wcout << L"CONSTRUCTOR CALLED !" << endl;
}
//----------------------------------------------------------------------------------------------------------------------
CValue::~CValue(void)
{
	wcout << L"DESTRUCTOR CALLED !" << endl;
}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
int CValue::GetValue(void) const noexcept
{
	return(MValue);
}
