//----------------------------------------------------------------------------------------------------------------------
#include "CMutex.h"
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
void CMutex::Init(void)
{
	KeInitializeMutex(&MMutex,0);
}
//----------------------------------------------------------------------------------------------------------------------
void CMutex::Lock(void)
{
	KeWaitForSingleObject(&MMutex,KWAIT_REASON::Executive,MODE::KernelMode,FALSE,nullptr);
}
//----------------------------------------------------------------------------------------------------------------------
void CMutex::Unlock(void)
{
	KeReleaseMutex(&MMutex,FALSE);
}
